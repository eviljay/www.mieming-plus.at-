  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Die Mitglieder der &Ouml;sterreichischen Bergrettung sind hervorragend ausgebildete Profis: Zu jeder Tages- und Nachtzeit sind sie binnen k&uuml;rzester Zeit bei der Unfallstelle und f&uuml;r Eins&auml;tze in steilem und unwegsamem Gel&auml;nde bestens ger&uuml;stet. Sie leisten bei Alpinunf&auml;llen, beim Transport von Verungl&uuml;ckten, bei Pisten- und Loipendiensten, bei Fels- und Eisr&auml;umungen sowie bei Lawinenungl&uuml;cken und Vermisstensuche professionelle und unverzichtbare Dienste.</p>
