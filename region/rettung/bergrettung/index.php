  <td colspan="3"><h2><a name="224263992f0bc464d"></a>Bergrettung Telfs</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Die Bergrettung hat in Tirol eine lange Tradition. So ging die Bergrettung Telfs 1930 aus der alpinen Vereinigung &bdquo;Bergs&ouml;hne&ldquo; unter dem Namen das Gr&uuml;ne-Kreuz hervor. In einem Tourismus- und Gebirgsland wie Tirol nimmt die Bergrettung einen unverzichtbaren Platz im Rettungswesen ein. Im Bereich der alpinen Unfallverh&uuml;tung ebenso wie auf dem Gebiet der Flugrettung z&auml;hlen die Bergretter im etablierten Rettungssystem zu einem der wichtigsten Partner des Landes Tirol.<br><br></p>
