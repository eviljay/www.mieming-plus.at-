    <td valign="top"><p>Das <b>Mieminger Gebirge</b> (auch <b>Mieminger Kette</b> genannt) ist eine Gebirgsgruppe der N&ouml;rdlichen Kalkalpen in den Ostalpen. Es befindet sich vollumf&auml;nglich in &Ouml;sterreich im Bundesland Tirol. Die Untergruppe befindet sich etwas im Schatten des ber&uuml;hmteren, direkt im Norden anschlie&szlig;enden Wetterstein. W&auml;hrend das Gebiet um Coburger H&uuml;tte, Seeben- und Drachensee im Westen (Ehrwalder Sonnenspitze und Vorderer Tajakopf mit neuem Klettersteig &uuml;ber die Tajakante) und die Hohe Munde ganz im Osten regen Besuch erhalten, ist es im wenig erschlossenen Mittelteil sehr ruhig geblieben. Die Hohe Munde ist auch ein beliebtes anspruchsvolles Skitourenziel.<br><br>&Ouml;ffentliche Verkehrsanbindung: In Ehrwald an der Westseite des Gebirges h&auml;lt die Au&szlig;erfernbahn. Nach Leutasch an der S&uuml;dseite fahren Busse von Mittenwald und Seefeld in Tirol.</p>
