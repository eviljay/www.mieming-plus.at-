  <td colspan="3"><h2><a name="031f2a99cd1420950"></a>Kraftstoff f&uuml;r sechs Stunden Flugzeit</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Der Dieselkraftstoff im Tank der Diamond Star reicht f&uuml;r sechs Stunden Flugzzeit. Eine mehr als ausreichende Menge f&uuml;r einen Alpenrundflug von einer Stunde. Unser Pilot hat die Checkliste auf dem rechten Oberschenkel. Alles geht nach Plan: Luftdruck messen, Motortest - dabei verstellt sich der Propeller - 900 Umdrehungen, passt. Ein allerletzter Blick auf die Messger&auml;te, die &Ouml;ltemperatur ist im gr&uuml;nen Bereich.</p>
