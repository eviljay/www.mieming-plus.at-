          <tr>
    <td></td>
    <td valign="top"><p>Das Flugzeug mu&szlig;te dann f&uuml;r den Flug vorbereitet werden. Die Schutzabdeckungen werden abgenommen, es folgen unterschiedliche Checks, dazu geh&ouml;rt das &quot;drainieren&quot;, d.h. dass Maschinen&ouml;l wird auf Kondenswasserr&uuml;ckst&auml;nde untersucht. Die Probe war negativ. Wir fliegen mit dem einmotorigen Leichtflugzeug Diamond Star, Kennzeichen: D-ERRP, vor einem Jahr bei Diamond Aircraft in Wiener-Neustadt gebaut.</p>
</td>
