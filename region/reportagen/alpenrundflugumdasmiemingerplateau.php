      <tr>
    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Pilot Ing. Werner Kirchebner aus Mieming-Weidach lud Mieming Plus zu einem Rundflug ein: von Innsbruck, &uuml;ber den Arlberg bis Reutte/Fernpass und wieder zur&uuml;ck &uuml;ber das Mieminger Plateau zum Flughafen Innsbruck. Bei herrlichstem Flugwetter und Temperaturen &uuml;ber 25 Grad Celsius ging es mit dem Composite Flugzeug DA40 Diamond Star am Montag, dem 8. Oktober 2007 gegen 10.40 Uhr MEZ in die Luft. Mieming Plus ver&ouml;ffentlicht 160 Fotos, die den Betrachter zum Mitfliegen einladen. 	  	  </td>
    </tr>
</table><br><br>
</td>
    <td></td>
