  <td colspan="3"><h2><a name="031f2a99cd128848d"></a>Einladung zum Fotoshooting &uuml;ber den Alpen</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>&quot;Ihr macht gute Fotos in Mieming Plus&quot;, lobte Ing. Werner Kirchebner aus Weidach unsere Arbeit, &quot;wenn Ihr Lust dazu habt, lade ich Euch zu einem Fotoshooting &uuml;ber die Alpen ein&quot;. Wir hatten Lust: Vom Flughafen Innsbruck - in der Fliegersprache kurz LOWI genannt - starteten wir bei sonnigem Wetter gegen 10.40 Uhr. Die Flieger orientieren sich nach der Greenwich Mean Time (GMT), danach war es 8.40 Uhr, zwei Stunden fr&uuml;her.</p>
</td>
