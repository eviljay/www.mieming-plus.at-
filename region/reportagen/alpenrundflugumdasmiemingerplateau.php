  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Vor dem Abflug war allerdings f&uuml;r den Piloten Werner Kirchebner noch einiges zu tun: Der Flugplan f&uuml;r den Zivilflug mu&szlig;te ausgef&uuml;llt werden und online nach Wien geschickt werden. Er enth&auml;lt u.a.die Routenbeschreibung, die Luftfahrzeug-Kennung und den Flugmeldepunkt. Der war &uuml;ber M&ouml;tz vorgesehen. Nachdem ein gr&uuml;nes &quot;F&quot; am Bildschirm aufleuchtete, war der Flug freigegeben.</p>
</td>
