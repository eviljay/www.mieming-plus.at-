  <td colspan="3"><h2><a name="031f2a9ba00423f03"></a>Ausgangspunkt war die Kapelle zum Hl. Benedikt</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Der Ausgangspunkt wurde am Vorabend, beim Feuerwehrball im Gespr&auml;ch mit dem Barwieser Ortsbauernobmann und Mieminger Gemeinderat Benedikt van Staa vereinbart. &bdquo;Schau Dir doch mal unsere Kapelle an, die wir dem Heiligen Benedikt gewidmet haben und die 2005 errichtet wurde.&ldquo; Das lateinische &bdquo;bene dictus&ldquo; hei&szlig;t &bdquo;wohl gesagt&ldquo; und damit war der Vorschlag angenommen. Die Kapelle zum Heiligen Benedikt liegt beim Hof von Benedikt van Staa in Barwies am Zirchbichl.</p>
</td>
