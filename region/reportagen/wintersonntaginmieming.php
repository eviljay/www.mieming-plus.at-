  <td colspan="3"><h2><a name="031f2a9ba0044a406"></a>Weg zur Kapelle ist lohnenswert</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Der Weg zur Kapelle ist mehr als lohnenswert. Eine kleine Bank in der offenen Holzkonstruktion mit einem Benedikt-Fenster l&auml;dt zur Meditation ein und &ouml;ffnet an diesem Sonntag einen Blick auf schneewei&szlig;e Winterlandschaften. Am Horizont sieht man den Zwiebelturm der Pfarrkirche in Barwies, unser n&auml;chstes Ziel. Der Betrachter &uuml;bt sich in Demut, wohl auch daran denkend, das &bdquo;Zucht und Ma&szlig;&ldquo; ein Konzept des Kl&ouml;stergr&uuml;nders Benedikt war.</p>
