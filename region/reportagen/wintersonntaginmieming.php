  <td colspan="3"><h2><a name="031f2a9b9f0e27d07"></a>Winterwandertag von Barwies zur St&ouml;ttlalm</h2></td>
  <td></td>
  </tr>
      <tr>
    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Mieming-Plus spiegelt den herrlichen Wintersonntag in Mieming am 25. J&auml;nner 2009 in einer Fotoreportage. Wir waren in Barwies, See, Untermieming, Weidach und Obermieming. Der Wandertag begann an der Kapelle zum Hl. Benedikt in Barwies, f&uuml;hrte von dort, am Feldkreuz vorbei, nach Untermieming, See, Weidach wieder zur&uuml;ck nach Obermieming und hatte schlie&szlig;lich die St&ouml;ttlalm am Fu&szlig;e der Mieminger Kette zum Ziel. Der Vormittagshimmel war noch etwas nebelverhangen, um die Mittagszeit kam die Sonne heraus und bei Temperaturen zwischen -5 bis -7 Grad hatten wir ein ideales Fotowetter. 	  	  </td>
