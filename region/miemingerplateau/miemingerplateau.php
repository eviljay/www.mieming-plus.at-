		      	        <b>Das </b><a href="../miemingergebirge/index.php">Mieminger Gebirge</a><b>:</b> (auch <a href="../miemingergebirge/index.php">Mieminger Kette</a> genannt) ist eine Gebirgsgruppe der N&ouml;rdlichen Kalkalpen in den Ostalpen. Es befindet sich im Bundesland Tirol/&Ouml;sterreich. Die Untergruppe befindet sich etwas im Schatten des ber&uuml;hmteren, direkt im Norden anschlie&szlig;enden Wetterstein. W&auml;hrend das Gebiet um Coburger H&uuml;tte, Seeben- und Drachensee im Westen (Ehrwalder Sonnenspitze und Vorderer Tajakopf mit neuem Klettersteig &uuml;ber die Tajakante) und die <a href="../miemingergebirge/hohemunde/index.php">Hohe Munde</a> (s. Foto) ganz im Osten regen Besuch erhalten, ist es im wenig erschlossenen Mittelteil sehr ruhig geblieben. Die Hohe Munde ist auch ein beliebtes anspruchsvolles Skitourenziel. 	  	  </td>
