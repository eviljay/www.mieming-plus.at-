            <tr bgcolor="#EFEFEF">
	                              <td width="50%"  class="cell" align="left" valign="top">
		      	        <b>Landschaftliche Vielfalt:</b> Das Mieminger Plateau (fr&uuml;her: <a href="./miemingerberg.php">Miemingerberg</a>) ist eine zwischen 850 m bis 1000 m hoch gelegene Mittelgebirgsterrasse oberhalb des Tiroler Oberinntals, am S&uuml;dfu&szlig; des <a href="../miemingergebirge/index.php">Mieminger Gebirges</a>. Die Hochfl&auml;che ist etwa 14 km lang und bis zu 4 km breit. Es f&auml;llt bis auf die Neigungen bei <a href="../gemeinden/telfs.php">Telfs</a> und <a href="../gemeinden/moetz.php">M&ouml;tz</a> 200 m steil ins Inntal ab und endet im Westen beim Holzleitensattel, wo die Stra&szlig;e weiter ins Gurgltal und zur Fernpa&szlig;strecke f&uuml;hrt. Das Gebiet ist gekennzeichnet durch eine kleinr&auml;umige Teilung in Wiesen und &Auml;cker, die sich mit L&auml;rchenwiesen und lichten W&auml;ldern abwechseln. Ein Netz von Spazier- und Radwegen und im Winter von Langlaufloipen durchzieht das Plateau und macht es zu einem beliebten Erholungsgebiet. 	  	  </td>
