  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Die Bauern auf dem Mieminger Plateau setzen zunehmend mehr auf die Direktvermarktung. Ob w&uuml;rziger Bergk&auml;se, heimisches Rindfleisch, oder Bauernbrot, wenn man wei&szlig;, woher die Produkte kommen, schmecken sie einem oft umso mehr. Auch, wenn die heimischen Bauern den L&ouml;wenanteil ihrer Lebensmittel &uuml;ber den Gro&szlig;handel absetzen, die Direktvermarktung ist nicht zu untersch&auml;tzen.</p>
