  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Die meisten b&auml;uerlichen Betriebe auf dem Mieminger Plateau sind zwar klein, zeichnen sich aber durch eine hohe Qualit&auml;t der Produkte aus.  Die Produktpalette ist sehr umfangreich und bietet immer wieder regionale Besonderheiten. Beispielsweise Ziegenmilchprodukte. Die Erzeugung von naturbelassenem K&auml;se ist auf enigen H&ouml;fen des Mieminger Plateaus eine Spezialit&auml;t.</p>
