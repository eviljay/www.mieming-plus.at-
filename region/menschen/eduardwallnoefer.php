  <td></td>
  <td colspan="3"><h2><a name="22426398e9096f114"></a>Beliebter fr&uuml;herer Landeshauptmann in Tirol</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Eduard Walln&ouml;fer trat im Jahr 1963 die Nachfolge von Landeshauptmann Tschiggfrei im Bundesland Tirol an. Im &quot;Handbuch zur neueren Geschichte Tirols&quot;, Band II, Seite 515, wird Person und Wirken wie folgt karakterisiert: &quot;Mit dem Bauern, Sch&uuml;tzen und Demokraten Eduard Walln&ouml;fer war f&uuml;r zweieinhalb Jahrzehnte geradezu die Idealbesetzung f&uuml;r die Stelle des Tiroler Landeshauptmannes gefunden worden. Der Abgang Eduard Walln&ouml;fers von der politischen B&uuml;hne schien lange Jahre eine unvorstellbare Ver&auml;nderung in der politischen Landschaft Tirols.&quot;<br><br>Eduard Walln&ouml;fer hat unz&auml;hlige Probleme angeh&ouml;rt, unz&auml;hlige Bittsteller empfangen und so das glaubw&uuml;rdigste Zeugnis eines christlichen Politikers abgelegt. Sein Amt hatte Rang, Macht und Ansehen; seine Person strahlte Bescheidenheit und gl&auml;ubiges Gottvertrauen aus. Aus seinem tiefen Wissen um die Bedeutung der Geschichte hat er sich immer f&uuml;r die Einheit des Landes Tirol eingesetzt. Walln&ouml;fer erkannte auch, da&szlig; die L&auml;nder im Alpenraum enge Bindungen und gemeinsame Wurzeln haben. Um deren Aufgaben und Probleme besser bew&auml;ltigen zu k&ouml;nnen, regte er die Gr&uuml;ndung der <a href="http://www.argealp.org" target="_blank">ARGE ALP</a> an. Vls er 1989 starb, folgte das ganze Land seinem Sarg. Auf seinem Grabkreuz steht ebenso schlicht wie einfach: &quot;Bauer und Landeshauptmann&quot;.</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
