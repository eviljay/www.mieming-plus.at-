			<p class="illu"><strong>Eduard Walln&ouml;fer </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="22426398e90997f16"></a>Vita</h2></td>
  <td></td>
  </tr>
      <tr>
    <td></td>
    <td colspan="3"><p><b>Eduard Walln&ouml;fer</b> (* 11. Dezember 1913 in Schluderns, S&uuml;dtirol; &dagger; 15. M&auml;rz 1989 in Innsbruck) war von 1963 bis 1987 auf Mandat der &Ouml;VP Landeshauptmann von Tirol.<br><br>Eduard Walln&ouml;fer begann seine Karriere in den 30er Jahren als Sekret&auml;r in der Landwirtschaftskammer. 1940 heiratete er die Bauerntochter Luise Thaler und &uuml;bernahm deren elterlichen Hof in Barwies. <br><br>Ab 1945 war Walln&ouml;fer Gemeinderat in Barwies. 1949 zog er in den Tiroler Landtag ein. Ab 1949 war er Landesrat, 1958 wurde er zum Obmann des Tiroler Bauernbundes gew&auml;hlt. Eduard Walln&ouml;fer trat 1963 die Nachfolge des &uuml;berraschend verstorbenen Landeshauptmannes Hans Tschiggfrey an und &uuml;bergab das Amt 1987 krankheitshalber an Alois Partl. Walln&ouml;fer verstarb 1989 an einer Lungenentz&uuml;ndung im Innsbrucker Krankenhaus.<br><br>W&auml;hrend seiner Amtszeit erfolgte ein stetiger wirtschaftlicher Aufschwung Tirols und ein Ausbau der Verkehrswege (z.B. Inntalautobahn). Er regte 1972 die Gr&uuml;ndung der Arbeitsgemeinschaft Alpenl&auml;nder (Arge Alp) an. Als Bauer und Sch&uuml;tze verk&ouml;rperte er f&uuml;r viele Tiroler den Typus des erfolgreichen &quot;Landesvaters&quot;. In der Bev&ouml;lkerung erlangte er breite Zustimmung und hohe Sympathie.</td>
