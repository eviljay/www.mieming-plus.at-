  <td colspan="3"><h2><a name="031f2a9ab70985c36"></a>Tiroler Landtagswahl 2008:</h2></td>
  <td></td>
  </tr>
      <tr>
    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td class="cell" align="left" valign="top">
		      	        Von 2375 Wahlberechtigten gingen in Mieming 65,52 Prozent bei der Landtagswahl am Sonntag, dem 8. Juni 2008 zur Wahlurne. Landesweit lag die Wahlbeteiligung heuer bei 62 Prozent. Gew&auml;hlt werden konnte neben dem Gemeindehaus in Obermieming in den Volksschulen Barwies und Untermieming. Gemeindewahlleiter Dr. Franz Dengg ver&ouml;ffentlichte die ersten lokalen Wahlerebnisse nach 17 Uhr. Von einem gro&szlig;en Medienaufgebot wurden die Stimmabgaben von Landeshauptmann DDr. Herwig van Staa und seiner Frau Luise in seiner Heimatgemeinde begleitet. &nbsp;</font>
	  	  </td>
    </tr>
