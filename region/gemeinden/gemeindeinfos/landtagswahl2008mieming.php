  <td colspan="3"><h2><a name="031f2a9ab709b7639"></a>Gute Wahlbeteiligung in Mieming</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Die Wahlbeteiligung in Mieming war mit 65,52 Prozent um 1,86 Prozent h&ouml;her als auf Landesebene (2003 rund 64%). In Mieming kam die &Ouml;VP auf &uuml;ber 48 Prozent, auf Landesebene auf 40,5 Prozent (- 9,45%). FP&Ouml; 10,73% (+3,07), SP&Ouml; 7,9% (-14,43), Gr&uuml;ne 12,24% (-4,46), die Liste Dinkhauser schaffte 18,63% auf Anhieb. F&uuml;r &Ouml;VP und SP&Ouml; waren das die schlechtesten Ergebnisse seit 1945. Landeshauptmann DDr. Herwig van Staa sagte am Morgen bei der Stimmabgabe er rechne mit keiner Absoluten mehr.</td>
