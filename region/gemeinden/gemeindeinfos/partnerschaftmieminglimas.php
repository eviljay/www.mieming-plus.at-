  <td colspan="3"><h2><a name="2242639944110c102"></a>Partnerschaftscharta</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Die Musikkapellen beider Kommunen musizierten gemeinsam und spielten u.a. die Hymnen Frankreichs, &Ouml;sterreichs und Tirols. Der Abend wurde in Anwesenheit von LA Mag. Jakob Wolf, dem franz&ouml;sischen Honorarkonsul in Innsbruck, RA Dr. Ivo Greiter, der B&uuml;rgermeister Mag. Dr. Siegfried Gapp (Mieming) und Michel Thien (Limas) und den Ratsmitgliedern beider Kommunen, wurde bei einem Tiroler Abend eine Partnerschaftscharta vereinbart, die nochmals die gemeinsamen Ziele der Partnerschaft beschreibt.</p>
</td>
