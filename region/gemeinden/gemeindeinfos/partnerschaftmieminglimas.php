  <td></td>
  <td colspan="3"><h2><a name="224263993c00f0a34"></a>Gro&szlig;es Jubil&auml;umsfest zum Pfingstwochenende</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Die Gemeinden Limas im franz&ouml;sischen Beaujolais und Mieming in Tirol pflegen seit 1997 eine dynamische Gemeindepartnerschaft. Zum 10j&auml;hrigen Jubil&auml;um besuchte eine gro&szlig;e Delegation aus Limas mit B&uuml;rgermeister Michel Thien an der Spitze Mieming. Die Jugendmusikkapelle von Limas, ein U-17 Fu&szlig;ballteam - insgesamt &uuml;ber 170 Limassiens - waren &uuml;ber Pfingsten G&auml;ste auf dem Mieminger Plateau.</p>
</td>
    <td><img src="../../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../../../images/224263993c01cbe47.jpg" target="_blank"><img src="../../../images/224263993c01cbe47_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>Treffen zum F&uuml;nfj&auml;hrigen </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="224263993c036e655"></a>Franz&ouml;sischer Fr&uuml;hjahrsmarkt</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Zum Auftakt der Jubil&auml;umsbegegnungen wurde am Pfingstsamstag, dem 27. Mai 2007, von Limas auf dem Gemeindeplatz ein Fr&uuml;hjahrsmarkt veranstaltet. Bei strahlendem Sonnenschein und Temperaturen &uuml;ber 21 Grad kamen zahlreiche Besucher auf den Gemeindeplatz. Angeboten wurden u.a. Wein (Beaujolais und Blanc de blanc), K&auml;se, W&uuml;rste, verschiedenste franz&ouml;sische Spezialit&auml;ten, Gem&uuml;se und Obst. Die Besucher konnten Cr&ecirc;pes, Tartes und andere K&ouml;stlichkeiten zu einem Glas Beaujolais oder Champagner verkosten. Mieming revanchiert sich am ersten Wochenende in der Vorweihnachtszeit mit einem Adventmarkt.</p>
</td>
    <td><img src="../../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../../../images/224263993d110bd06.jpg" target="_blank"><img src="../../../images/224263993d110bd06_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>Fr&uuml;hjahrsmarkt </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="2242639944110aa01"></a>Partnerschaft mit wichtigen Zielen</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Die offiziellen Jubil&auml;umsfeierlichkeiten konzentrierten sich auf den Pfingstsonntag. Im Sitzungssaal der Gemeinde Mieming wurde am Vormittag ein gemeinsamer Gemeinderat zwischen Mieming und Limas vorgeschlagen. Ziel der Kooperation beider Kommunen ist eine aktive Partnerschaft in den Bereichen humanit&auml;re Hilfe und des Naturschutzes zu entwickeln. Die Kommunen tauschten Geschenke aus. In einem Fu&szlig;ballfreundschaftsspiel begegneten sich die Teams aus Limas und der SVG Mieminger Plateau, weiters gab es noch eine Begegnung der Sch&uuml;tzen.</p>
</td>
    <td><img src="../../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../../../images/22426399441128305.jpg" target="_blank"><img src="../../../images/22426399441128305_150.jpg" width="150" height="112" alt="" border="0"><br></a>
