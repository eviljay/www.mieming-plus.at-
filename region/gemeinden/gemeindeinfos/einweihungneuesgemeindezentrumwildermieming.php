  <td colspan="3"><h2><a name="031f2a99f50bf9976"></a>Einweihung war ein gro&szlig;es Fest</h2></td>
  <td></td>
  </tr>
      <tr>
    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Wildermieming hat ein neues Haus der Begegnung. Die Einweihung des schmucken Gemeindezentrums am Sonntag, dem 25. November 2007 war ein gro&szlig;es Fest, das nach dem Empfang zahlreicher G&auml;ste und dem Einzug der Formationen am Vormittag mit einer von Dekan Josef Tiefenthaler zelebrierten Messfeier begann. Das K&uuml;chenteam um Charly Haselwanter kochte 420 Grie&szlig;nockerln und prutzelte ebenso viele Schnitzel, die Frauen aus Wildermieming steuerten dazu 120 Kilogramm hausgemachten Erd&auml;pfelsalat dazu. F&uuml;r die Unterhaltung sorgten die Musikkapellen aus Wildermieming und Burgstall mit Unterst&uuml;tzung der Musikgruppe Tschirgenka aus dem Pitztal.	  	  </td>
