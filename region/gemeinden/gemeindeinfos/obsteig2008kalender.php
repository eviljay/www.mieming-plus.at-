            <tr bgcolor="#EFEFEF">
	                              <td class="cell" align="left" valign="top">
		      	        Als Ergebnis eines erstmaligen Projektes der &Ouml;ffentlichen Bibliothek wurde am Sonntag, dem 9. Dezember 2007, der Kalender &quot;Obsteig 2008&quot; in der Volksschule &ouml;ffentlich pr&auml;sentiert. Der Kalender erscheint in einer limitierten Auflage von 300 St&uuml;ck im X-TREM-Verlag, Obsteig und wurde graphisch von Herbert Krug gestaltet. Der Obsteig-Kalender ist das Ergebnis eines Fotowettbewerbes, zu dem 43 TeilnehmerInnen 88 Bilder einreichten. &nbsp;</font>
