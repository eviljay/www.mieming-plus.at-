  <td></td>
  <td colspan="3"><h2><a name="8627589d490b4c718"></a>Gemeinden droht die Armutsfalle</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Den Gemeinden am Mieminger Plateau droht die Armutsfalle. Mit Blick in die mittelfristige Zukunft d&uuml;rfte sich nach der mit gesch&auml;tzten rund 140% am st&auml;rksten verschuldeten Gemeinde Obsteig auch in Mieming und Wildermieming die wirtschaftlichen Situationen verschlechtern. Dr. Franz Dengg, der neue B&uuml;rgermeister in Mieming rechnet bis zum Ablauf dieses Etatjahres mit einem Verschuldungsgrad von bis zu 100 %, Klaus Stocker geht f&uuml;r seine Gemeinde Wildermieming von 63% aus.</p>
