  <td colspan="3"><h2><a name="8627589d490ab1813"></a>1. Treffen der neuen Plateau-B&uuml;rgermeister</h2></td>
  <td></td>
  </tr>
      <tr>
    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        In einem ersten Treffen der neuen B&uuml;rgermeister der drei Gemeinden auf dem Mieminger Plateau wurde auf mehreren Ebenen eine enge Zusammenarbeit vereinbart. Zwei Tage vor ihrer gemeinsamen Angelobung in der Innsbrucker Hofburg trafen sich auf Einladung von Mieming-Plus am Samstag, dem 28. M&auml;rz 2010 im Hotel Stern in Obsteig die am 14. M&auml;rz gew&auml;hlten B&uuml;rgermeister Hermann F&ouml;ger (Obsteig), Dr. Franz Dengg (Mieming) und Klaus Stocker (Wildermieming). Drei Gemeinden in zwei politischen Bezirken verabreden interdisziplin&auml;re Kooperationen in allen relevanten Bereichen. 	  	  </td>
