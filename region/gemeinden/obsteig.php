    <td colspan="3"><p>Obsteig ist die westlichste Gemeinde des <a href="../miemingerplateau/index.php">Mieminger Plateaus</a>, oberhalb des <a href="../../tourismus/ferienregion-tirolmitte.php">Oberinntals</a>, am Fu&szlig; des Mieminger Gebirges. Die Gemeinde besteht aus verstreuten D&ouml;rfern und Weilern am Plateau. Beim 1119 m hoch gelegene <i>Holzleitensattel</i> im Westen f&auml;llt das Plateau steil in das Gurgltal ab. Der Hauptort Obsteig liegt entlang einer ausgebauten Stra&szlig;e.<br><br>Das Gebiet um Obsteig ist ein beliebtes Wander- und Ausflugsgebiet; au&szlig;erdem verf&uuml;gt Obsteig &uuml;ber ein Skigebiet und im Winter sind zahlreiche Loipen angelegt.<br><br>Das Schloss Klamm zwischen Obsteig und Fronhausen, unterhalb der Stra&szlig;e gelegen, bewachte einst die Transportroute, die von <a href="./moetz.php">M&ouml;tz</a> herauff&uuml;hrte, und diente in der Fernsehserie <a href="../miemingerplateau/miemingerplateau.php">Bergdoktor</a> als &quot;Schloss Sonnenstein&quot;. Der 25 m hohe Wasserfall des Klammbachs ist von der Burg aus in wenigen Minuten zu erreichen.<br><br><b>Ortsteile: </b>Wald, Thal, Finsterfiecht, Roller, Langgarten, Holzleiten, Weisland, Aschland, Arzkasten, Mooswald, Oberstrass, Unterstrass, Gschwent (Gschwentmadele), Schloss Klamm. Der Nordpolfahrer Johann Haller (1844-1906) war k.k. F&ouml;rster in Obsteig und ist dort begraben.</p>
