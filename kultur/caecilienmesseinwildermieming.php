    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Die Musikkapelle Wildermieming, unter der Leitung von Kapellmeister Alois Kranebitter, lud am Tag der Hl. C&auml;cilia, Samstag, 22. November 2008 im Gemeindezentrum Wildermieming zur C&auml;cilienfeier ein. Die feierliche Messe zelebrierten Pfarrer Pater Andreas Rolli und Diakon Christian Tschaikner. &Uuml;ber 100 Besucher waren vom anspruchsvollen Konzert der MK Wildermieming begeistert. H&ouml;hepunkte waren die Solistendarbietungen von Daniela Weissbacher (Gesang) aus Telfs, Helen Scheibler (Violine) aus Mieming und Gerhard Czermak (Horn) aus Wildermieming mit musikalischer Unterst&uuml;tzung von Keyboarder Elmar Gapp und den Mariens&auml;ngerinnen aus Wildermieming.	  	  </td>
