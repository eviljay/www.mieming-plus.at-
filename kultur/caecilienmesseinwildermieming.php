  <td colspan="3"><h2><a name="031f2a9b6013b3769"></a>C&auml;cilienmesse wurde 1855 in Paris uraufgef&uuml;hrt</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Das Konzertprogramm der Musikkapelle Wildermieming mit den o.a. Solisten war sehr vielseitig und ein Genu&szlig; f&uuml;r die Zuh&ouml;rer. Das dargebotene Repertoire umfasste z.B. das Hornkonzert von Mozart, die Serenata Sentimentale &ndash; eine Romanze f&uuml;r Violine &ndash; Cavaleriana Russitacana, Titel wie &bdquo;The Rose&ldquo; und &bdquo;The Power of Love&ldquo; und abschlie&szlig;end das &bdquo;Amen&ldquo; von Pavel Stanek. Die klassische C&auml;cilienmesse wurde am 22. November 1855, dem Jahrestag der Hl. C&auml;cilia, in der Kirche St. Eustache in Paris uraufgef&uuml;hrt.</p>
