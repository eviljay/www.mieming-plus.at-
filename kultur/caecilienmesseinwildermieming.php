  <td colspan="3"><h2><a name="031f2a9b60136df63"></a>C&auml;cilienmesse zum ersten Mal im Gemeindezentrum</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Die C&auml;cilienmesse wurde zum ersten Mal im Wildermieminger Gemeindezentrum veranstaltet. In den Vorjahren wurde sie in der St. Nikolaus-Pfarrkirche in Wildermieming gefeiert. Unter den G&auml;sten waren u.a. B&uuml;rgermeister Josef Stoll, Altb&uuml;rgermeister Robert Marthe und Dr. Werner Plunger (fr&uuml;herer Direktor der Wirtschaftskammer Tirol). Als Dankesch&ouml;n f&uuml;r die Leistungen der Musikanten lud Obmann Herbert Oberdanner die Musikkapelle und alle Solisten zum abschlie&szlig;enden Essen ein.</p>
</td>
