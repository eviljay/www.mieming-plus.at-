  <td colspan="3"><h2><a name="031f2a9ae00e55b4e"></a>Kapelle zu St. Afra</h2></td>
  <td></td>
  </tr>
      <tr>
    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td class="cell" align="left" valign="top">
		      	        Im Weiler Affenhausen in Wildermieming steht als Hofkapelle eines der &auml;ltesten H&ouml;fe der Gemeinde eine barocke Kapelle zu St. Afra - kurz: die Afrakapelle. Sie wurde zum 1. Mal in den 60er Jahren renoviert und zuletzt 1998. Die vier Glasfenster der Afrakapelle stammen von der Wildermieminger Malerin Chryseldis Hofer-Mitterer. Die Festmesse zur Einweihung zelebrierte der damalige Abt von Stift Stams (von 1985 bis 2003), Mag. Josef Maria K&ouml;ll, verstorben im M&auml;rz 2008 in Meran. Die Heilige Afra, die vor &uuml;ber 1700 Jahren einen legend&auml;rer M&auml;rtyrertod starb, wird von den Affenhausenern sehr verehrt.&nbsp;</font>
	  	  </td>
    </tr>
