  <td colspan="3"><h2><a name="22426398e40ef7054"></a>Glasfenster der K&uuml;nstlerin Chryseldis</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Die Afrakapelle Kapelle in Affenhausen tr&auml;gt den Namen der fr&uuml;hchristlichen Heiligen, die der Chronik zufolge auch Namenspatronin des Dorfes Affenhausen (&bdquo;Affrahausen&ldquo;) sein soll. Affenhausen liegt an der alten Handelsstra&szlig;e von Hall nach Augsburg. Die Salzfuhrleute sollen an dieser Stelle eine Kapelle erbaut haben, die der heiligen <i>Afra</i> geweiht war, von der man sich himmlischen Beistand erhoffte. Afra wurde 1064 heiliggesprochen.</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
