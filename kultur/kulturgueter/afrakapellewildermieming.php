    <td valign="top"><p>G&uuml;nther Stecher malte vor zwanzig Jahren eine Weihnachtskarte der Afrakapelle. Den gerahmten Linolschnitt zeigt uns sein Vater, Walter Stecher. Das Original der k&uuml;nstlerischen Arbeit ist leider nicht mehr vorhanden. Somit ist das kleine Bildnis der Kapelle St. Afra aus Affenhausen eine wertvolle Rarit&auml;t. Vielleicht gibt es ja zu einem der kommenden Weihnachtsfeier ein neues Bild der sehenswerten Kapelle?</td>
