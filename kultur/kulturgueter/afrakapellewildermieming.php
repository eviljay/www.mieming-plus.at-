  <td colspan="3"><h2><a name="031f2a9ae00e81f53"></a>1997/98 liebevoll renoviert</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>1960 und zuletzt 1997/98 wurde die Afrakapelle von den Affenhausenern liebevoll saniert. Stimmungsvoller Blickfang f&uuml;r den Andachtsuchenden sind die vier Glasfenster der K&uuml;nstlerin Chryseldis Hofer-Mitterer, die in Wildermieming lebt und arbeitet. Die Fenster wurden von der Familie Stecher in Affenhausen f&uuml;r die Kapelle iniiert. Die k&uuml;nstlerischen Arbeiten von Chryseldis waren eine Spende der Malerin, weitere Fenster spendeten die Familien Brenner (Hotel Traube), Stecher (Steindruckerei Stecher &amp; Stecher) und ein Blumengesch&auml;ft aus Telfs.</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
