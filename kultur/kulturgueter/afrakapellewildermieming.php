  <td colspan="3"><h2><a name="22426398e40ea5d4d"></a>Afrafest zu Ehren der Heiligen</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Einmal im Jahr, im August, wird in Wildermieming-Affenhausen das Afrafest gefeiert. Die Initiative geht auf die Familie G&uuml;nther und Annaliese Stecher aus Affenhausen zur&uuml;ck. Vor zwei Jahren widmeten sie ihr 20j&auml;hriges Firmenjubil&auml;um der Heiligen Afra. Das Fest wird mit einem Gottesdienst vor der Afrakapelle er&ouml;ffnet. Zum Afrafest werden von Tiroler K&uuml;nstlern hochwertige Bilder auf Speckstein gemalt, die in der Steindruckerei Stecher &amp; Stecher zu Lithografien gedruckt werden.</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../../images/031f2a9ae00ea8c56.jpg" target="_blank"><img src="../../images/031f2a9ae00ea8c56_150.jpg" width="150" height="112" alt="" border="0"><br></a>
