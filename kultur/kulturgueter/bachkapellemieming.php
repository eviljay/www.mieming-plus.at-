  <td></td>
  <td colspan="3"><h2><a name="031f2a9a7508b830b"></a>Renovierung abgeschlossen</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Nepomuk wurde 1393 in der Moldau ertr&auml;nkt. Man kennt ihn mit Talar und Chorrock, hermelinbesetztem Schultermantel bzw. - kragen, Birett und einem Kruzifix in der Hand. Der Johannes von Nepomuk Kapellenbildstock, wurde im Auftrag der Agrargemeinschaft Obermieming restauriert. Die Renovierung der Bachkapelle in Obermieming konnte im Herbst 2007 abgeschlossen werden. Die Kapelle geh&ouml;rt zu den sehenswerten Kulturg&uuml;tern der Gemeinde Mieming und tr&auml;gt zur Dorfbildversch&ouml;nerung bei.</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
