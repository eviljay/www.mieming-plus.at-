  <td colspan="3"><h2><a name="031f2a9a890c2db02"></a>Feierliche Einweihung am 25. Mai 2008</h2></td>
  <td></td>
  </tr>
      <tr>
    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td class="cell" align="left" valign="top">
		      	        Weit &uuml;ber 200 Mieminger kamen zur Einweihung der Urbankapelle am Sonntag, dem 25. Mai 2008. Die Kapelle wurde vor kurzem an ihren neuen Standort, unweit des Feuerwehrhauses in Mieming verlegt und restauriert. Das einzige Kulturgut in Mieming mit drei Namen: Urbankapelle, Bach- und Nepomukkapelle. Die Einweihung der Kapelle am Lehnbach in Obermieming wurde an dem Tag gefeiert, an dem auch der sogenannte &bdquo;Bachsegen&ldquo; erteilt wird. Dieser Tag f&auml;llt auch mit dem Ged&auml;chtnis an den Heiligen Urban, dem Winzerheiligen, zusammen. Die Kapelle geh&ouml;rt zu den sehenswerten Kulturg&uuml;tern der Gemeinde Mieming. &nbsp;</font>
	  	  </td>
    </tr>
