  <td></td>
  <td colspan="3"><h2><a name="031f2a9a7508a1408"></a>Einweihung der Bachkapelle</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Der Bittgang mit anschlie&szlig;endem Bachsegen hat Tradition und geht auf die Zeit zur&uuml;ck, als der Lehnbach in Mieming noch nicht verbaut war und immer wieder gr&ouml;&szlig;ere Sch&auml;den an den Fluren anrichtete. Weil dieser Bittgang auf den 25. Mai, den Ged&auml;chtnistag des Heiligen Urban fiel, wurde sozusagen stillschweigend aus dem Johannes von Nepomuk ein Urban.</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../../images/031f2a9a7508a0106.jpg" target="_blank"><img src="../../images/031f2a9a7508a0106_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>Nepomuk Bildstock </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="031f2a9aa9109dc01"></a>Eine Mieminger Spezialit&auml;t</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Es kann aber auch sein, so die Mieminger, dass vielleicht deshalb aus dem Johannes von Nepomuk ein Urban wurde, um ihn von Johannes bei der unteren Lehnbachbr&uuml;cke zu unterscheiden. Ein Mieminger Spezialti&auml;t. Dabei ist nicht auszuschlie&szlig;en, dass der Heilige eine mehr oder wieder st&auml;rkerere Beziehung zum Winzerheiligen, denn zum Bachheiligen haben k&ouml;nnte. Die Statue der Kapelle zeigt jedenfalls eindeutig den Heiligen Johannes von Nepomuk, den Br&uuml;cken- und Bachheiligen.</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
