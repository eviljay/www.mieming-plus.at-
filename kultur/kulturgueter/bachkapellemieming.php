  <td></td>
  <td colspan="3"><h2><a name="031f2a9aa910d5209"></a>Festl, die man in Mieming mag</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Zur Einweihung kamen am fr&uuml;hen Sonntagmorgen alle Mieminger, die mit ihrer Gemeinde und deren Entwicklung verbunden sind. Die Musikkapelle Mieming unter ihrem Leiter Christian Holzeis spielte auf. Unter den G&auml;sten waren u.a. B&uuml;rgermeister Dr. Siegfried Gapp, Vize-B&uuml;rgermeister Reinhard Gastl und Altb&uuml;rgermeister bzw. Ehrenmitglied der Musikkapelle, Karl Spielmann. Die Freiwillige Feuerwehr Mieming, unter ihrem Kommandanten Thomas Perkhofer richtete einen z&uuml;nftigen Fr&uuml;hschoppen aus. Festl, die man in Mieming mag.</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
