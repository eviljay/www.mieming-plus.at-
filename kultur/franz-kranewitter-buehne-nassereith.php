  <td colspan="3"><h2><a name="8627589c340c5461b"></a>Drama um Andreas Hofer im Theatersommer 2009</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Regisseurin Barbara Ferrari setzt auf neue Gesichter und hat das Werk beschaulich und kraftvoll, ohne Schn&ouml;rkel und Sentimentalit&auml;t inszeniert. Die Rolle des Andre Hofer spielt Ren&eacute; Bowalle, der &uuml;berzeugend spielt und Hofers Charakter gut darstellt. Seine Frau Anna spielt Martina Wander. Franz Kranewitter hat mit dem Werk &bdquo;Andre Hofer&ldquo; eine sehr pers&ouml;nliche Sicht der Person Hofer gezeichnet, in Tirol wurde es deshalb zun&auml;chst mit einem Auff&uuml;hrungsverbot belegt.</p>
</td>
