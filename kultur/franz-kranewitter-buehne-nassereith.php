  <td colspan="3"><h2><a name="8627589c340c9be1d"></a>Andre Hofer - Schauspiel in vier Aufz&uuml;gen</h2></td>
  <td></td>
  </tr>
      <tr>
    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Theatersommer 2009 in Nassereith - die Franz-Kranewitter-Freichlichtb&uuml;hne spielt im Andreas-Hofer-Gedenkjahr bis 23. August 2009 das Schauspiel &quot;Andre Hofer&quot;. Premiere des St&uuml;ckes war am Freitag, dem 19. Juni 2009. Regie: Barbara Ferrari. &quot;Der Autor des St&uuml;ckes, Franz Kranewitter, folgt in den Grundz&uuml;gen den historischen Quellen und verzichtet auf jede Szene, die dem konservativen Hof-Mythos dienlich sein k&ouml;nnte&quot;, lassen die Nassereither Theaterleute wissen. Kranewitter zeigt in seinem Schauspiel ausschlie&szlig;lich die Misserfolge und das Ende um Andreas Hofer. Von seinem Aufstieg, von seinen Triumphen ist keine Rede mehr. Die Personen der Handlungen sehen sich auf Schaupl&auml;tze verwiesen, wo sie nicht agieren, sondern nur diskutieren k&ouml;nnen - das Geschehen spielt in Wirtsstuben bzw. bei der Pfandler Almh&uuml;tte. 	  	  </td>
    </tr>
</table><br><br>
</td>
