  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Das St&uuml;ck wurde 1901 im bayerischen Meiningen uraufgef&uuml;hrt. Die 1903 angesetzte Erstauff&uuml;hrung in Innsbruck aber erregte einen Skandal: Seit Dezennien - so erinnerten sich damals die &quot;Innsbrucker Nachrichten&quot;-hatte man im Tiroler Theaterbetrieb eine &auml;hnliche Auseinandersetzung nicht erlebt. Die Franz Kranewitter-B&uuml;hne spielt das Schauspiel als Freiluftauff&uuml;hrung am Eingang des Gafleintales. F&uuml;r das Plakat wurde eine Lithographie von Daniela Senn verwendet.</p>
