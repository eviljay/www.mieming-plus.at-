  <td colspan="3"><h2><a name="031f2a9ae90895d08"></a>Seit f&uuml;nf Jahren Regionsblasmusikfest</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>&bdquo;Das Radl beginnt sich wieder in Wildermieming zu drehen&ldquo;, sagte Tourismuschef Hubert Trenkwalder. Seit f&uuml;nf Jahren gebe es das Regionsblasmusikfest. In jedem Jahr ist eine andere Plateaugemeinde Gastgeberin. Im vergangenen Jahr war das gro&szlig;e Musikfest in Obsteig. &bdquo;Anfangs sollte das Blasmusikfest ein Angebot an unsere G&auml;ste sein&ldquo;, so Trenkwalder in seiner Begr&uuml;&szlig;ung, &bdquo;heute ist es ein Fest f&uuml;r unsere G&auml;ste und die einheimische Bev&ouml;lkerung&ldquo;. Gemeinsam mit dem Wildermieminger B&uuml;rgermeister Josef Stoll und dem fr&uuml;heren TVB-Obmann Josef &bdquo;Peppi&ldquo; J&auml;ger (Seniorchef Landhotel J&auml;ger) ehrte Hubert Trenkwalder zum Auftakt treue G&auml;ste, die seit Jahren ihre Ferien auf dem Mieminger Plateau verbringen.</p>
</td>
