  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Herbert Oberdanner, Obmann der Musikkapelle Wildermieming stellte in seiner Er&ouml;ffnungsrede die teilnehmenden Musikkapellen mit ihren Kapellmeistern und Repr&auml;sentanten vor. G&auml;ste waren heuer die MK Arzl aus dem Pitztal mit ihrem Kapellmeister Anton Ha&szlig;lwanter. Die Musikkapelle Obsteig spielte unter Kapellmeister Erich Neurauter zum Auftakt des Blasmusikfestes und begeisterte die rund 300 Zuh&ouml;rerinnen und Zuh&ouml;rer mit einem vielseitigen Musikrepertoire. Da wurde von der Polka &uuml;ber &bdquo;Dem Land Tirol die Treue&ldquo; bis zu einem Bert Kaempfert-Medley alles geboten, was dem Publikum gefiel. Ein perfekter Auftakt zu einem stimmungsvollen Fest.</p>
