  <td colspan="3"><h2><a name="031f2a9ae90859d07"></a>Musikfest f&uuml;r das Mieminger Plateau</h2></td>
  <td></td>
  </tr>
      <tr>
    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Das Wochenende in Wildermieming war ein Musikfest der guten Laune f&uuml;r das Mieminger Plateau. Das Regionsblasmusikfest startete am Samstag mit einem musikalisch hochwertigen Sommernachtskonzert und hatte am Sonntag, dem 27. Juli 2008 seinen H&ouml;hepunkt auf dem Gemeindeplatz. Nach einem Sternmarsch ab 9 Uhr zogen die Musikkapellen aus Obsteig, Arzl und Wildermieming zum Gemeindehaus. Die traditionelle Feldmesse zelebrierte heuer Pater Ulrich aus Telfs, musikalisch begleitet von der Musikkapelle Wildermieming. 	  	  </td>
    </tr>
