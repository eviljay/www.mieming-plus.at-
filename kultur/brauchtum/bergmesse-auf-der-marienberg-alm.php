  <td colspan="3"><h2><a name="8627589c67101b907"></a>Marienbergalpe mit 750 Hektar Gesamtfl&auml;che</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Benedikt van Staa, der mit Jahresbeginn Hansj&ouml;rg Wett als Obmann der Marienbergalpe abl&ouml;ste, begr&uuml;&szlig;te vor der Bergmesse alle Gl&auml;ubigen und stellte die Almgemeinschaft vor. Bewirtschaftet werden aktuell ca. 750 ha Gesamtfl&auml;che und der Bundesforst. Die Alpinteressentschaft Marienberg hat die Weiderechte. Von Mitte Juni bis Mitte September betreut Alp-Hirt Herbert Schuchter das Weidevieh. Gemeinsam mit seiner Frau Anne-Marie und der Kellnerin Martina betreut H&uuml;ttenwirt Herbert Schuchter die Marienberg Alm.</p>
