          <tr>
    <td></td>
    <td valign="top"><p>Nach der bewegenden Bergmesse bei der Marienberg Alm spielten vor herrlicher Kulisse und gro&szlig;em Publikum die &quot;3 Grie&szlig;kogler&quot; aus Obsteig. H&uuml;ttenwirt Herbert Schuchter mit Familie und zahlreichen Helferinnen und Helfer servierten ihren G&auml;sten Gulasch mit Kn&ouml;del, W&uuml;rstl oder Kasnockerln. In der H&uuml;tte sangen und musizierten die Mieminger S&auml;nger und die Jager. Tiroler Volksliedgut in filmreifer Kulisse. Viele G&auml;ste beherrschten das popul&auml;re Repertoire und sangen mit. Die Marienberg Alm war &uuml;brigens h&auml;ufig Drehort der TV-Serie &quot;Der Bergdoktor&quot; mit Harald Krassnitzer in der Hauptrolle.</p>
