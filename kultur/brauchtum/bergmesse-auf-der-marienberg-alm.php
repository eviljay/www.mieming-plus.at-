  <td colspan="3"><h2><a name="8627589c670f3ad01"></a>Marienbergmesse mit gro&szlig;em Zuspruch</h2></td>
  <td></td>
  </tr>
      <tr>
    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Zum Gedenken an Hans Haid, Hans Karg, Max Pirpamer und alle verstorbenen Hirten und Almbauern wurde von der Alpinteressentschaft Marienberg und der Familie des H&uuml;ttenwirts Herbert Schuchter am Sonntag, dem 9. August 2009 eine Bergmesse auf der Marienberg Alm gefeiert. Die Messe zelebrierte Abt Mag. German Erd (Stift Stams). Zur Bergmesse kamen rund 200 Gl&auml;ubige zur Marienberg Alm (1623 Meter). F&uuml;r die musikalische Begleitung der Bergmesse sorgten die Mieminger S&auml;nger (Ltg. Siegfried K&ouml;ll), die Obsteiger Weisenbl&auml;ser und die Jagdhornbl&auml;sergruppe Mieminger Plateau (Hornmeister Karl Frauenhoffer). Im Anschluss an die Bergmesse spielten die &quot;3 Grie&szlig;kogler&quot; zur Unterhaltung und in der Marienberg Alm unterhielten die Mieminger S&auml;nger die G&auml;ste der Alm-Bar. 	  	  </td>
