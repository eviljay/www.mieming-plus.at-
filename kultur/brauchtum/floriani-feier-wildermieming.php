            <tr bgcolor="#EFEFEF">
	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Wildermieming feierte am Sonntag, dem 2. Mai 2010 &bdquo;Floriani&ldquo;. St. Florian ist u.a. der Schutzpatron aller Feuerwehrleute. Er wird - au&szlig;er gegen Feuer- und Brandgefahren - auch angerufen gegen Unfruchtbarkeit der Felder und unvorhersehbarer Naturkatastrophen. Nach dem Einzug der Musikkapelle und der Freiwilligen Feuerwehr in das Gemeindehaus Wildermieming, zelebrierte Pfarrer und Alt-Dekan Albert Markt die Festmesse vor rund 200 Gl&auml;ubigen. Zur musikalischen Umrahmung der Musikkapelle Wildermieming, unter der Leitung von Kapellmeister Alois Kranebitter, gab es eine gesangliche K&ouml;stlichkeit. Solist Alfred Fritz sang die Arie &bdquo;Nessun dorma&ldquo; aus der Puccini-Oper &bdquo;Turandot&ldquo;. Unter den G&auml;sten waren B&uuml;rgermeister Klaus Stocker, Vize-B&uuml;rgermeister Josef Steinhauser, zahlreiche Gemeinder&auml;te, Ehrenmitglieder und Goteln.	  	  </td>
