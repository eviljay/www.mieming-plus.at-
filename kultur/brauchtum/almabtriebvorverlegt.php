  <td colspan="3"><h2><a name="0352ff99a60ccede5"></a>Mieming: Almabtrieb von der Hochfeldernalm</h2></td>
  <td></td>
  </tr>
      <tr>
    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Der Einladung zum inzwischen 6. Almabtrieb der Agrargemeinschaft Hochfeldernalm folgten in Mieming am Samstag, dem 20. September 2008 mehrere Hundert Besucherinnen und Besucher. Ein Herbstauftakt nach Ma&szlig;: &Uuml;ber 8 Stunden waren die Hirten mit &uuml;ber 100 K&uuml;hen auf ihrem Weg von der 1754 hohen Hochfeldernalm im Gaistal, bis sie gegen 15 Uhr in Obermieming beim Gasthof zur Post eintrafen. Nach ihrem Eintreffen am &bdquo;K&auml;lberhag Riegl&ldquo;, beim Stadtl von Anton Post, fand ein z&uuml;nftiges Almfest&rsquo;l statt, zu dem &bdquo;Die Schupfenviertler&ldquo; aus Obsteig aufspielten. 	  	  </td>
    </tr>
</table><br><br>
</td>
    <td></td>
    </tr>
    
