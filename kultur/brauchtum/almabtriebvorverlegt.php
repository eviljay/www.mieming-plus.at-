  <td></td>
  <td colspan="3"><h2><a name="0352ff99a60c8a6e1"></a>Weit &uuml;ber 200 K&uuml;he wurden &quot;gealpt&quot;</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Der Almabtrieb ist der Abschluss der Almweidezeit und Beginn der Winterstallung. Rund 240 K&uuml;he der Mieminger Bauern wurden heuer &quot;gealpt&quot;, d.h. sie verbrachten den Sommer auf der Hochfeldernalm. Etwa 100 Tiere wurden auf dem Fu&szlig;weg unter der Leitung von Alpobmann Klaus Scharmer (Obermieming), dem Alphirt Norbert Kluckner, seiner Frau Pia und vielen flei&szlig;igen Helfern von den Bergweiden (Almen) ins Tal getrieben, wo es nun in den Stallungen der Bauernh&ouml;fe &uuml;berwintert.</p>
</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
