  <td colspan="3"><h2><a name="0352ff99a30d53530"></a>Herden werden kunstvoll geschm&uuml;ckt</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Ist der Almsommer f&uuml;r Mensch und Tier ohne t&ouml;dliche Unf&auml;lle verlaufen, werden die Herden f&uuml;r den Abtrieb kunstvoll geschm&uuml;ckt. F&uuml;r den Kopfschmuck der Tiere wird traditionell Latschenkiefer verwendet, auch Seidenblumen und echte Blumen werden von den B&auml;uerinnen bei der Herstellung der kunstvollen &quot;Proster&quot; verarbeitet. Die liebevoll gestalteten Gebinde enthalten ein Kreuz als Vergelt`s Gott f&uuml;r die unfallfreie Almsaison, die Spiegel und Glocken sind heidnischen Ursprungs und sollen b&ouml;se Geister abwehren.</p>
