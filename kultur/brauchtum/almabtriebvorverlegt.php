  <td colspan="3"><h2><a name="031f2a9b210f212d0"></a>Ende des Almsomers</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Vom 15. Juni bis zum 20. September betreuten die Alphirten Norbert Kluckner und seine Frau Pia 230 K&uuml;he auf der Hochfeldernalm. Unter dem Jungvieh waren zehn Melkk&uuml;he. &quot;Weit &uuml;ber die H&auml;lfte des Viehs wurden in den Tagen davor auf das Mieminger Plateau verbracht&quot;, so Almobmann Klaus Scharmer. Die Hochfeldernalm wird von Obermieminger Bauern bewirtschaftet. Der Almabtrieb symbolisiert das Ende des Almsommers.</p>
</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
