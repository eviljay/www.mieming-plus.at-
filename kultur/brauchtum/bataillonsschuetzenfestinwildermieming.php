  <td colspan="3"><h2><a name="031f2a9b06082bb03"></a>Besinnung auf die Wurzeln</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Zur Feldmesse, die der Sch&uuml;tzenkurat Msgr. Dekan Franz Saurer aus Telfs zelebrierte, spielten die Musikkapellen Mieming und Wildermieming gemeinsam, unter der Leitung von Kapellmeister Christian Holzeis. Die Globalisierung mache die Welt anscheinend ein St&uuml;ck kleiner, sagte Dekan Saurer, dabei k&ouml;nne es geschehen, da&szlig; man seine Identit&auml;t verliere. Umso mehr sei es notwendig, sich auf die Wurzeln zu besinnen, die der Tiroler Sch&uuml;tzenverband in seinem Grundsatzpapier verankere: &quot;Treue zur Heimat, zum Glauben und zum Brauch der V&auml;ter&quot;.</p>
