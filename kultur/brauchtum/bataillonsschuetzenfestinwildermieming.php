  <td colspan="3"><h2><a name="031f2a9b0508b8e02"></a>Bataillonssch&uuml;tzenfest H&ouml;rtenberg in Wildermieming</h2></td>
  <td></td>
  </tr>
      <tr>
    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Das Bataillonssch&uuml;tzenfest H&ouml;rtenberg in Wildermieming fand am Sonntag, dem 24. August 2008 bei strahlendem Sonnenschein statt. Die Sch&uuml;tzen kamen neben den Gastgebern aus Wildermieming aus Burgstall in S&uuml;dtirol, Stuttgart, Zirl, Roppen, M&ouml;tz, Obsteig, Mieming, Ranggen, Hatting, Scharnitz, Flaurling, Polling, Leutasch, Reith b. S., Oberperfuss, Inzing, Telfs, Oberhofen und Kematen. Begleitet von den Musikkapellen Mieming und Wildermieming sowie den Schweglern aus Telfs. Zahlreiche Ehreng&auml;ste, darunter Landtagspr&auml;sident DDr. Herwig van Staa, begleiteten zum Auftakt die rund 600 Aktiven in ihren traditionellen Trachten bei ihrem Zug vom Hotel J&auml;ger durch das Dorf zur Volksschule. Dort zelebrierte zun&auml;chst der Sch&uuml;tzenkurat Msgr. Dekan Franz Saurer eine Feldmesse. 	  	  </td>
    </tr>
