  <td colspan="3"><h2><a name="031f2a9b060120392"></a>Disziplinierte Aktive</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Die Aktiven &uuml;bten sich am Vorabend zum eigentlichen Bataillonssch&uuml;tzenfest in Disziplin, denn am Sonntagmorgen, dem 24. August, um 8.15 Uhr, versammelten sich alle Sch&uuml;tzenkompanien und die Musikkapellen. Der Auftakt eines beeindruckenden Festtages. Den Ehrenschutz &uuml;bernahm Landtagspr&auml;sident DDr. Herwig van Staa, seine Zusage gab er noch als Tiroler Landeshauptmann. Nach dem Abschreiten der Formationen zogen Sch&uuml;tzen, Kanoniere und Musiker vom Hotel J&auml;ger durch das Dorf zur Volksschule.</p>
</td>
