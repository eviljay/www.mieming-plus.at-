  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Weltlicher Abschluss der Osterfeierlichkeiten war das Fr&uuml;hjahrskonzert der Musikkapelle Wildermieming vor rund 200 Besucherinnen und Besucher am Abend des Ostersonntags im Gemeindehaus. Unter den G&auml;sten sahen wir B&uuml;rgermeister Klaus Stocker, Vize-B&uuml;rgermeister Sepp Steinhauser, Altb&uuml;rgermeister Josef Stoll, Alt-Landeskapellmeister Hans Eibl, Bezirksmusikobmann Ing. Bruno Haselwanter, Alt-Dekan Albert Markt und den Ehrenbobmann der MK Wildermieming, Hans Volgger. &quot;Die St&uuml;cke 'Slovenja&quot; und 'Reminiszenzen' waren f&uuml;r uns eine gro&szlig;e Herausforderung, sagte Kapellmeister Alois Kranebitter, der Wildermieming damit auch beim Landeswertungsspiel im Herbst in Innsbruck pr&auml;sentieren m&ouml;chte.<br><br></p>
