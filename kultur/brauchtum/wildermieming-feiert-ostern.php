  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Bei der feierlichen Auferstehungsmesse am Abend des Karsamstags in der Pfarrkirche zum Hl. Nikolaus z&uuml;ndete Pfarrer Albert Markt die Osterkerze an, das Licht wurde anschlie&szlig;end an die Gemeinde weitergereicht. Die brennende Kerze versinnbildlicht dabei Christus als Licht der Welt. Nach der rund eineinhalbst&uuml;ndigen Messe zog die Gemeinde gegen 22 Uhr zum Kinderspielplatz. Jungbauernschaft und Langjugend errichteten zur Entz&uuml;ndung des Osterfeuers einen Holzkegel von ca. vier Meter H&ouml;he. Der Brauch der Osterfeuer geht auf das 16. Jahrhundert zur&uuml;ck.</p>
