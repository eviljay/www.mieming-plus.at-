  <td colspan="3"><h2><a name="8627589d530a07438"></a>Palmweihe und Palmprozession zum Gottesdienst</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Nach der Palmweihe am Palmsonntag, dem 28. M&auml;rz 2010 mit Alt-Dekan Albert Markt an der Friehofskapelle in Wildermieming, wurden an der Pfarrkirche die Palmstangen aufgestellt. Dazu spielte die Bl&auml;sergruppe der Musikkapelle Wildermieming. Nach der Prozession durch das Dorf, versammelte sich die Gemeinde zum Gottesdienst in der Pfarrkirche. Klaus Trenkwalder spielte die Orgel. Zur anschlie&szlig;enden St&auml;rkung luden die Ortsb&auml;uerinnen unter Leitung von Isabella Zauscher alle Teilnehmer der Palmprozession ins Gemeindehaus ein. Der Erl&ouml;s kommt der bevorstehenden Pfarrkichensanierung zugute.</p>
