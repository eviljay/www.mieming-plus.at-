	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Seit der junge Lehrer Klaus Trenkwalder 1967 die Leitung der Volksschule Wildermieming &uuml;bernommen hatte, gab es an dieser Minischule einen Sch&uuml;lerchor. Dieser entwickelte sich zu einem hervorragenden Kinderchor und qualifizierte sich zwei Mal zur Teilnahme am &Ouml;sterreichischen Bundesjugendsingen &ndash; die h&ouml;chste Stufe, die ein Pflichtschulchor in &Ouml;sterreich erreichen kann. Dieser Volksschulchor ging &uuml;ber in einen Jugendchor, der auch internationale Bekanntheit erlangte. 	  	  </td>
