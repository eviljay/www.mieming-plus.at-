  <td colspan="3"><h2><a name="031f2a9aa906d206b"></a>Zwei Auftritte im Fernsehen</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Die Veranstaltung von eigenen Chorkonzerten ist in einem kleinen Dorf nat&uuml;rlich schwierig. So traten die S&auml;nger in den ersten Jahren gemeinsam mit der Musikkapelle Wildermieming beim Fr&uuml;hjahrskonzert im Rathaussaal Telfs auf. Auch ein Konzert in der Pfarrkirche Wildermieming gemeinsam mit dem Chor der Slawistikstudenten an der Universit&auml;t Innsbruck fand gro&szlig;e Beachtung. Zum zehnj&auml;hrigen Jubil&auml;um organisierten Chor und Musikkapelle gemeinsam ein Almsingen auf der Tillfu&szlig;alm im Gaistal, das &ndash; wohl auch bedingt durch das herrliche Wetter &ndash; einen gro&szlig;en Zuh&ouml;reransturm ausl&ouml;ste. Vor einigen Jahren wurde dieses Almsingen beim Bergdoktorhaus durchgef&uuml;hrt. Auch hier begeisterten Musikanten und Ch&ouml;re mit ihren guten Leistungen und bunten Trachten ein zahlreiches Publikum. Zum zwanzigj&auml;hrigen Jubil&auml;um nahm man gerne eine Einladung der &bdquo;V&ouml;lser S&auml;ngerrunde&ldquo; zum gemeinsamen Josefikonzert an und erntete durch den gepflegten und frischen Chorklang viel Applaus.<br><br>H&ouml;hepunkte aus medialer Sicht waren zwei Auftritte im &ouml;sterreichischen Fernsehen. Einmal wurde im Rahmen der Sendung &bdquo;Sing mit&ldquo; das Dorf und der Singkreis Wildermieming einem breiten Fernsehpublikum vorgestellt, ein anderes Mal waren die S&auml;ngerInnen zu Gast bei Sepp Forcher in der Sendung &bdquo;Klingendes &Ouml;sterreich&ldquo;.<br><br>Auch mehrer Chorfahrten festigten die Freundschaftsbande der S&auml;ngerInnen und ihrer Angeh&ouml;rigen. So besuchte man die Toskana, sang im Innenhof der Musikhochschule von Siena und gab in der Abteikirche von San Antimo ein kleines Chorkonzert, das die vielen - vor allem italienischen - Besucher begeisterte.<br>Eine andere Reise f&uuml;hrte nach Steyr und in die Wachau und vor kurzem besuchte der Chor S&uuml;db&ouml;hmen, Budweis und Krumau.<br><br>Zwanzig Jahre ist eine lange Zeit und so manche S&auml;ngerInnen verlie&szlig;en den Chor, andere stie&szlig;en neu dazu. Zur Zeit hat der Singkreis zwanzig Mitglieder und ist in allen Stimmen recht gut besetzt. Wie in vielen Ch&ouml;ren mangelt es ein bisschen an M&auml;nnerstimmen und der eine oder andere Tenor oder Bass w&auml;re herzlich willkommen. Der Stress h&auml;lt sich in Grenzen, das Wohlf&uuml;hlen beim Singen und bei einem Glasl nach der Probe ist wichtiger. Vielleicht ist dieser Beitrag Anlass f&uuml;r den Einen oder die Andere es einmal bei einem Chor zu probieren. Die Proben beginnen wieder Mitte September. Ansprechpartner ist der Chorleiter Klaus Trenkwalder oder auch jedes andere Chormitglied.</p>
