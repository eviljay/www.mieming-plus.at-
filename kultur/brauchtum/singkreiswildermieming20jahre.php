  <td colspan="3"><h2><a name="031f2a9aa906ac35e"></a>Hervorgegangen aus dem Jugendchor</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>1988 entstand der Wunsch, diesen Jugendchor in einen Gemischten Chor umzuwandeln. Am 29. Februar 1988 fand in einem Klassenraum der Volksschule die erste Chorprobe statt &ndash; und somit auch die Begr&uuml;ndung des Singkreises Wildermieming. Seit damals finden die Proben auch in der Schule statt &ndash; mittlerweile in der neu geschaffenen Aula mit guter Akustik.<br><br>Der Sinn dieses Chores war von Anfang an die Pflege des gemeinsamen Singens von Liedern aus den verschiedensten Kulturkreisen wie Volkslied, Spiritual, Geistliches Liedgut und alte Meister sowie auch moderne Kompositionen. Wichtig war und ist die Freude am Singen und die dadurch entstandene Freundschaft. Die Proben finden jeden Mittwochabend von Mitte September bis Ende Mai statt. Dann folgt eine ausgedehnte Sommerpause.<br><br>Schnell kristallisierten sich einige Fixpunkte heraus. So gestaltet der Singkreis seit seinem Bestand die Weihnachtsmette in der Pfarrkirche Wildermieming und auch sonst so manchen Gottesdienst zu festlichen Anl&auml;ssen. Seit vielen Jahren &uuml;bernehmen die S&auml;ngerInnen eine Stunde der n&auml;chtlichen Anbetung am Karfreitag mit Meditation und geistlichen Liedern. Auch den alten Brauch des Ankl&ouml;pfelns im Advent lie&szlig; man wieder aufleben und zieht an so manchem dunklen Dezemberabend von Haus zu Haus, um den Menschen die alten Herbergslieder vorzutragen.</p>
</td>
