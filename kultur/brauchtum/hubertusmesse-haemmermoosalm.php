  <td colspan="3"><h2><a name="8627589ca10c31d55"></a>Hubertusmesse mit Mieminger Jagdhornbl&auml;sern</h2></td>
  <td></td>
  </tr>
      <tr>
    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Die J&auml;gerschaft rund um die Jagdreviere der Leutasch feierten mit der Hubertusmesse und der Segnung des Hubertushirsches auf der H&auml;mmermoosalm im Gaistal der Gemeinde Leutasch ihr Erntedankfest. Die Hubertusmesse vor rund 300 Besuchern umrahmten musikalisch die Jaghornbl&auml;ser Mieminger Plateau mit ihrem Dirigenten Alfred Widauer. Den Hubertushirsch, einen 18-Ender, erlegte heuer im Revier Gehrn der Jagdherr Dipl. Ing. Gerd Sommer. Mit dem Hubertusfestakt beendete die Olympiaregion Seefeld ihre 11. Ganghofer Hubertuswoche, die traditionell im Zeichen von Jagd &amp; Natur steht. Die G&auml;ste wurden begr&uuml;&szlig;t vom Leutascher B&uuml;rgermeister Thomas M&ouml;&szlig;mer und Markus Tschoner, Direktor des TVB Olympiaregion Seefeld.	  	  </td>
    </tr>
</table><br><br>
</td>
    <td></td>
    </tr>
    
