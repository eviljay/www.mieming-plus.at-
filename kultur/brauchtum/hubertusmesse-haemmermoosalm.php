  <td></td>
  <td colspan="3"><h2><a name="8627589ca10d7ad5b"></a>Die Tradition der Hubertusmesse</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Die Hubertusmesse wird &uuml;blicherweise am Hubertustag, dem 3. November gefeiert. &quot;Im November liegt bei uns schon Schnee&quot;, so Hans Neuner, deshalb haben wir die Feier auf den 10. Oktober vorgezogen. Das Erntedankfest der J&auml;ger ist St. Hubertus, dem Schutzpatron der Jagd gewidmet. Pfarrer Josef Schm&ouml;lzer: &quot;Der &Uuml;berlieferung nach war Hubertus als junger Edelmann ein leidenschaftlich ausschweifender J&auml;ger. Sp&auml;ter erkannte Hubertus in allen Wesen Gesch&ouml;pfe g&ouml;ttlichen Ursprungs und hat sich deshalb hegend und pflegend f&uuml;r sie verwandt&quot;.</p>
</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../../images/8627589ca10daad5c.jpg" target="_blank"><img src="../../images/8627589ca10daad5c_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>Hubertushirsch </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
