  <td></td>
  <td colspan="3"><h2><a name="8627589ca10d35a58"></a>Faszination Wild, Jagd und Natur</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Die Ganghofer-Hubertuswoche wird auch im elften Jahr von Monika und Hans Neuner aus Leutasch-Klamm organisiert. Hans Neuner, vulgo &quot;Bantl&quot;, ist Jager und Wanderf&uuml;hrer. Die Hubertuswoche startete mit einer von ihm gef&uuml;hrten Wanderung zum Thema &quot;Hirschgerechte Zeichen&quot;. Die Hubertusmesse wurde zum ersten Mal in der H&auml;mmermoosalm gefeiert. In den Jahren davor war die Magdalenkirche in Leutasch Ort des Geschehens. Pfarrer Josef Schm&ouml;lzer mit Verweis auf die Legende des Heiligen Hubertus: &quot;Beugen wir uns vor dem Sch&ouml;pfer, der es zul&auml;sst, dass wir jeden Tag die Faszination Wild, Jagd und Natur erleben d&uuml;rfen&quot;.</p>
</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../../images/8627589ca10d69759.jpg" target="_blank"><img src="../../images/8627589ca10d69759_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>Monika + Hans Neuner </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="8627589ca10db385e"></a>Jagdhornbl&auml;ser ernteten viel Applaus</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Die Hubertusmesse steht im engen Zusammenhang mit den sich ver&auml;ndernden Erscheinungen des Jahreskreises. Sie wird meist im herbstlichen Revier aufgef&uuml;hrt, das durch seine pr&auml;chtigen Farben, die Ernte der Fr&uuml;chte und die Jagd gepr&auml;gt ist. Mit dem Blasen der Hubertusmesse folgen die Jagdhornbl&auml;ser einem jahrhundertealten Brauch, bei dem die blasenden H&ouml;rner in der Natur die fehlende Kirchenmusik f&uuml;r das Abhalten der liturgischen Messe vor Beginn oder am Ende der Jagd ersetzen. Die Jagdhornbl&auml;ser aus Mieming und Wildermieming ernteten nach der Messe viel Applaus und gaben bis zu drei Zugaben beim anschlie&szlig;enden Jagerabend in der H&auml;mmermoosalm.</p>
</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../../images/8627589ca10dc655f.jpg" target="_blank"><img src="../../images/8627589ca10dc655f_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>Mieminger Jagdhornbl&auml;ser </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
