  <td></td>
  <td colspan="3"><h2><a name="8627589ca10ddee61"></a>Wild und Wald geh&ouml;ren zusammen</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Die Jaghornbl&auml;sergruppe Mieminger Plateau wurde 1986 in Untermieming gegr&uuml;ndet. Hornmeister (Obmann) ist Karl Frauenhoffer aus Untermieming, musikalischer Leiter Josef Widauer aus Obermieming und von Alfred Widauer aus Telfs werden die Hornisten geleitet. Die Jagdhornbl&auml;ser stammen &uuml;berwiegend aus Mieming und Wildermieming und sind alle passionierte J&auml;ger und Heger. &quot;Wild und Wald geh&ouml;ren zusammen und deshalb ist es eine unserer wichtigsten waidm&auml;nnischen Aufgaben, die sensible Natur im Gleichgewicht zu halten&quot;, sagt Josef Widauer.</p>
</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
