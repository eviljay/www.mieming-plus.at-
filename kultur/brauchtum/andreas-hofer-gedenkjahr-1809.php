  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Dem Komponisten Florian Pedarnig, der f&uuml;r die Zugabe mit Musikanten der MK Mieming und Wildermieming von Christian Holzeis den Dirigentstab &uuml;bernahm, sei der w&uuml;rdige Rahmen, in dem sein Werk pr&auml;sentiert werde, ein gro&szlig;es Anliegen, so Holzeis. Die Musikkapelle Mieming spielte zum Auftakt u.a. neben der Ouvert&uuml;re &bdquo;Klingendes Land&ldquo; und &bdquo;Tirol 1809&ldquo; von Sepp Tanzer, den Marsch &bdquo;Tirol is lei oans&ldquo; vom anwesenden Wahlmieminger Prof. Sepp Hell.</p>
