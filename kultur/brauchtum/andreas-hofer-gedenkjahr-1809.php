  <td colspan="3"><h2><a name="8627589c320bc3ba4"></a>Konzertanter Abend mit Sch&uuml;tzenkompanien</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Hannes Spielmann, Obmann der Musikkapelle Mieming, ging in seiner Begr&uuml;&szlig;ung auf den Ablauf des Festaktes ein, der vom Barwieser Volksschuldirektor Hans-Christian Ringer historisch informativ und spannend moderiert wurde. Den musikalischen Teil des Abends gestalteten die Musikkapellen Mieming und Wildermieming, unter der Leitung von Christian Holzeis, f&uuml;r den ehrw&uuml;rdigen Rahmen sorgte die Beteiligung der Sch&uuml;tzenkompanien Eyrs (S&uuml;dtirol), Wildermieming und Mieming unter dem Kommando von Hauptmann Thomas Schneider.</p>
</td>
