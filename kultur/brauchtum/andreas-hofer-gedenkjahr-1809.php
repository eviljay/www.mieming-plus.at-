  <td colspan="3"><h2><a name="8627589c100ad6c03"></a>Festmusik zum Andreas-Hofer-Gedenkjahr in Mieming</h2></td>
  <td></td>
  </tr>
      <tr>
    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Um die 500 G&auml;ste folgten am Samstag, dem 20. Juni 2009 der Einladung der Musikkapelle und Sch&uuml;tzenkompanie Mieming zur Festmusik zum Andreas-Hofer-Gedenkjahr 1809 am Eduard-Walln&ouml;fer-Platz im Mieminger Ortsteil Barwies. Gemeinsam mit den Sch&uuml;tzenkompanien Wildermieming und Eyrs (S&uuml;dtirol) sowie der Musikkapelle Wildermieming wurde den Besuchern von insgesamt 200 Mitwirkenden am Vorabend des Herz-Jesu-Sonntags ein fesselnder thematischer Abend geboten. H&ouml;hepunkt war der &quot;Tiroler Zapfenstreich&quot;. Unter den Ehreng&auml;sten waren Landtagspr&auml;sident DDr. Herwig van Staa, der &ouml;sterreichische Generalkonsul am Hl. Stuhl in Rom, Mag. Franz Wechner, Landessch&uuml;tzenkommandantstv. Mjr. Stefan Zangerl und der Komponist der &bdquo;Festmusik der Sch&uuml;tzen&ldquo;, Florian Pedarnig. 	  	  </td>
    </tr>
</table><br><br>
</td>
    <td></td>
    </tr>
    
