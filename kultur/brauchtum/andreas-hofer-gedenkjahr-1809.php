  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>&bdquo;Ein ehrliches Gedenken und Erinnern an die Ereignisse von 1809 ist unser Beitrag zum heurigen Gedenkjahr&ldquo;, betonte Christian Holzeis, Kapellmeister der Musikkapelle Mieming und der Abend solle kein &bdquo;Event oder Spektakel&ldquo; sein. Der konzertante Teil bestand aus rein thematischer Musik, w&auml;hrend im zweiten Teil die Festmusik der Sch&uuml;tzen, zusammengestellt und instrumentiert von Florian Pedarnig gespielt wurde. &bdquo;Die enge Verbundenheit des Sch&uuml;tzentums mit dem Glauben zieht sich wie ein roter Faden durch das ganze Werk&ldquo;, so Christian Holzeis.</p>
</td>
