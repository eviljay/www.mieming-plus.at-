  <td></td>
  <td colspan="3"><h2><a name="031f2a9ab0022767f"></a>Dokumentation: Tradition und Brauchtum</h2></td>
  <td></td>
  </tr>
      <tr>
    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td class="cell" align="left" valign="top">
		      	        Fronleichnamsprozessionen am Mieminger Plateau geh&ouml;ren zu den christlichen Brauchtumsveranstaltungen, die auch heute noch vom ganzen Dorf gefeiert werden. Im vergangenen Jahr waren wir bei der Prozession in Untermieming, heuer dokumentieren wir in Mieming-Plus die Fronleichnamsprozession in Wildermieming, geleitet von Pater Hermann, Rektor im Missionshaus, Absam. G&uuml;nther Stecher hat den Prozessionsverlauf fotografiert. Gerlinde Stoll aus Wildermieming informiert &uuml;ber die Hintergr&uuml;nde von Fronleichnam. &nbsp;</font>
	  	  </td>
    </tr>
</table><br><br>
</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="031f2a9ab001a9a0b"></a>Fronleichnam</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Das Fronleichnamsfest, ist offiziell das &bdquo;Hochfest des Leibes und Blutes Christi&ldquo;, auch unter Blutstag bekannt, mit dem die leibliche Gegenwart Jesu Christi im <i>Sakrament der Eucharistie</i> gefeiert wird. Fronleichnam wird am Donnerstag nach dem Dreifaltigkeitsfest, dem zweiten Donnerstag nach Pfingsten, begangen (60. Tag nach dem Ostersonntag) und f&auml;llt somit fr&uuml;hestens auf den 21. Mai und sp&auml;testens auf den 24. Juni. Der Donnerstag als Festtermin kn&uuml;pft an den eigentlichen Gedenktag des letzten Abendmahls, den Gr&uuml;ndonnerstag an.</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../../images/031f2a9ab0024f685.jpg" target="_blank"><img src="../../images/031f2a9ab0024f685_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>Fronleichnamsfest </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="031f2a9ab001b870c"></a>Feierliches Hochamt</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Vor der Fronleichnamsprozession findet in der Kirche ein feierliches Hochamt statt. Die anschlie&szlig;ende Prozession deutet an, dass der Glaube an Jesus Christus nicht innerhalb der Kirchenmauern verbleiben darf. Christen sind dazu aufgerufen, die frohe Botschaft des Glaubens in die Welt hinaus zu tragen.<br></td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../../images/031f2a9ab00255a87.jpg" target="_blank"><img src="../../images/031f2a9ab00255a87_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>Pater Hermann </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="031f2a9ab001bb10d"></a>Prozessionen mit Monstranz</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Die Prozession zu Fronleichnam ist Bekenntnis zum Glauben an die Gegenwart Christi in der Hostie, dem verwandelten Brot, das in der &quot;Monstranz&quot;, einem kostbaren Schaugef&auml;&szlig; mit kleinen Glasfenstern meist unter einem Baldachin (&quot;Himmel&quot;) vom Priester in der Prozession durch das Dorf und zwischen den bl&uuml;henden Wiesen mitgetragen wird.</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../../images/031f2a9ab0025ea89.jpg" target="_blank"><img src="../../images/031f2a9ab0025ea89_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>Bekenntnis zum Glauben </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="031f2a9ab0023ef80"></a>Blumenteppiche, Buchen- und Birkenzweige</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>M&auml;dchen gestalten bereits am Vorabend zur Prozession, an dem Ort, wo dann der Altar mit der Monstranz aufgestellt wird, wundersch&ouml;ne Blumenteppiche. Den Weg der Prozession schm&uuml;cken Buchen- oder Birkenzweige. Zwischen den wehenden, reich bestickten Fahnen tragen Frauen eine Muttergottesstatue, und die Statue der Hl. Mutter Anna. Eine kleine Statue der Hl. Maria wird von vier Buben mitgetragen.</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../../images/031f2a9ab00267d8b.jpg" target="_blank"><img src="../../images/031f2a9ab00267d8b_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>Buchen- und Birkenzweige </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="031f2a9ab00242281"></a>Salutsch&uuml;sse</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
