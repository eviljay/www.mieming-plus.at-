            <tr bgcolor="#EFEFEF">
	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        50 Musikerinnen und Musiker der Musikkapelle Mieming zogen am Silvestertag, dem 31. Dezember 2009 durch alle Orsteile ihrer Gemeinde von Haus zu Haus und w&uuml;nschten &bdquo;a guat&rsquo;s nuis Jahr 2010!&ldquo;. Das Silvester-Blasen der Musikkapelle Mieming ist ein alter Brauch. Mieming-Plus begleitete eine Gruppe durch Untermieming. Auf dem erlebnisreichen Weg trafen wir unter anderem die Familie von Martin H&ouml;llwarth, dem ehemaligen &ouml;sterreichischen Weltklasse-Skispringer. Im Haus von Judith und Elmar Au&szlig;erlechner wurden unsere Silvester-Bl&auml;ser mit einem Gegen-St&auml;ndchen &uuml;berrascht. Ein weiterer H&ouml;hepunkt war die Station im Hause von Rosmarie und Johann Krug in Tabland. Beim wohl bekanntesten Schnapsbrenner wurden Schlehen, Zwetschken, Brombeer, Holunder, Vogelbeere und Nusseler gereicht. &quot;Mangeler's Edelbr&auml;nde&quot; hatten es in sich. Unsere Eindr&uuml;cke dokumentieren wird in unserer Fotoreportage mit 160 Bildern. 	  	  </td>
