  <td colspan="3"><h2><a name="8627589cf50b876b2"></a>Eine alte Tradition in Mieming</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>&bdquo;Das Silvester-Blasen&ldquo; gibt es in Mieming seit Anfang 1930&ldquo;, erkl&auml;rt Hannes Spielmann, Obmann der Musikkapelle Mieming. In den 60er und Anfang der 70er Jahre waren es noch zwei Gruppen, die in Mieming musikalische Neujahrsgr&uuml;&szlig;e &uuml;berbrachten &ndash; die &bdquo;Obere&ldquo; und die &bdquo;Untere Partie&ldquo;. Zust&auml;ndig f&uuml;r Ober- und Unter-Mieming mit insgesamt acht Ortsteilen. Inzwischen sind es f&uuml;nf Partien, die den ganzen Silvestertag auf dem Weg sind. &bdquo;Das ist pers&ouml;nlicher und so k&ouml;nnen wir den meisten Miemingern &bdquo;a guat&rsquo;s nuis Jahr&ldquo; w&uuml;nschen&ldquo;, sagt Hannes Spielmann.</p>
