    <td valign="top"><p>Der Krampusbrauch war urspr&uuml;nglich in ganz &Ouml;sterreich verbreitet und wurde in der Zeit der Inquisition verboten, da es bei Todesstrafe niemandem erlaubt war, sich als teuflische Gestalt zu verkleiden. In schwer zug&auml;nglichen Orten wurde dieser Brauch dennoch weitergef&uuml;hrt. In vielen D&ouml;rfern und St&auml;dten Tirols gibt es heute wieder Krampusumz&uuml;ge, bei denen Tuifl unter lautem L&auml;rm ihrer Glocken durch die Stra&szlig;en ziehen, um Passanten zu erschrecken. Krampustag ist der 5. Dezember, w&auml;hrend das Fest des Heiligen Nikolaus auf den 6. Dezember (Nikolotag) f&auml;llt; &uuml;blicherweise erscheinen aber beide Gestalten gemeinsam am Abend des 5. Dezembers zum Teil jedoch auch am 6. Dezember.</p>
