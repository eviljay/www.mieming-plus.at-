  </tr>
      <tr>
    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Die weit &uuml;ber 400 Besucherinnen und Besucher des 3. Wildermieminger Krampuslaufes mussten am Freitag, dem 27. November 2009 keine &uuml;berdurchschnittlichen Vorstellungskr&auml;fte mobilisieren, um sich beim Auftritt der Tuifl aus Telfs, Oetz, Kufstein und Wildermieming geradewegs in der H&ouml;lle zu w&auml;hnen. Um 19 Uhr startete das Teufelsspektakel auf dem Platz vor der Volksschule in Wildermieming. Die Wildermieminger Tuifl sind im Verein Grieslehn-Pass organisiert. Ihr Motto beim heurigen Krampuslauf war &bdquo;Gut &amp; b&ouml;se&ldquo;. Nach dem Krampuslauf, an dem neben den Jung- und Alt-Tuifln aus Wildermieming noch drei Gastgruppen teilnahmen, gab es Musik und Unterhaltung mit den Alpen-Cowboys im beheizten Zelt am Festplatz. Unter den G&auml;sten sahen wir u.a. auch B&uuml;rgermeister Josef Stoll. 	  	  </td>
    </tr>
</table><br><br>
</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="8627589cd30da1ec1"></a>Auftakt mit den Wildermieminger Jungtuifl</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Den Auftakt der rund zweist&uuml;ndigen Show machten die Wildermieminger Jungtuifl, gefolgt von den Flaurlinger Tuifln, dem Kochental-Pass aus Telfs, den Habicher Tuifln aus Oetz und dem Ferrus Pass aus Kufstein. Die Kufsteiner hatten &uuml;brigens auch eine Tuiflin. Grieslehn-Pass &ndash; die Wildermieminger Tuifl machten in neuen Kost&uuml;men den Abschluss. &bdquo;Jedes Jahr wird ein anderer zum &bdquo;Oberteufel&ldquo; gek&uuml;rt, der beim Lauf die Hauptrolle spielt. Ansonsten geht es um den Kampf gut gegen b&ouml;se, bzw. weniger b&ouml;se gegen sehr b&ouml;se&ldquo;, sagt Thomas Scholl.</p>
</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../../images/8627589cd30da5bc2.jpg" target="_blank"><img src="../../images/8627589cd30da5bc2_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>Jungtuifl </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="8627589cd30d760be"></a>Wildermieminger Tuiflverein hei&szlig;t &quot;Grieslehn-Pass&quot;</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Vor drei Jahren hat es die wilden Gesellen samt H&ouml;rnern und Fell direkt aus der Grieslehn-Runse, oberhalb von Wildermieming, &bdquo;herabgesp&uuml;lt&ldquo;, erl&auml;utert Obmann und Vereinsgr&uuml;nder Thomas Scholl. Seither hei&szlig;t der Tuiflverein in Wildermieming auch &bdquo;Grieslehn-Pass&ldquo;. &bdquo;Pass&ldquo; deshalb, weil fr&uuml;her die traditionelle Gruppe aus Nikolaus, Krampus und Korbtr&auml;ger als &bdquo;Pass&ldquo; bezeichnet wurde. 17 Mitglieder hat der Verein, alles junge M&auml;nner zwischen 20 und 30 Jahren.</p>
</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../../images/8627589cd30d842bf.jpg" target="_blank"><img src="../../images/8627589cd30d842bf_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>Grieslehn-Pass </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="8627589cd30dbdfc4"></a>Im Vordergrund steht die H&ouml;llenshow</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Tuifl gab und gibt es in Wildermieming schon l&auml;nger. Sie machten ihre Hausbeuche und waren in der Vergangenheit recht unorganisiert. Das &auml;nderte sich 2003 mit der Vereinsgr&uuml;ndung. &bdquo;Grieslehn-Pass&ldquo; war geboren. Als Traditionsverein sehen sich die Wildermieminger Tuifl nicht. Im Vordergrund steht bei ihnen am Abend des Krampuslaufs die H&ouml;llenshow und das Vergn&uuml;gen der Zuseher, die sich nicht vor den wilderen Gesellen f&uuml;rchten m&uuml;ssen. Zum Schluss lassen alle ihre Masken fallen und sp&auml;testens dann sieht jeder, dass unsere Tuifl ortsbekannte Burschen sind.</p>
