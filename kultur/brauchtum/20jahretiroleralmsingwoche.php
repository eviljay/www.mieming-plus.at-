  <td colspan="3"><h2><a name="031f2a9aa9063d54c"></a>Erste Tiroler Almsingwoche im Kaunertal</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>So fand im Jahre 1989 die &bdquo;Erste Tiroler Almsingwoche&ldquo; im Kaunertal statt. Gekommen waren 24 S&auml;ngerinnen und S&auml;nger aus allen drei Teilen Tirols. Es wurde gesungen, musiziert und einige Berge des Kaunertales bestiegen. Dazu gab es ein &ouml;ffentliches Abschlusskonzert, in der Wallfahrtkirche Kaltenbrunn wurde ein Gottesdienst gestaltet und f&uuml;r die Teilnehmer eine Musikkassette aufgenommen. Seit zehn Jahren gibt es zur Dokumentation eine CD. Diese Eckpfeiler wurden bis heute beibehalten.</td>
