  <td colspan="3"><h2><a name="031f2a9aa9064624d"></a>Jedes Jahr in einem anderen Bezirk</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Jedes Jahr findet die Almsingwoche in einem anderen Bezirk Tirols statt, davon je zweimal auch in Osttirol und in S&uuml;dtirol. Der Kreis der Teilnehmer wurde immer gr&ouml;&szlig;er und erreichte in den letzten Jahren stets an die 100 Personen, darunter auch S&auml;ngerIinnen aus anderen Bundesl&auml;ndern. Im Sinne eines gemeinsamen Tirol war immer ein Drittel der Pl&auml;tze f&uuml;r Teilnehmer aus S&uuml;dtirol reserviert und wurde von diesen auch gerne wahrgenommen. Au&szlig;erdem hatten die Bewohner der Gastgebergemeinde Gelegenheit, ohne Kursbeitrag an der Veranstaltung teilzunehmen, was auch vielfach genutzt wurde.</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
