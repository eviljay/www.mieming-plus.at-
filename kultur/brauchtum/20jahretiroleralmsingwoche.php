  <td></td>
  <td colspan="3"><h2><a name="031f2a9aa905dd540"></a>R&uuml;ckblick</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Nach mehreren Jahren der Chorleitung bei der &bdquo;S&uuml;dtiroler Sing und Wanderwoche&ldquo; im Pustertal entschloss sich der Wildermieminger Chorleiter Klaus Trenkwalder diese Veranstaltung in Nordtirol aufzubauen. Herbert Degenhart aus Pettnau &uuml;bernahm die organisatorische Leitung und der damalige Landesrat f&uuml;r Finanzen Franz Kranebitter aus Telfs unterst&uuml;tzte diese Initiative mit Begeisterung.</td>
