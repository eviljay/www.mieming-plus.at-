  <td colspan="3"><h2><a name="031f2a9aa9065c352"></a>Einmaliges Erlebnis</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Das Singen in einem Chor dieser Gr&ouml;&szlig;e ist f&uuml;r die S&auml;nger ein einmaliges, gro&szlig;artiges Erlebnis. Und diese Chorproben sind umrahmt von Wanderungen in die jeweilige Bergwelt, deren h&ouml;chste auf &uuml;ber 3000m in Obergurgl und in Sulden am Ortler f&uuml;hrten. Die finanzielle Basis schuf in allen diesen Jahren das Kulturreferat des Landes Tirol. Da die anf&auml;ngliche Unterst&uuml;tzung durch den Tiroler S&auml;ngerbund aus unerkl&auml;rlichen Gr&uuml;nden bald zur&uuml;ckgezogen wurde, bem&uuml;hte sich der Organisator Herbert Degenhart um Sponsoren und fand diese erst in der Raiffeisenlandesbank Tirol, sp&auml;ter in der Tiroler Wasserkraft.</td>
