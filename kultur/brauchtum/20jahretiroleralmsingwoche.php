  <td></td>
  <td colspan="3"><h2><a name="031f2a9aa90631f4b"></a>Singen und Bergwandern</h2></td>
  <td></td>
  </tr>
      <tr>
    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td class="cell" align="left" valign="top">
		      	        Die Tiroler Almsingwoche ist ein Unikat im Rahmen der gesanglichen Aus- oder Fortbildungswochen. Sangesbegeisterte Menschen treffen einmal im Jahr zusammen, um eine Woche lang miteinander einerseits im gro&szlig;en gemischten Chor zu singen und andererseits die Umgebung und die Berge ihres jeweiligen Standortes zu erwandern und zu besteigen. Sie geht auf eine Initiative des Wildermieminger Chorleiters Klaus Trenkwalder zur&uuml;ck. Heuer treffen sich die Tiroler S&auml;ngerinnen und S&auml;nger zum letzten Mal zum Singen und Wandern. Nach 20 Jahren wird die Aktion mit einem Treffen im Kaunertal beendet. &nbsp;</font>
	  	  </td>
    </tr>
</table><br><br>
</td>
    <td></td>
    </tr>
    
