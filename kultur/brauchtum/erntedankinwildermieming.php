  <td></td>
  <td colspan="3"><h2><a name="031f2a99c10269484"></a>Jungbauern-Tradition</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Die ersten als &bdquo;Erntedank&ldquo; bezeichneten Feste fanden in Tirol in den siebziger Jahren des 20. Jahrhunderts statt. Ins Leben gerufen und organisiert wurden sie damals wie heute von den Jungbauern der einzelnen Gemeinden, so auch in Wildermieming. &quot;Der Zusammenhalt unter den Jungbauern ist recht gut&quot;, sagt Obmann Matthias Fink (27), von Beruf Hauptschullehrer und nebenberuflich in Stall und Feld im Finkhof beim Vater t&auml;tig.</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
