			<p class="illu"><strong>Jungbauern Wildermieming </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="031f2a99c1028f089"></a>Erntedankkrone und b&auml;uerliche Produkte</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Ein wichtiges Brauchrequisit ist die Erntedankkrone &ndash; sie wird aufwendig aus Buchs, Getreidegarben, Blumen und Feldfr&uuml;chten gestaltet. Sie gilt als bildhaftes Symbol f&uuml;r die eingebrachte Ernte und wird als Dank daf&uuml;r in vielen (unentgeltlichen) Arbeitsstunden hergerichtet. Neben der Erntekrone werden manchmal auch K&ouml;rbe mit verschiedenen b&auml;uerlichen Produkten in die Kirche mitgenommen und dort geweiht.</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
