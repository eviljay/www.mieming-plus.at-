  <td></td>
  <td colspan="3"><h2><a name="031f2a99c10179a81"></a>Feldmesse mit Pater Robert Locher</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Das Erntedank-Fest in Wildermieming, am Sonntag, dem 30. September 2007, begann um 9 Uhr mit einer Feldmesse auf dem Dorfparkplatz vor dem Ladele von Josef Hafele, zelebriert von Pater Robert Locher SJ., seit 2004 Kirchenrektor, d.h. Verantwortlicher f&uuml;r die Jesuitenkirche in Innsbruck. F&uuml;r eine stimmungsvolle Umrahmung sorgten die Musikkapelle Wildermieming, unter der Leitung von Alois Kranebitter. Gesungene F&uuml;rbitten und das Gebet nach der Kommunion spendeten die Kinder der Volksschule.<br></td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
