	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Das Sch&ouml;lfelerfest wird zum festen Bestandteil erfolgreicher Jahresfeste in Wildermieming: Zum 2. Sch&ouml;lfelerfestl der Freiwilligen Feuerwehr unter dem Motto &bdquo;all you can eat&ldquo; (&bdquo;soviel Du essen kannst&ldquo;) kamen heuer &uuml;ber 350 Besucherinnen und Besucher. Emil Wackerle: &quot;&Uuml;ber 180 Kilo Erd&auml;pfel wurden bis Mitternacht verspeist, zehn Kilo mehr und wir w&auml;ren ausverkauft gewesen&quot;. Zu den vier gereichten Erd&auml;pfelsorten Planta, Violetta, Laura und Freya, gab es Speck, Kaminwurzen, Graukas, K&auml;se, selbstgebackenes Brot, Milch und Buttermilch. Das sp&auml;tsommerliche, gute Wetter verw&ouml;hnte die G&auml;ste zus&auml;tzlich. Das &bdquo;Joggl-Trio&ldquo; sorgte f&uuml;r stimmungsvolle Tanzmusik. 	  	  </td>
