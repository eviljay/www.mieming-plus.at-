  <td colspan="3"><h2><a name="8627589c8f0b23431"></a>Sch&ouml;lfeler sind in der Schale gekochte Erd&auml;pfel</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Die Sch&ouml;lfeler haben ihren Weg zwar noch nicht in die Lexika dieser Welt geschafft, aber Wildermieming wird sicherlich daf&uuml;r sorgen, dass im Tirol-Lexikon eines Tages zu lesen ist &bdquo;Sch&ouml;lfeler sind in der Schale gekochte Erd&auml;pfel, die man mit Butter und Salz isst oder mit Graukas und Speck&ldquo;. Bei unseren deutschen Nachbarn hei&szlig;en die Sch&ouml;lfeler &bdquo;Pellkartoffeln&ldquo;, im Rheinland &bdquo;Quallmann&ldquo; oder &bdquo;Quellm&auml;nner&ldquo;, im restlichen &Ouml;sterreich gekochte Erd&auml;pfel oder &quot;Gsottene&quot; und in der Schweiz &bdquo;Gschwellti&ldquo;. Das Garen in der Schale sorgt &uuml;brigens f&uuml;r geringere Verluste an Geschmacks- und N&auml;hrstoffen als bei gesch&auml;lten Erd&auml;pfeln.</p>
