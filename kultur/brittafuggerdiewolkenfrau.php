  <td colspan="3"><h2><a name="031f2a99d70ab4249"></a>Gro&szlig;es Interesse an der Lesung</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>&bdquo;Wir haben nicht mit soviel Publikum gerechnet&ldquo;, sagte der Telfer Altb&uuml;rgermeister Helmut Kopp in seiner stimmungsvollen Begr&uuml;&szlig;ung. F&uuml;r eine literarische Lesung in der Tat ungew&ouml;hnlich. Britta Fugger freute sich &uuml;ber das gro&szlig;e Interesse an der Lesung ihres ersten Erz&auml;hlbandes &bdquo;Die Wolkenfrau&ldquo;, einer Sammlung von Alltagsgeschichten, die sie in den vergangenen zehn Jahren verfasste und immer wieder &uuml;berarbeitet hatte.</td>
    <td><img src="../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../images/031f2a99d70ab2547.jpg" target="_blank"><img src="../images/031f2a99d70ab2547_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>Altbgm. Helmut Kopp </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="031f2a99d70adb84c"></a>Einblicke in die Alltagsseele der Erz&auml;hlfiguren</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Die Erz&auml;hlungen gew&auml;hren dem Leser Einblicke in die Alltagsseele der Erz&auml;hlfiguren. Anita im neuen Kleid, Irina, Pandora, Sara und ich tanzen mit Heinrich und dem Mann im Mond in Pennsylvania. Die Schlacht um F1 wird nach der Zwischenlandung in Eden vom Wachdienst in Wien beaufsichtigt. Wie lebt es sich auf einem Balkon? &bdquo;Ich liebe die Worte, die Gedanken und S&auml;tze in diesem Buch&ldquo;, sch&auml;rmt die Lektorin Florentine Prantl.</td>
    <td><img src="../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../images/031f2a99d70ada74a.jpg" target="_blank"><img src="../images/031f2a99d70ada74a_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>Felix Mitterer </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="031f2a99d70b0b454"></a>Buchtitel von Chryseldis Hofer-Mitterer</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>&bdquo;Britta Fugger hat mir immer mal wieder aus ihren Erz&auml;hlungen vorgelesen, mich fasziniert die Sprache ihrer Geschichten&ldquo;, h&ouml;ren wir von Chryseldis Hofer-Mitterer, die f&uuml;r die Umschlaggestaltung gewonnen werden konnte. Ihr Titelbild auf k&ouml;nigsblauem Hintergrund zeigt einen Stuhl, der eine Frau symbolisiert, mit Hochzeitsgewand, im Hintergrund die Wolken. Die Zahlen des Stuhles sind die Geburtstage der Familie Britta Fuggers.</td>
    <td><img src="../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
