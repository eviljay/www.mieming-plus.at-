  <td colspan="3"><h2><a name="031f2a99d70a4ff45"></a>Lesung in Telfs</h2></td>
  <td></td>
  </tr>
      <tr>
    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td class="cell" align="left" valign="top">
		      	        Gemeinsam mit Felix Mitterer las die in Telfs lebende Sprach- und Literaturwissenschaftlerin Britta Fugger aus ihrem ersten Erz&auml;hlband &bdquo;Die Wolkenfrau&ldquo;. Zur Lesung in der Musikschule Telfs kamen am Samstagabend, dem 27. Oktober 2007, &uuml;ber 160 Besucherinnen und Besucher. Stimmungsvolle irische Weisen der &bdquo;Saligen&ldquo; waren eine Hommage an Felix Mitterer, zur Zeit noch in Irland lebend. Die Malerin Chryseldis Hofer-Mitterer gestaltete den Buchtitel der limitierten Auflage des Bandes &quot;Die Wolkenfrau&quot;. &nbsp;</font>
	  	  </td>
    </tr>
