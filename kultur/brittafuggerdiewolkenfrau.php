  <td colspan="3"><h2><a name="031f2a99d70b33655"></a>Kleine bibliophile Auflage</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Auf Wunsch der Autorin Britta Fugger und ihrer Freundin, der Malerin Chryseldis, erscheinen die Erz&auml;hlungen in einer sehr kleinen, bibliophilen Auflage. Jedes Exemplar dieser Reihe ist h&auml;ndisch nummeriert und signiert. Verlegt vom Stamser Journalisten Thomas Ploder unter Bona Editio. &bdquo;Geld kann man damit nicht verdienen&ldquo;, so der viel gelobte Ploder, &bdquo;aber wir wissen ja inzwischen auch, dass Geld den Charakter verdirbt&ldquo;.</td>
