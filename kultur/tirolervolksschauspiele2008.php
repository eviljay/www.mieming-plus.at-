  <td colspan="3"><h2><a name="031f2a9ae40cde14f"></a>Bezug auf &quot;100 Jahre Markterhebung&quot;</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Der Spielplan der Tiroler Volksschauspiele 2008 nimmt Bezug auf das heurige Telfer Jubil&auml;um &bdquo;100 Jahre Markterhebung&ldquo;. Wie kaum eine andere Tiroler Gemeinde ist Telfs von der Industrie und der damit verbundenen Lohnarbeit gepr&auml;gt. Gerade in den heuer gefeierten letzten 100 Jahren zogen die Fabriken Arbeitskr&auml;fte aus dem In&ndash; und Ausland an und viele sind geblieben. Das hat, neben einer Menge Probleme, auch eine aufgeweckte Gemeinde hervorgebracht, die ihre Probleme oft schon bew&auml;ltigt hatte, als anderen Gemeinden solche Probleme erst Sorgen zu machen begannen.</p>
</td>
