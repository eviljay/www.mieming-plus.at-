	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Vorhang auf zu den Tiroler Volksschaupielen 2008, die ihr Programm dem Jubil&auml;umsjahr &quot;100 Jahre Telfs&quot; widmen: Premiere hatte am Samstag, dem 26. Juli 2008 das St&uuml;ck &quot;Herr Puntila und sein Knecht Matti&quot; von Bertold Brecht, in der Inszenierung von Markus V&ouml;llenklee. Au&szlig;erdem werden zwei weitere Produktionen im Programm sein: &quot;Fenn - Das Dorf&quot; von C. Churchill und &quot;Shakespeares s&auml;mtliche Werke leicht gek&uuml;rzt&quot; von A. Long, D. Singer und J. Winfield.	  	  </td>
