  <td colspan="3"><h2><a name="031f2a9a820164909"></a>Herr Puntila und sein Knecht Matti</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Die scharfsichtige und hintersinnige Parabel von Herr und Knecht wirft Fragen auf, die gerade in der letzter Zeit wieder erschreckend aktuell geworden sind. Ist Arbeit nur eine Ware? Macht Geld immer gl&uuml;cklich? Und wie steht es mit der Moral im Kapitalismus? Oder ist er gerade deswegen so erfolgreich, weil er unmoralisch ist? Und - ist er &uuml;berhaupt noch erfolgreich? Aber vor allem: kann lebendiges und saftiges Volkstheater solche Fragen stellen? Bertolt Brecht meint ja - wir werden es versuchen. Regie f&uuml;hren wird Markus V&ouml;llenklee, B&uuml;hne und Kost&uuml;me werden Karl-Heinz Steck und Andrea Kuprian gestalten. Das ca. 25-k&ouml;pfige Ensemble wird sich aus unseren bew&auml;hrten Tiroler Spielern, Telfer Amateuren und einigen G&auml;sten zusammensetzen.</p>
</td>
