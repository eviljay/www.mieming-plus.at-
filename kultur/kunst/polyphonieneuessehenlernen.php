  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Hunderte Besucher str&ouml;mten vergangenen Samstag nach Nassereith zur ersten Ausstellung der neu gegr&uuml;ndeten Galerie Kala. Unter dem Namen &bdquo;Polyphonies&ldquo; vereinigt Christian Hosp zehn K&uuml;nstlerInnen &ndash; Arpana Caur, Ashim Purkayastha, Baiju Parthan, Biju Joze, Dhruva Mistry, Hema Upadhyay, K.T. Shivaprasad, Rajesh Ram, Ravinder Reddy, Suhasini Kejriwal &ndash; aus den unterschiedlichsten Regionen Indiens.</td>
