  <td></td>
  <td colspan="3"><h2><a name="031f2a9bc40d22725"></a>6. Afralithographie von Franz P&ouml;hacker</h2></td>
  <td></td>
  </tr>
      <tr>
    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Der Bildhauer und Zeichner Franz P&ouml;hacker arbeitete seit Mittwoch, dem 4. M&auml;rz 2009 im Atelier der Steindruckerei Stecher &amp; Stecher in Wildermieming an der achtfarbigen 6. Afra-Lithographie &bdquo;Scheinbar befl&uuml;gelt&ldquo;, die in limitierter Auflage zum Afrafest in Wildermieming am 9. August dieses Jahres erstmals &ouml;ffentlich pr&auml;sentiert wird. F&uuml;r Franz P&ouml;hacker war die Arbeit am Stein eine Premiere. Acht Steine musste der K&uuml;nstler lithographieren - f&uuml;r jede Farbe einen. Mieming-Plus begleitete den K&uuml;nstler bei seiner Arbeit in Wildermieming. 	  	  </td>
    </tr>
</table><br><br>
</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="031f2a9bc40e45d28"></a>Gestaltungswille zur Lithographie am Stein</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>F&uuml;r den Bilderhauer, Zeichner und Kunstp&auml;dagogen Franz P&ouml;hacker war es wohl so etwas wie ein zwangsl&auml;ufiges Mu&szlig;, einmal am Stein zu lithographieren. Der K&uuml;nstler ist dem Stein verbunden. Beim letzten Afrafest formulierte er dem Steindrucker G&uuml;nther Stecher gegen&uuml;ber seinen ernsthaften Gestaltungswillen und damit war ausgemacht, die 6. Afralithographie mu&szlig; von Franz P&ouml;hacker kommen. Alles war zum 1. Arbeitstag vorbereitet: die Lithographievorlage, ein Probestein f&uuml;r erste Skizzen und der sorgf&auml;ltig geh&uuml;tete Stein, auf dem Franz P&ouml;hacker lithographieren soll.</p>
</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../../images/031f2a9bc40e67b2f.jpg" target="_blank"><img src="../../images/031f2a9bc40e67b2f_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>Lithographievorlage </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="031f2a9bc40e46d29"></a>P&uuml;nktlichkeit - ein Arbeitsprinzip</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Zeit und Datum waren l&auml;ngst ausgemacht. Mittwoch, 4. M&auml;rz, 11 Uhr. Punkt 11 Uhr l&auml;utet die Klingel im Hause der Steindruckerfamilie Stecher im Wildermieminger Ortsteil Affenhausen. Franz P&ouml;hacker, in Begleitung seines &auml;ltesten Sohnes Daniel (43) tritt ein. P&uuml;nktlichkeit geh&ouml;rt zu den Arbeitsprinzipien des freundlichen, zur&uuml;ckhaltenden K&uuml;nstlers. Kaffee zur Begr&uuml;&szlig;ung. Am Tisch sitzen Walter und G&uuml;nther Stecher. &bdquo;Was macht der Gustl?&ldquo; G&uuml;nther Stecher erz&auml;hlt, dass er August Stimpfl am Vortag noch besuchte und der plane eine Ausstellung.</p>
</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../../images/031f2a9bc40e5982d.jpg" target="_blank"><img src="../../images/031f2a9bc40e5982d_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>Lithographiestein </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="031f2a9bc40e47a2a"></a>Franz P&ouml;hacker zieht es ins Atelier</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Das Begr&uuml;&szlig;ungsgespr&auml;ch geht auf der Ebene weiter. Die gemeinsamen Freunde und K&uuml;nstlerkollegen sind das Thema. Paul Flora etwa, dem es gerade nicht so gut ginge oder Herbert Danler und andere. Franz P&ouml;hacker steht nach knappen 15 Minuten auf. Ihn zieht es ins Atelier. Sein Arbeitsplatz im Atelier der Steindruckerei ist vorbereitet. Da liegt der Stein, Fettstifte in unterschiedlichen Gr&ouml;&szlig;en, die Vorlage zur Lithographie, das Aquarell &bdquo;Scheinbar befl&uuml;gelt&ldquo;. Kurze Einweisung und nach weniger als zwei Minuten sind Franz P&ouml;hackers erste Probeskizzen auf dem Stein zu sehen.</p>
</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../../images/031f2a9bc40e4ed2b.jpg" target="_blank"><img src="../../images/031f2a9bc40e4ed2b_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>Erst-Skizzen am Stein </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="031f2a9bc40e7f631"></a>Lithographiert wird auf dem Solenhofer Plattenkalk</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Lithographiert wird auf einem Solenhofer Plattenkalk. &bdquo;Der einzige Stein, der neutral ist, weil er Fett und Wasser gleichzeitig aufnimmt&ldquo;, erkl&auml;rt Steindrucker G&uuml;nther Stecher. Das Verfahren in der Kurzfassung: Der K&uuml;nstler bringt eine fetthaltige Zeichnung auf den Stein. Beim Drucken wird der Stein befeuchtet. Die nicht bezeichneten Stellen nehmen die Feuchtigkeit auf, die fette Zeichnung st&ouml;&szlig;t sie ab &ndash; nimmt aber im Gegenzug die Farben an. Beim Druck wird mittels Reibepresse die Zeichnung vom Stein auf Papier &uuml;bertragen.</p>
</td>
