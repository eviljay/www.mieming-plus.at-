  <td></td>
  <td colspan="3"><h2><a name="031f2a9a7a07fff0a"></a>Treffpunkt weltoffener Menschen</h2></td>
  <td></td>
  </tr>
      <tr>
    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Das K&uuml;nstlerehepaar Ursula und Dietmar Tiefengraber geh&ouml;rt seit Jahren zum fixen Bestandteil der Kunstszene im Tiroler Oberland und dar&uuml;ber hinaus. Ihr Atelier in Wildermieming ist ein gesch&auml;tzter Treffpunkt weltoffener Menschen mit Lebensart. Die Malerin Ursula Tiefengraber lernte die Bildhauerei und Restauration auf der HTL in Innsbruck. Dietmar Tiefengraber besch&auml;ftigte sich schon Anfang der 80er Jahre mit der Malerei und arbeitet seit 1993 als freischaffender K&uuml;nstler. 1991 beschlossen Ursula und Dietmar Tiefengraber gemeinsame k&uuml;nstlerische Wege zu gehen. 	  	  </td>
    </tr>
</table><br><br>
</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="031f2a9a7a07eb903"></a>Hirn, H&auml;nde und Finger</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Uschi und Didi Tiefengraber arbeiten mit Holz, Beton, Stein, Pigmenten, aber auch mit Weiden vom nahen St&ouml;ttlbach. Ihre Arbeitsger&auml;te sind vor allem, so Dietmar Tiefengraber, &bdquo;Hirn, H&auml;nde und Finger&ldquo;. Und er erg&auml;nzt mit einem verschmitzten Augenzwinkern: &bdquo;Alles, was langsamer ist als wir, wird verarbeitet&ldquo;. Dahinter steckt die N&auml;he zu nat&uuml;rlichen Werkstoffen. Sichtbar, f&uuml;hlbar - &uuml;berall, im Atelier Tiefengraber.</p>
</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../../images/031f2a9a7a086bd2e.jpg" target="_blank"><img src="../../images/031f2a9a7a086bd2e_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>Sichtbar, f&uuml;hlbar </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="031f2a9a7a0b73a54"></a>Der Apfel f&auml;llt nicht weit vom Stamm</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Ursula Tiefengraber kam als 9j&auml;hrige mit ihrer Mutter, der Malerin und Restauratorin Charlotte Friedl 1979 aus Wien nach Wildermieming. Mit ihrem Mann Dietmar wohnt sie nun in unmittelbarer Nachbarschaft der Mutter. Charlotte Friedl lebt und arbeitet im gegen&uuml;berliegenden Haus. Sie besuchte die Akademie f&uuml;r bildende Kunst in Wien und geh&ouml;rt ebenfalls zum Establishment der Tiroler Kunstszene.</p>
</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../../images/031f2a9a7a0b71e52.jpg" target="_blank"><img src="../../images/031f2a9a7a0b71e52_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>Haus Charlotte Friedl </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="031f2a9a7a07edb04"></a>Bereicherung f&uuml;r das kulturelle Leben</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>&Uuml;ber 300 Jahre alt soll er sein, der Stadl in Wildermieming, in dem das Atelier Tiefengraber seit Anfang der 90er Jahre ein zuhause hat. Ursula und Dietmar Tiefengraber retteten den Stadl vor dem Verfall und bereicherten damit das kulturelle Leben ihrer Gemeinde, die inzwischen weit mehr zu bieten hat, als ein verblassendes &bdquo;Bergdoktor-Image&ldquo;. Wenngleich die ersten Jahre in Wildermieming nicht leicht waren: &bdquo;Die Dorfbev&ouml;lkerung war anfangs sehr skeptisch&ldquo;, sagt Ursula Tiefengraber, &bdquo;heute gibt es keine Probleme mehr, wir sind integriert&ldquo;.</p>
</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../../images/031f2a9a7a08a013c.jpg" target="_blank"><img src="../../images/031f2a9a7a08a013c_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>Stadl-Atelier </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="031f2a9a7a07efe05"></a>Auftaktausstellung &quot;Stelzeng&auml;nger&quot;</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Das Atelier Tiefengraber ist in der Tat nicht mehr weg zu denken. Nicht in Wildermieming und schon gar nicht am Mieminger Plateau. Ursula und Dietmar Tiefengraber  begr&uuml;ndeten ihre gemeinsame, k&uuml;nstlerische Arbeit mit ihrer 1. Ausstellung &bdquo;Stelzeng&auml;nger&ldquo; 1999 im eigenen Atelier. Sieben Skulpturen der ersten Schaffensperiode sind noch heute bei Eberhard Hauff und Charlotte Seeling  in Wildermieming-Siedlung zu sehen. Ein Teil der Skulpturen ging nach Hamburg zur Journalistin und Lektorin Monika Schiffer, die bis heute gerne nach Wildermieming zur&uuml;ck kommt.</p>
</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
