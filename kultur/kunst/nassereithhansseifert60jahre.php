	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Bilder aus der aktuellen Schaffensperiode des Absamer Malers Hans Seifert waren bis zum 21. Oktober 2007 in der Kunsthalle Hosp &ndash; Die andere &bdquo;ART&ldquo; , in Nassereith zu sehen. Die etwa 60 Bilder wurden bislang noch nie auf einer Ausstellung gezeigt. Zur Vernissage kamen nach Aussage des Seniorgaleristen Dietmar Hosp so viele G&auml;ste wie niemals zuvor. Hans Seifert arbeitet seit einigen Jahren sehr eng mit der Kunsthalle Hosp zusammen. 	  	  </td>
