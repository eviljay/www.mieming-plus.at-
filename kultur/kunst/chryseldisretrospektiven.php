	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Mit einer Andacht in Wort und Bild am Vorabend von Allerheiligen wurde die erfolgreiche Ausstellung &quot;Retrospektiven&quot; von Chryseldis in Schloss Landeck abgeschlossen. Die Andacht setzte sich mit dem sehns&uuml;chtigen Glauben an die Auferstehung auseinander. Mit einer grossen Retrospektive feierte die Stadt Landeck im Oktober den 60. Geburtstag ihrer K&uuml;nstlerin Chryseldis. Die international anerkannte Malerin lebt und arbeitet in Wildermieming-Affenhausen. 	  	  </td>
