  <td colspan="3"><h2><a name="031f2a99fa0dd771d"></a>Chryseldis-Ausstellung in Ehrwald</h2></td>
  <td></td>
  </tr>
      <tr>
    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Der Einladung zur Ausstellungser&ouml;ffnung &bdquo;Chryseldis&ldquo; in der Galerie Spielmann in Ehrwald folgten am Freitag, dem 30. November 2007 zahlreiche Kunstinteressierte, viele Freunde und Weggef&auml;hrten der K&uuml;nstlerin. Aus Irland reisten Anna und Felix Mitterer an. Die Malerin Chryseldis Hofer-Mitterer lebt und arbeitet in Wildermieming und geh&ouml;rt zu den anerkannt gro&szlig;en Tiroler K&uuml;nstlern. Bis zum 1. M&auml;rz 2008 sind in der Kunstausstellung Werke der gesamten Schaffensperiode Chryseldis&lsquo; zu sehen. 	  	  </td>
