  <td colspan="3"><h2><a name="031f2a99fa0de1720"></a>Kleine, aber feine Galerie Spielmann</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Die kleine, aber feine Galerie Spielmann im Talkessel zwischen Mieminger Kette und Zugspitzmassiv, war an diesem Abend ein gro&szlig;er Gastgeber. Die Liste der namhaften Besucherinnen und Besucher ist lang, stellvertretend f&uuml;r alle, seien einige Namen genannt: Der Telfer Altb&uuml;rgermeister Helmut Kopp, der Maler und Literat Gerald Nitsche aus Landeck, die &bdquo;Wolkenfrau&ldquo; Britta Fugger, Literaturwissenschaftlerin aus Telfs, die Malerin und Journalistin Daniela Senn aus Nassereith, die Schauspielerin Monika Schletterer-Falbesoner aus Telfs, der Journalist und Kinderbuchautor Hubert Flattinger aus Mieming, die Steindrucker Anna-Liese und G&uuml;nther Stecher aus Wildermieming und aus dem gleichen Ort das K&uuml;nstlerduo Ursula und Dietmar Tiefengraber.</p>
</td>
