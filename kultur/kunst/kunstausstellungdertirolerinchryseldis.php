  <td colspan="3"><h2><a name="031f2a99fa0de0b1f"></a>Chryseldis-Arbeiten sind rar</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>F&uuml;r Alexandra und Herbert Spielmann ist dies erst die zweite Ausstellung ihrer noch jungen Galerie an der Ehrwalder Hauptstra&szlig;e 63 eine gro&szlig;e Auszeichnung und Herausforderung gleicherweise. Sie sagen, &bdquo;Wer ein Werk der K&uuml;nstlerin erstehen m&ouml;chte, der kann das nicht auf den g&auml;ngigen Marktpl&auml;tzen der Kunst. Keine Suchmaschine bietet sie an&ldquo;. Chryseldis mache sich rar und deshalb seien ihre Arbeiten so begehrt.</p>
</td>
