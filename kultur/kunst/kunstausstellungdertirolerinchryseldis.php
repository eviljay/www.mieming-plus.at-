  <td colspan="3"><h2><a name="031f2a99fa0ddb91e"></a>Chryseldis ist wie ihre Bilder</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>&bdquo;Chryseldis ist so wie ihre Bilder&ldquo;, sagt ihr Mann Felix Mitterer, der mit Tochter Anna eigens aus Irland anreiste. Ein Bild von Chryseldis sei unter Tausenden zu erkennen, denn sie habe ihre eigene, unverwechselbare Bildwelt erschaffen, so Mitterer. Schon fr&uuml;her gestand er, dass ihm Chryseldis Muse war: &bdquo;Vielleicht werden meine St&uuml;cke einmal etwas von ihrer Bedeutung verlieren, weil sie sehr zeitgebunden sind, die Bilder von Chryseldis aber werden als Extrakt davon immer weiterbestehen und ihre G&uuml;ltigkeit haben&ldquo;.</p>
</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
