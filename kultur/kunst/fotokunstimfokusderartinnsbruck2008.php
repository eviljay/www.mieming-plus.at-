  <tr>
  <td></td>
  <td colspan="3"><h2><a name="031f2a9a060c74003"></a>Internationale Messe f&uuml;r zeitgen&ouml;ssische Kunst</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Die Innsbrucker ART ist seit Anbeginn als Messe f&uuml;r Kunst-Einsteiger wie auch Sammler von zeitgen&ouml;ssischer internationaler Kunst konzipiert, wobei Messemacherin Johanna Penz seit letztem Jahr verst&auml;rkt daran arbeitet, &quot;dass neben dem traditionellen Tafelbild, das nach wie vor der Superseller jeder Kunstmesse ist, die Fotokunst wie auch die Neuen Medien noch st&auml;rker als bisher an Boden gewinnen. So wie sich das am internationalen Kunstmarkt etwa bei Auktionen wie auch bei anerkannten Sammlern schon seit einigen Jahren als ganz klarer Trend abzeichnet.&quot;<br><br>Penz sieht die Schwerpunktsetzung als Prozess, in den sowohl die Aussteller wie auch das Publikum miteinzubeziehen sind. &quot;Sehgewohnheiten und Kunstvorlieben &auml;ndern sich nicht von heute auf morgen.&quot; Trotzdem sei es immens wichtig, den Trends am internationalen Kunstmarkt Rechnung zu tragen. &quot;Daher werden wir in diesem Jahr in enger Zusammenarbeit mit unseren Stammausstellern, das Angebot an guter Fotokunst deutlich erh&ouml;hen.&quot;</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../../images/031f2a9a060cb9a07.jpg" target="_blank"><img src="../../images/031f2a9a060cb9a07_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>Johanna Penz </strong> Foto: Michael Rathmayr<br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="031f2a9a060cd2311"></a>Fokus auf Fotokunst</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Folgerichtig werde auch die traditionelle gro&szlig;e Sonderschau der ART Innsbruck, welche in diesem Jahr von der renommierten Innsbrucker Galerie Rhomberg ausgerichtet wird, ihren Fokus auf Fotokunst richten. Auch die von Land und Stadt gef&ouml;rderte Sonderschau &quot;Junge Kunst auf der ART Innsbruck&quot; bleibt ihrem Grundkonzept der vergangenen Jahre treu: mit &quot;Apfel und Birnen, weder Kohlrabi noch Kartoffel&quot; setzen Philipp Haselwanter und Roland Maurmair als Ausstellungsmacher wie K&uuml;nstler ein erfrischend subversives und unkonventionelles Gegen-Statement zum Kunstkaufmarkt, bei dem es so gut wie nie um den Enstehungsprozess, sondern einzig um das vermarktbare Ergebnis oder Produkt eines k&uuml;nstlerischen Prozesses geht.<br><br>Dazu die Ausstellungsmacher: &quot;Wir haben beschlossen, den Prozess der Kunstproduktion vom Atelier bis in den Ausstellungsraum weiterzuf&uuml;hren, unsere Produktion findet nicht nur zuhause statt, wir werden auch vorort individuell auf den Raum und das &ouml;rtliche Diapositiv eingehen und spontan im Raum interagieren.&quot;<br><br>Die 12. ART Innsbruck wird am Donnerstag, 7. Feber 2008 mit einer Preview und anschlie&szlig;enden &ouml;ffentlichen Soir&eacute;e er&ouml;ffnet. Die Messe dauert wieder vier Tage, von Freitag, 8. bis einschlie&szlig;lich Montag, 11. Feber.</td>
