  <td colspan="3"><h2><a name="031f2a9a060cc860d"></a>12. Auflage der ART in Messehalle 4</h2></td>
  <td></td>
  </tr>
      <tr>
    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td class="cell" align="left" valign="top">
		      	        Vom 8. bis 11. Feber 2008 findet in der Innsbrucker Messehalle 4 die mittlerweile zw&ouml;lfte Auflage der ART Innsbruck statt. Knapp sechzig Aussteller aus 10 L&auml;ndern haben sich zur 12. Innsbrucker ART vom 8. bis 11. Feber in der Innsbrucker Messehalle 4 bereits angemeldet. Damit ist die &auml;lteste und nach wie vor gr&ouml;&szlig;te Kunstmesse im Westen &Ouml;sterreichs laut Messemacherin Johanna Penz also schon so gut wie ausverkauft. &nbsp;</font>
	  	  </td>
    </tr>
</table><br><br>
</td>
    <td></td>
