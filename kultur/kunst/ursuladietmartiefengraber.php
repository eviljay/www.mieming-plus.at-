    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Bis zum 28. September 2008 war die Kunstausstellung &bdquo;Retrospektiven&ldquo; von Ursula und Dietmar Tiefengraber im Mesnerhaus/KunstWerkRaum in Mieming-Untermieming ge&ouml;ffnet. Eine viel beachtete Rede zur Er&ouml;ffnung hielt der Autor und Fernsehregisseur Eberhard Hauff, der mehr als zwei Jahrzehnte das M&uuml;nchner Filmfest leitete. &quot;Die Tiefengrabers sind Sinnsuchende. Das waren sie, als meine Frau und ich sie 1996 kennenlernten und das sind sie bis heute und werden es ihr Leben lang bleiben&quot;, so Hauff. In ihrer Ausstellung geht es Ursula und Dietmar Tiefengraber um H&ouml;hen und Tiefen des Lebens, um die Erfahrungen mit der Natur des Menschen. Die konzeptionelle Idee hatten Uschi und Dietmar Tiefengraber vor rund zwei Jahren. 	  	  </td>
