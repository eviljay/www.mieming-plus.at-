    <td></td>
    <td valign="top"><p>Sabeth Holland stammt aus Widnau, im St. Galler Rheintal und wuchs in einer K&uuml;nstlerfamilie auf. Ihre Vorliebe f&uuml;r das Anglophile beeinflussten ihr k&uuml;nstlerisches Denken und kommt in ihren Kunstwerken zum Ausdruck. Die Viefalt der Farben spielt in den Kunstwerken der 3fachen Mutter eine herausragende Rolle. M&auml;rchenhaft und orientalisch, wild und dynamisch - die K&uuml;nstlerin &uuml;berzeugt durch Experimentierfreude.</p>
