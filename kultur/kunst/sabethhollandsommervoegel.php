	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Lithographien und &Ouml;lbilder von der Schweizer K&uuml;nstlerin Sabeth Holland wurden am Samstag, dem 24. Mai 2008 in einer ersten Werkstatt- und Gartenvernissage bei Stecher &amp; Stecher in Affenhausen pr&auml;sentiert. Die drei Lithographien-Steindrucke &quot;Butterflies I, II und III&quot; wurden in limitierter Auflage (je 70 St&uuml;ck) von 37 Steinen gedruckt und mit Blattgoldauflagen noch zus&auml;tzlich veredelt. Bildformat: 320 x 510 mm, Papierformat: 500 x 700 mm. Preis pro Blatt: Euro 580,00 - die Serie (drei Lithographien): Euro 1.500,00. Zu den Lithographien werden auch einige &Ouml;lbilder der K&uuml;nstlerin ausgestellt. Sabeth Holland wohnt und wirkt seit 1983 in St.Gallen/Schweiz. Die K&uuml;nstlerin arbeitete seit November des vergangenen Jahres im Atelier der Steindruckerei Stecher &amp; Stecher. 	  	  </td>
