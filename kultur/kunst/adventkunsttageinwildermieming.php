            <tr bgcolor="#EFEFEF">
	                              <td class="cell" align="left" valign="top">
		      	        28.000 Euro f&uuml;r &quot;Frauen helfen Frauen&quot; und Tiroler Frauenhaus: Die Steindruckerei Stecher &amp; Stecher aus Wildermieming spendete die Erl&ouml;se aus dem Verkauf der Lithographien zum 5. Afra-Fest in voller H&ouml;he f&uuml;r einen guten Zweck. Mit der Scheck&uuml;bergabe am Samstag, dem 6. Dezember 2008 wurde auch die Kunstbar zu den Adventkunsttagen im Hause Stecher er&ouml;ffnet. Bis zum 31. Dezember werden G&auml;ste mit einem Afra-Cocktail, kreiert vom Cocktail-Weltmeister Angelo Ganner in der Steindruckerei begr&uuml;&szlig;t. Empfohlen werde nebenbei besondere Weihnachtsgeschenke - Lithographien von B&ouml;ck, Chryseldis, Danler, Flora, Hable, Holland, Ljubanovic, Savio, Stimpfl, Scherer, Tilly, Tomaselli oder Zauner. &nbsp;</font>
