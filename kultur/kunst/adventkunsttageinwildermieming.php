  <td colspan="3"><h2><a name="031f2a9b7210a2b5a"></a>Beitr&auml;ge f&uuml;r Menschen in Not</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Zum anderen ging es darum, einen Beitrag f&uuml;r all jene in unserer Gesellschaft zu leisten, mit denen es das Schicksal nicht so gut gemeint hat. Menschen, die dringend auf die Hilfe von au&szlig;en angewiesen sind. Die Stechers zelebrieren dies nun bereits zum 5. Mal. Anl&auml;sslich des 20-Jahr-Jubil&auml;ums der Steindrucker Stecher &amp; Stecher fand im Jahre 2004 das erste Fest zu Ehren der Heiligen Afra (.../um 304 den diokletianischen Christen-Verfolgungen zum Opfer gefallen) in Wildermieming statt.</td>
