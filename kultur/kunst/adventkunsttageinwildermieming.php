  <td colspan="3"><h2><a name="031f2a9b72104b257"></a>Adventfeier in der Kunstbar Stecher &amp; Stecher</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Zahlreiche Menschen waren der Einladung zur Adventfeier in die Kunstbar von Walter, Annaliese und G&uuml;nther Stecher in Wildermieming-Affenhausen gefolgt. Galt es doch, etwas ganz Besonderes zu zelebrieren. Zum einen das Beisammensein mit netten Menschen in einem Haus voll Kunst und Harmonie, das f&uuml;r jeden Besucher stets seine Pforten ge&ouml;ffnet hat und in dem jeder einfach &quot;ganz herzlich mit offenen Armen&quot; willkommen gehei&szlig;en wird.</td>
