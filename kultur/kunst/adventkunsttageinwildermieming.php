  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Dank der Initiative von Walter, Annaliese und G&uuml;nther Stecher sind bis dato insgesamt mehr als 130.000 Euro zusammengekommen, die in Not geratenen Frauen zur Verf&uuml;gung gestellt werden konnte. F&uuml;r die diesj&auml;hrige Jubil&auml;umslithographie zum Fest der Heiligen Afra haben die Stechers mit Chryseldis eine ganz besondere K&uuml;nstlerin gewinnen k&ouml;nnen. In ihren beiden Lithographien &quot;Lebensweg I und II&quot; spiegelt sich die Ver&auml;nderung wider, die uns in unseren jeweiligen Lebensabschnitten begleiten.</td>
