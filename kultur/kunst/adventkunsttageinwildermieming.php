  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Namhafte K&uuml;nstler wie August Stimpfl, Paul Flora und Herbert Danler haben bisher f&uuml;r die Gestaltung der Jubil&auml;umslithographien verantwortlich gezeichnet. Die Erl&ouml;se aus dem Verkauf der Lithographien zum Afra-Fest wurden seither in voller H&ouml;he der Initiative &quot;Frauen helfen Frauen&quot; und dem Tiroler Frauenhaus zur Verf&uuml;gung gestellt. &quot;Ich bin dem Herrgott dankbar, dass er uns die M&ouml;glichkeit gibt, anderen Menschen zu helfen&quot;, sagt Hausherr G&uuml;nther Stecher in seiner Er&ouml;ffnungslaudatio.</td>
