    <td valign="top"><p>Besonderes Geschenk f&uuml;r Paul Flora zum 85. Geburtstag, den der K&uuml;nstler am Freitag, dem 29. Juni 2007, feierte: Ein Asteroid, der seine Bahn zwischen Mars und Jupiter zieht, hat den Namen Paul Flora erhalten. Mieming Plus gratuliert zum Geburtstag, den Paul Flora an einem Ort verbringt, den er verst&auml;ndlicherweise nicht verraten m&ouml;chte. &quot;Ich bin nicht da, ich verschwinde&quot;, sagte der Jubilar, der gerade in der Steindruckerei Stecher &amp; Stecher in Wildermieming seine neueste Lithographie zeichnete.</p>
