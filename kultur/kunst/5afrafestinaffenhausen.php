  <td colspan="3"><h2><a name="031f2a9af712a92cf"></a>Lebensweg-Motive</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Die Erl&ouml;se aus dem Verkauf Lithographien zum Afrafest werden seither der Initiative &bdquo;Frauen helfen Frauen&ldquo; und dem Tiroler Frauenhaus in voller H&ouml;he gespendet. Insgesamt brachte die Aktion der Familie Stecher bislang ein Spendenvolumen von &uuml;ber 100.000,00 Euro auf. Afralithographien gab es bislang von August Stimpfl, Paul Flora und Herbert Danler. Die Jubil&auml;umslithographien zum 5. Afrafest wurden den Lebensweg-Motiven der f&uuml;nf Chryseldis-Fenster im Innsbrucker Trauungssaal im Goldenen Dachl entnommen.</p>
