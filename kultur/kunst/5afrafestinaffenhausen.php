	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Das 5. Afrafest der Steindruckerei Stecher &amp; Stecher in Wildermieming besuchten am Sonntag, dem 10. August 2008 bei hochsommerlichen Temperaturen &uuml;ber 300 G&auml;ste, darunter zahlreiche Prominente aus Politik, Kunst und Kultur. Heuer wurden die Jubil&auml;umslithographien &bdquo;Lebensweg I und II&ldquo; der K&uuml;nstlerin Chryseldis Hofer-Mitterer pr&auml;sentiert. Den Ehrenschutz f&uuml;r das diesj&auml;hrige Afrafest &uuml;bernahm die Innsbrucker B&uuml;rgermeisterin Hilde Zach. Zum Auftakt zelebrierte der Stamser Abt German Erd eine stimmungsvolle Festmesse vor der Afrakapelle in Affenhausen, musikalisch umrahmt von der Musikkapelle Wildermieming, unter der Leitung von Kapellmeister Alois Kranebitter. 	  	  </td>
