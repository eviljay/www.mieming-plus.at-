  <td colspan="3"><h2><a name="031f2a9aba0aa721e"></a>Hubert Flattinger Ausstellung in Mieming</h2></td>
  <td></td>
  </tr>
      <tr>
    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td class="cell" align="left" valign="top">
		      	        Unter dem Titel &quot;Meine Art&quot; pr&auml;sentiert der Tiroler Autor und Illustrator Hubert Flattinger ab 13. Juni im KunstWerkRaum Mieming neue Gem&auml;lde- und Bildgeschichten. Einen Teil, bzw. einen Raum der Galerie widmet Flattinger dabei jenen Aquarellgeschichten, mit denen er seit mittlerweile vierzehn Jahren die Kinderseite der Tiroler Tageszeitung illustriert. Da gibt es ein Wiedersehen mit schr&auml;gen Typen aus dem Tier- und Menschenreich, &quot;Erlebens-Gutscheine f&uuml;r alle F&auml;lle&quot; und Originale aus der Zitatenreihe &quot;Spruchreif&quot; zu sehen - Bilder, die zum Teil im Herbst 2004 in der Fine Art Gallery in New Orleans unter dem Titel &quot;Mai Pretty Gut Drawings&quot; zu sehen waren.&nbsp;</font>
	  	  </td>
    </tr>
</table><br><br>
</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="031f2a9abd122cd59"></a>Vor Ausstellungsbeginn wurde noch gemalt</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Trotz oder vielleicht wegen der Fussball-Europameisterschaft war die Vernissage zur Ausstellungser&ouml;ffnung am Freitag, dem 14. Juni 2008 im KunstwerkRaum/Mesnerhaus mehr als gut besucht. &bdquo;Man mi&szlig;t es an den Gesichtern, die man hofft zu sehen&ldquo;: es kamen &bdquo;enge Freunde, vertraute Gesichter und neue Gesichter &ndash; mehr als sympathisch &ndash; ich werte das als Kompliment&ldquo;, sagte Hubert Flattinger, der sein letztes Bild zur Ausstellung noch am Vortag fertig malte und dessen Frau Ines zwei Tage lang vor der Ausstellung noch letzte Rahmen tischlerte.</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../../images/031f2a9abd1257067.jpg" target="_blank"><img src="../../images/031f2a9abd1257067_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>Hubert + Ines Flattinger </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="031f2a9abd123155a"></a>Zeichnungen, Acrylwerke, &Ouml;lbilder</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Zu sehen sind Illustrationen aus seinen B&uuml;chern und der &bdquo;tete&ldquo;-Kinderseite, meist Bleistiftzeichnungen und Aquarelle seiner mit Worten zu Papier gebrachten Erz&auml;hlungen. &bdquo;Mit lichtechten Farben und ein bi&szlig;chen Deckwei&szlig; dazu gemischt&ldquo;, so Hubert Flattinger. Aber den Gro&szlig;teil seiner &bdquo;Art&ldquo; bestreiten Acrylwerke und &Ouml;lbilder auf Leinwand. &bdquo;Viele Bilder entstehen in meinem Kopf beim Schreiben&ldquo; sagt der Journalist, Autor und Maler, der von sich selbst sagt, selten zufrieden zu sein.</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../../images/031f2a9abd125d869.jpg" target="_blank"><img src="../../images/031f2a9abd125d869_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>&quot;Wasch dich mal wieder&quot; </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="031f2a9abd123405b"></a>Die meisten Bilder sind autobiografisch</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Mit seiner Ausstellung in Mieming kann er durchaus zufrieden sein. Die meisten Bilder sind autobiografisch. Seine Selbstportraits, z.B. oder seine grunds&auml;tzliche Art, menschliche Charaktere in Form ihrer Lieblingskleidungsst&uuml;cke zu portraitieren. &bdquo;An der Kleidung erkennst Du Menschen&ldquo; und diese Anschauung teilt Flattinger wohl mit vielen seiner &quot;Art&quot;. Die Idee hatte er auf einer Reise mit seiner Frau Ines nach Dublin. Wir sahen in einem Gesch&auml;ft das Hemd von Neil Young, brachten es mit nach Tirol und die Idee zu diesen Portraits war geboren.</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../../images/031f2a9abd126586b.jpg" target="_blank"><img src="../../images/031f2a9abd126586b_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>Neil Young's Hemd </strong> <br></p>
	</td>
