  <td colspan="3"><h2><a name="031f2a9abd1235d5c"></a>Musik und Malen</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Hubert Flattinger wurde mit der Musik der Beatles und der kanadischen Singer-Song-Writer Neil Young und Joni Mitchell gro&szlig;. &quot;Das waren Freunde&quot; und w&auml;hrend er das erz&auml;hlt, klingt &bdquo;Heart of Gold&ldquo; aus den Fenstern des Mesnerhauses. Melancholisch und in die Tiefe gehend. Mit der Musik sei es schlie&szlig;lich, wie mit dem Malen, &bdquo;K&uuml;nstler bringen ihre Geschichten so oder so zum Ausdruck&ldquo;. Am liebsten h&ouml;re er die Beatles, so Flattinger. Alles von den Beatles, aber besonders gerne &bdquo;She&rsquo;s leaving home&ldquo; aus &bdquo;Sgt. Pepper&rsquo;s Lonely Hearts Club Band&ldquo;.</td>
