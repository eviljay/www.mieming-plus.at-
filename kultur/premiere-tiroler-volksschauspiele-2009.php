    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Am Samstag, dem 25. Juli 2009 starteten um 20 Uhr im Kranewitter Stadl in Telfs die Tiroler Volksschauspiele - und zollen mit der Urauff&uuml;hrung von Felix Mitterers &bdquo;1809 - Mein bestes Jahr&quot; dem Gedenkjahr Tribut. Im neuen St&uuml;ck von Felix Mitterer geht es um den &bdquo;Metzger Klaus&ldquo;, eine historische Telfer Gestalt aus dem Jahr 1809, eine etwas schr&auml;ge und widerborstige Figur. Der Telfer Historiker Dr. Stefan Dietrich hat schon l&auml;nger &uuml;ber den Metzger Klaus geforscht und Felix Mitterer sein Quellenmaterial zur Verf&uuml;gung gestellt. Dieser hat dann f&uuml;r die heurigen Tiroler Volksschauspiele ein Einpersonen-St&uuml;ck geschrieben, bei dem der Metzger Klaus im Wirtshaus auftritt und von seinen Taten erz&auml;hlt. Das St&uuml;ck wird am Mittwoch, dem 12. August 2009 im Gasthof Gerhardhof in Wildermieming aufgef&uuml;hrt. 	  	  </td>
    </tr>
