		      	        Zum 1. Mal in seinem sehr jungen Leben besuchte Luis am Sonntag, dem 17. Mai 2009 mit Mama Iris und Papa Gregor Bieler aus Mieming die Simmering Alm. H&uuml;ttenwirt Thomas Soraperra freute sich riesig &uuml;ber den sicherlich j&uuml;ngsten Gast an diesem Sonntag auf der Alm. Der kleine Luis ist am 31. J&auml;nner 2009 in Innsbruck per Kaiserschnitt mit gesunden und runden 3.800 Gramm auf die Welt gepurzelt. Seinem grossen Namensvetter (Luis Trenker) alle Ehre machend, hatte Luis &uuml;berhaupt keine Probleme mit der Hitze und dem H&ouml;henunterschied. W&auml;hrend des gesamten zweist&uuml;ndigen Aufstiegs &uuml;ber den Harnersteig schlief Luis seelig im Wickeltuch am Bauch vom Papa. Ganz im Gegensatz zu Mama Iris ben&ouml;tigte der Kleine w&auml;hrend des Aufstiegs nicht einmal etwas zu Trinken. Dies &auml;nderte sich allerdings nach dem Erreichen der Simmering-Alm. Nach einem Kleider- und Windelwechsel (Papa kam doch etwas ins Schwitzen) verzichtete der Kleine auf den obligatorischen Begr&uuml;ssungsschnaps und ging gleich zur Milchaufnahme bei Mama &uuml;ber. Der Abstieg erfolgte &uuml;ber den Alpsteig, nachdem Papa Gregor seinen Fl&uuml;ssigkeitsverlust mir drei Radlern kompensiert hatte, war das Vertrauen der Mutter in dessen Trittsicherheit auf Null gesunken, so dass der kleine Luis den Heimweg schlafend am Bauch der Mama antrat. &quot;Luis m&ouml;chte &uuml;brigens schon bald seiner ersten pers&ouml;nlichen Einladung folgen: Am Sonntag, dem 14. Juni will er zur Bergmesse auf die Simmering Alm. F&uuml;r alle, die noch nie bei einer Bergmesse auf der Simmering Alm waren, empfehlen wir unsere Berichterstattung vom vergangen Jahr, vielleicht sehen wir uns heuer? [ <a href="../../kultur/brauchtum/bergmesseaufdersimmeringalm.php">weiter</a> ] 	  	  </td>
