  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Alle bisherigen Vorstellungen waren ausverkauft und deshalb geht es weiter: Ein Ur-Tiroler Schlagers&auml;nger und ein zuagroaster Vorarlberger Blues-Pianist treffen sich zuf&auml;llig auf der B&uuml;hne und starten dann eine kleine Weltreise in Sachen Musik. Die Termine dieses Liederabend-Programms 'Doppelbuchung' finden allesamt auf der L&ouml;wen-B&uuml;hne in Mieming statt, und zwar am Donnerstag, 29. Mai 2008.</td>
