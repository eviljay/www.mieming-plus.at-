  <tr>
  <td></td>
  <td colspan="3"><h2><a name="031f2a99ec1082099"></a>Pfarrer Herbert Traxl segnete das neue Haus</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Die Gemeindevertreter, allen voran Bgm. Dr. Siegfried Gapp, Kulturreferent Ulrich Stern und Bauausschussobmann Manfred Maurer leiteten die notwendigen Schritte zum Ausbau ein. Im Herbst 2006 wurde beim alten Probelokal an der Hauptschule mit den Abbrucharbeiten begonnen. &Uuml;bergangsweise probte die Musikkapelle Mieming im Keller des Sozialzentrums. Nur nach einem Jahr konnte das neue Ausbildungszentrum von Pfarrer Mag. Herbert Traxl gesegnet werden.</p>
</td>
