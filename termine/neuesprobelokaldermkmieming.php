  <td></td>
  <td colspan="3"><h2><a name="031f2a99ec0fc8f88"></a>Neues Haus der Musik</h2></td>
  <td></td>
  </tr>
      <tr>
    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Mieming hat ein neues &bdquo;Haus der Musik&ldquo;. Am Sonntag, dem 18. November 2007 wurde es um 10.30 Uhr feierlich er&ouml;ffnet. Vor der offiziellen &Uuml;bergabe durch die Gemeinde Mieming fand mit der Beteiligung der Musikkapelle Mieming eine C&auml;cilienmesse in der Pfarrkirche Untermieming statt. Nach den Ansprachen von MK-Obmann Hannes Spielmann, Bezirkshauptmann Stv. Mag. Andreas Nagele, B&uuml;rgermeister Dr. Siegfried Gapp und der Landtagsabgeordneten Claudia Hirn segnete Pfarrer Mag. Herbert Traxl das Probelokal. Danach stand das Haus der &Ouml;ffentlichkeit zur Besichtigung offen. 	  	  </td>
    </tr>
</table><br><br>
</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="031f2a99ec0fddc8b"></a>Gemeinde setzte deutliche Signale</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>MK-Obmann Hannes Spielmann begr&uuml;&szlig;te alle Ehreng&auml;ste und bedankte sich bei den F&ouml;rderern des neuen Musikhauses, besonders aber bei der Gemeinde Mieming, die mit ihrer Entscheidung zu diesem Projekt zwei deutliche Signale gesetzt habe: Einerseits ein klares Bekenntnis zum Erhalt und Ausbau des Vereinswesens der Gemeinde und andererseits zur sozialen Einbeziehung der Jugend in das Gemeindeleben.</p>
</td>
    <td><img src="../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../images/031f2a99ec0fdca89.jpg" target="_blank"><img src="../images/031f2a99ec0fdca89_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>Hannes Spielmann </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="031f2a99ec100b990"></a>Investition in die Zukunft</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Die notwendige Infrastruktur habe zwar Geld gekostet, so Spielmann, aber &bdquo;die Fr&uuml;chte dieser Investitionen k&ouml;nnen noch in weiter Zukunft geerntet werden&ldquo;. &bdquo;Wir Vereine danken der Gemeinde f&uuml;r ihre Unterst&uuml;tzung&ldquo;, sagte Hannes Spielmann mit Blick auf B&uuml;rgermeister Dr. Siegfried Gapp. Der gab das Kompliment in seiner Ansprache zur&uuml;ck an die Vereinsmitglieder der Musikkapelle, die Mieminger S&auml;nger und die Verantwortlichen Musikschule, &bdquo;die mit ihrer ehrenamtlichen Arbeit einen nicht unerheblichen Beitrag zum Gelingen beigetragen haben&ldquo;.</p>
</td>
    <td><img src="../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../images/031f2a99ec100a08e.jpg" target="_blank"><img src="../images/031f2a99ec100a08e_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>B&uuml;rgermeister Dr. Siegfried Gapp </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="031f2a99ec101d493"></a>Akute Platzprobleme zwangen zum Neubau</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Das Haus der Musik bietet Prober&auml;ume f&uuml;r die Musikkapelle Mieming, die Mieminger S&auml;nger und die Musikschule. Die akuten Platzprobleme der Musikkapelle machten den Neubau n&ouml;tig. Steigende Mitgliederzahlen, erheblicher Raumbedarf f&uuml;r die Ger&auml;tschaften, Notenarchive, die sanit&auml;ren Verh&auml;ltnisse und nicht zuletzt ein nicht vorhandener Aufenthaltsbereich waren un&uuml;bersehbare Defizite.</p>
</td>
    <td><img src="../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../images/031f2a99ec101c291.jpg" target="_blank"><img src="../images/031f2a99ec101c291_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>Musikkapelle Mieming </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="031f2a99ec104e796"></a>Engagement f&uuml;r das Musikvereinswesens</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Die Landtagsabgeordnete Claudia Hirn gratulierte im Namen der Landesregierung und lobte das Mieminger Engagement f&uuml;r das Musikvereinswesen: &bdquo;Das ist gut investitiertes Geld und ich bin beeindruckt, was Sie daraus gemacht haben&ldquo;. Bezirkshauptmann-Stellvertreter Mag. Andreas Nagele sagte, die Gemeinde Mieming unter ihrem B&uuml;rgermeister Dr. Siegfried Gapp, sei bekannt f&uuml;r ihre soziale und nachhaltige Gemeindepolitik.</p>
</td>
    <td><img src="../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../images/031f2a99ec104cb94.jpg" target="_blank"><img src="../images/031f2a99ec104cb94_150.jpg" width="150" height="112" alt="" border="0"><br></a>
