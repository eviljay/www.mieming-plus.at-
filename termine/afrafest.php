  <td colspan="3"><h2><a name="224263995f0beed1e"></a>Afrafest mit kleiner, aber feiner Tradition</h2></td>
  <td></td>
  </tr>
      <tr>
    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td width="33%"  class="cell" align="left" valign="top">
		      	        Zum 1. Afrafest im August 2004, unter dem Ehrenschutz von Di&ouml;zesanbischof Dr. Manfred Scheuer und Luise van Staa, gab es zwei Anl&auml;sse: 1.700 Jahre Hl. Afra, der vermutlichen Namensgeberin von Affenhausen (Afrahausen) und der Schutzpatronin der Afrakapelle in Wildermieming-Affenhausen sowie dem 20j&auml;hrigen Betriebsjubil&auml;um der Steindruckerei Stecher &amp; Stecher. Die Heilige Afra (Schutzpatronin der Fuhrleute) setzte sich zu Lebzeiten f&uuml;r benachteiligte Frauen ein, deshalb wurde der Reinerl&ouml;s von 28.600 Euro des 1. Afrafestes der Initiative &bdquo;Frauen helfen Frauen&ldquo; und dem Tiroler Frauenhaus &uuml;bergeben und August Stimpfl zeichnete die Jubil&auml;umslithographie &bdquo;Das Weib&ldquo;. Es dauerte ein Jahr, bis das Spendengeld &uuml;bergeben werden konnte, deshalb wurde das 2. Afrafest 2005 etwas 	  	  </td>
                          <td width="33%"  class="cell" align="left" valign="top">
		      	        &nbsp;	  	  </td>
                          <td width="34%"  class="cell" align="left" valign="top">
		      	        kleiner gestaltet und diente der Scheck&uuml;bergabe. Das Geld f&uuml;r die Fraueninitiativen nahm stellvertretend LA Anneliese Junker entgegen. Das urspr&uuml;nglich nur zum Jubil&auml;um geplante Afrafest war ein solcher Erfolg, dass die Familie Stecher beschloss, es k&uuml;nftig j&auml;hrlich im August zu veranstalten. 2006 folgte das 3. Afrafest mit einer Festlithographie von Paul Flora (&bdquo;Harlekin mit zwei B&auml;llen&ldquo;) und heuer wird am 11. August das inzwischen 4. Afrafest gefeiert. Die diesj&auml;hrige Benefiz-Lithographie ist von Herbert Danler, &bdquo;<a href="../images/afrafest.danler.lithographie.jpg">Stifthof am Sonnenberg</a>
&ldquo; - sie wurde in einer limitierten Auflage von 160 St&uuml;ck auf Stein gedruckt.	  	  </td>
