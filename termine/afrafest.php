  <td colspan="3"><h2><a name="224263997b0925e17"></a>Danler signiert „Stifthof am Sonnenberg“</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>F&uuml;r das 4. Afra-Fest gestaltete der Maler Prof. Herbert Danler die Benefiz-Lithographie &bdquo;Stifthof am Sonnenberg&ldquo; . Das Motiv wurde von 7 Steinen ( 7 Farben ) in limitierter Auflage von 160 St&uuml;ck auf 310 gr B&uuml;ttenkarton gedruckt. Papierformat 400 x 350 mm, Bildformat 290 x 241 mm. Die Bl&auml;tter wurden vom K&uuml;nstler numeriert und signiert. Die Lithographie wird beim Afra-Fest pr&auml;sentiert, kann aber auch schon jetzt bei Stecher &amp; Stecher reserviert werden. Preis pro Blatt Euro 330,-- mit Zertifikat.</p>
