<td colspan="3"><h1>130 Jahre Freiwillige Feuerwehr Mieming</h1></td>
<td></td>
</tr>

  <tr>
  <td></td>
  <td colspan="3"><h2><a name="224263992f0956f0d"></a>Gro&szlig;es Programm beim Tag der offenen T&uuml;r</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Mehrere hundert G&auml;ste feierten mit der Freiwilligen Feuerwehr Mieming am Samstag, dem 16. Juni bei &uuml;berwiegendem Sonnenschein den Tag der offenen T&uuml;r. Anl&auml;sslich ihres 130j&auml;hrigen Jubil&auml;ums pr&auml;sentierte sich die Feuerwehr mit einem spannenden Programm unter dem Motto &bdquo;L&ouml;schen &ndash; einst und jetzt&ldquo;. Aktiv mit dabei u.a. die Feuerwehren aus Innsbruck, Obsteig, L&auml;ngenfeld, Wildermieming und M&ouml;tz. Unter den G&auml;sten u.a. Ehrenkommandant Paul Larcher, Bgm. Dr. Siegfried Gapp, Abschnittskommandant Roland Markert, die Kommandanten der Feuerwehren Wildermieming, Stams, M&ouml;tz, &Ouml;tzal Bahnhof und Wenns, Weltcup-Skispringer Martin H&ouml;llwarth.</p>
</td>
    <td><img src="../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../images/2242639952088bd65.jpg" target="_blank"><img src="../images/2242639952088bd65_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>L&ouml;schvorf&uuml;hrungen </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="22426399520895467"></a>Feuerwehr zeigte sich leistungsstark</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Alle Feuerwehrleute waren im Einsatz und pr&auml;sentierten Ausschnitte ihrer Leistungen. Feuerwehrkommandant Thomas Perkhofer informierte und moderierte, oft unter gro&szlig;em Beifall des interessierten Publikums. Es gab viel zu sehen, neues und altes Ger&auml;t, Feuerwehrautos aus unterschiedlichen Epochen oder ein rund 200 Jahre altes L&ouml;schger&auml;t, das sogar zum Einsatz kam. Ein Fest auch f&uuml;r die Kinder, die sich &uuml;ber die Hupfburg oder Feuerwehrmaskottchen &quot;Flori&quot; freuten oder die Fahrt bei Blaulicht im L&ouml;schfahrzeug.</p>
</td>
    <td><img src="../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../images/2242639952089ec68.jpg" target="_blank"><img src="../images/2242639952089ec68_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>Thomas Perkhofer </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="224263995208c1d6c"></a>Viel Feuer und Rauch</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Kompliment an alle Akteure: Die Freiwillige Feuerwehr Mieming war bestens vorbereitet. Es gab viel Feuer und Rauch bei den beeindruckenden L&ouml;schvorf&uuml;hrungen. Das Publikum - auch die Kinder - wurde aktiv in die Vorf&uuml;hrungen eingebunden. Wer wei&szlig; schon was ein Fettbrand ist oder wie Fl&uuml;ssigkeitsbr&auml;nde entstehen? Wer den Feuerwehrleuten gestern zuschaute und zuh&ouml;rte, ist auf Alltagsunf&auml;lle vorbereitet.</p>
</td>
    <td><img src="../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../images/224263995208bfd6a.jpg" target="_blank"><img src="../images/224263995208bfd6a_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>Fettbrand </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="224263995208f996f"></a>Die ersten Minuten sind entscheidend</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Altkommandant Paul Larcher erz&auml;hlte Mieming Plus, was er in zwanzig Jahren leitender Feuerwehrarbeit erlebt hat: &quot;Da wurde noch das Wasser aus dem Bach hochgepumpt, heute geht ja alles viel, viel schneller&quot;. Entscheidend seien die ersten 15 Minuten bei einem L&ouml;scheinsatz, danach k&ouml;nnten die Feurwehrleute weitgehend nur noch Schaden begrenzen. Paul Larcher war von 1963 bis 1983 Kommandant der Freiwilligen Feuerwehr in Mieming.</p>
</td>
    <td><img src="../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../images/224263995208f7d6d.jpg" target="_blank"><img src="../images/224263995208f7d6d_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>Alkommandant Paul Larcher </strong> (im Gespr&auml;ch mit Beppi Widauer)<br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="22426399520925072"></a>1877 war das Gr&uuml;ndungsjahr</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Seit den Tagen des ersten Kommandanten, Heinrich Larcher - von 1907 bis 1920 - hat sich viel ver&auml;ndert sagt Thomas Perkhofer, Chef der Mieminger Wehr seit 1998. &quot;1877 war das Gr&uuml;ndungsjahr, es ging los mit einem 65 schuhlangen Hanfschlauch und die erste Spritze kostete damals 1000 Gulden&quot;. Die Freiwillige Feuerwehr war damals f&uuml;r die Plateaugemeinden Mieming, Wildermieming und Obsteig zust&auml;ndig.</p>
</td>
    <td><img src="../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../images/22426399520923470.jpg" target="_blank"><img src="../images/22426399520923470_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>Fl&uuml;ssigkeitsbrand </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="2242639952094c678"></a>Ein voller Erfolg</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Der Tag der offenen T&uuml;r war ein voller Erfolg f&uuml;r die engagierte Mieminger Feuwehr und das Publikum zeigte sich vielfach begeistert von den Vorf&uuml;hrungen, besonders auch von den Nostalgie-L&ouml;sch&uuml;bungen. Wer wollte, konnte beim Zielspritzen mitmachen, die alten Handpumpen ausprobieren oder beim L&ouml;schen Hemmungen abbauen. Am abend spielte die Band Herzschrittmacher und damit fand der Tag der offenen T&uuml;r einen gem&uuml;tlichen Ausklang in der Firefighterbar.</p>
</td>
    <td><img src="../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../images/2242639952094a776.jpg" target="_blank"><img src="../images/2242639952094a776_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>Begeistertes Publikum </strong> <br></p>
	</td>
