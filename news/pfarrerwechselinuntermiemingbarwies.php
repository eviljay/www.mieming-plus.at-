  <td colspan="3"><h2><a name="031f2a9a950c4bd3c"></a>Pfarrer Herbert Traxl verl&auml;sst Mieming</h2></td>
  <td></td>
  </tr>
      <tr>
    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td class="cell" align="left" valign="top">
		      	        Pfarrer Mag. Lic. Herbert Traxl wird am 7. Juli 2008, nach der Isidori-Prozession, aufgrund eines Pfarrerwechsels die Pfarrgemeinden Mieming verlassen. Pfarrer Traxl wechselt zur Pfarre Zams. Pater Andreas Rolli, zur Zeit Pfarrer in Obsteig, wird die Pfarren Mieming und Barwies &uuml;bernehmen. Lesen Sie dazu einen Beitrag von Georg Edlmair, Obmann des Pfarrgemeinderates Barwies, erschienen in der Mieminger Dorfzeitung, Nr. 3, vom 17. April 2008: &nbsp;</font>
	  	  </td>
    </tr>
</table><br><br>
</td>
