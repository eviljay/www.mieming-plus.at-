  <td></td>
  <td colspan="3"><h2><a name="031f2a9a950c4983b"></a>Verlautbarung in der Sonntagsmesse</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Am Sonntag, dem 6. April 2008, wurde durch Herrn Mag. Franz Haidegger, amtsf&uuml;hrender Direktor im Generalvikariat und Leiter des Arbeitsbereiches Seelsorger&auml;ume der Di&ouml;zese Innsbruck, in der Sonntagsmesse zu Untermieming und Barwies verlautbart, dass uns Mag. Lic. Herbert Traxl, Pfarrer zu Mieming, am 7.7.2008 - montags nach der Isidoriprozession - aufgrund eines Pfarrerwechsels verlassen wird.</td>
    <td><img src="../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
