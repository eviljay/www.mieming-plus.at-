  <td></td>
  <td colspan="3"><h2><a name="031f2a9a950c97d41"></a>Pfarrer Traxl geht nach Zams</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Zuerst, hat mir Pfarrer Herbert gesagt, wird er seinen wohlverdienten Urlaub im heimatlichen Bauernhof verbringen. Dann folgt ein nur halbes &quot;Sabbatjahr&quot; in einer monastischen (=kl&ouml;sterlichen) Gemeinschaft in Piemont (Italien), um sich auf die neuen Aufgaben vorzubereiten, die auf ihn warten in der Pfarre Zams und des noch aufzubauenden Spiritualit&auml;tszentrums auf der Kronburg.</td>
    <td><img src="../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../images/031f2a9a950c9723f.jpg" target="_blank"><img src="../images/031f2a9a950c9723f_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>Messe in Untermieming </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="031f2a9a950ca6a44"></a>Pater Andreas Rolli &uuml;bernimmt im September</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Pater Anreas Rolli, derzeit Pfarrer in Obsteig, wird die Pfarren Mieming und Barwies ab September &uuml;bernehmen. In der Zeit dazwischen - Juli bis September - werden Aushilfen von der Seelsorgamtsleitung der Di&ouml;zese Innsbruck in Absprache mit unserer profunden Pfarramtsleiterin Astrid Hesche organisiert, die die beiden Pfarren betreuen werden.</td>
    <td><img src="../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
	    	<td valign="top"><a href="../images/031f2a9a950ca5b42.jpg" target="_blank"><img src="../images/031f2a9a950ca5b42_150.jpg" width="150" height="112" alt="" border="0"><br></a>
			<p class="illu"><strong>Pater Andreas Rolli </strong> <br></p>
	</td>
    <td></td>
    </tr>
    
  <tr>
  <td></td>
  <td colspan="3"><h2><a name="031f2a9a950cb1847"></a>In Mieming vieles zuwege gebracht</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Mit einem weinenden und einem lachenden Auge nur lassen wir ihn gehen, unseren Pfarrer Herbert, der in seiner Mieminger Zeit vieles zuwege brachte - doch davon sp&auml;ter..., und so sprechen wir unser &quot;fiat mihi&quot;. Wir hoffen, dass er die Zeit am Plateau in guter Erinnerung halten wird - dem Foto nach hat er sich vom zum gro&szlig;en (Pfarr-)Gemeindesegner entwickelt (zuerst dachte ich Dirigenten, weil's ein Foto der Einweihung des Probelokals er MK ist)..., ja - priesterlicher Segen ist allzeit vonn&ouml;ten.</td>
    <td><img src="../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
