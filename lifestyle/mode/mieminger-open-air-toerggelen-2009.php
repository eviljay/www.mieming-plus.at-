  <td colspan="3"><h2><a name="8627589cca0a39709"></a>Open-Air-T&ouml;rggelen war Schau der Superlative</h2></td>
  <td></td>
  </tr>
      <tr>
    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Mieming h&auml;lt Schritt mit den gro&szlig;en Mode-Metropolen Tirols: Das inzwischen 3. Open-Air-T&ouml;rggelen war eine Mode-Unterhaltungsshow der Superlative. Rund 700 Besucherinnen und Besucher kamen bei gef&uuml;hlten 5 Grad Plus am Sonntag-Nachmittag, dem 15. November 2009 auf den Gemeindeplatz nach Mieming. &bdquo;Eine Modenschau mit Herzblut&ldquo;, sagt die Initiatorin, Marie-Luise Maurer. Elf Models, neun Kinder und acht Jugendliche &ndash; allesamt ambitionierte Amateure und &uuml;berwiegend aus Mieming stammend &ndash; gestalteten unter der choreographischen Leitung von Claudia Girtler eine unterhaltsame und leidenschaftliche Modenschau, professionell moderiert von Hubert Trenkwalder. Der besondere Clou war heuer eine Video-Wand, die das ganze B&uuml;hnengeschehen auch f&uuml;r jene sichtbar machte, die nicht die besten Stehpl&auml;tze hatten. 	  	  </td>
