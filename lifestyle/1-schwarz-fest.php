  <td colspan="3"><h2><a name="8627589c470d23cb3"></a>Slacklining im Hotelgarten</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>F&uuml;r die aktiven Teilnehmer der zehn Programmangebote gab es jeweils eine Rose. Wer am Abend die meisten Rosen vorweisen konnte, wurde Schwarz-K&ouml;nig, -k&ouml;nigin, -prinz und &ndash;prinzessin. Beim Slacklining im Hotelgarten wurde das Gleichgewichtsempfinden der Mutigsten herausgefordert. Slacken ist eine Trendsportart, &auml;hnlich dem Seiltanzen, bei der man auf einem Gurtband balanciert. Die vor allem von Skatern gesch&auml;tzte Sportart, entwickelte sich Anfang der 80er Jahre.</p>
</td>
