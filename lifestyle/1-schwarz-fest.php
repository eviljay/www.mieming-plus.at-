  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Martha Pirktl &ouml;ffnete f&uuml;r die Schwarz-Fest-G&auml;ste ihr Kr&auml;utergartl. &bdquo;Martha&rsquo;s Kr&auml;utergartl&ldquo; wurde 1994 angelegt und liefert der hauseigenen K&uuml;che t&auml;glich gartenfrische Kr&auml;uter. &bdquo;Obwohl wir inzwischen genug Kr&auml;uter haben&ldquo;, so Martha Pirktl im Gespr&auml;ch mit Mieming-Plus, &bdquo;m&ouml;chten wir das Kr&auml;utergartl wieder mal erweitern&ldquo;. K&uuml;chenchef Mario Walch pr&auml;sentierte am Kr&auml;uterb&uuml;ffet 27 unterschiedliche Kr&auml;uter, einen Kr&auml;uteraufstrich und eine kleine Kr&auml;uterkunde.</p>
</td>
