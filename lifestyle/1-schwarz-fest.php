  <td colspan="3"><h2><a name="8627589c470a669a9"></a>Mittsommer-Silvester im Alpenresort Schwarz</h2></td>
  <td></td>
  </tr>
      <tr>
    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Eine Woche lang erlebten die G&auml;ste im Alpenresort Schwarz in Mieming eine Mittsommerwoche mit reichhaltigem Programmangebot. H&ouml;hepunkt war in diesem Rahmen das 1. Schwarz-Fest, am Freitag, dem 10. Juli 2009, dass am Abend mit einem gro&szlig;en Klangwolken-Feuerwerk und der Inthronisierung der &bdquo;Schwarz-Mittsommer-K&ouml;nigsfamilie&ldquo; - seinen Abschluss fand. Das Schwarz-Fest startete f&uuml;r Fr&uuml;haufsteher mit einer Sonnenaufgangswanderung zur St&ouml;ttlalm. Ende Juni wurden nach halbj&auml;hriger Bauphase die &bdquo;Schwarz Wasserwelten&ldquo; mit sieben Pools und einem Naturerlebnis- Badesee er&ouml;ffnet. Schwarz-Juniorchef Thomas Pirktl: &bdquo;Das Mittsommernachstfest im Schwarz soll fester Bestandteil unserer Programmangebote werden&ldquo;. 	  	  </td>
    </tr>
</table><br><br>
</td>
    <td></td>
    </tr>
    
