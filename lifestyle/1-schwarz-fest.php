  <td colspan="3"><h2><a name="8627589c470ce28b0"></a>Berglerfr&uuml;hst&uuml;ck, Sommerspass und Abend-Gala</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Auf der St&ouml;ttlalm servierte K&uuml;chenchef Mario Walch kurz nach 6 Uhr ein z&uuml;nftiges Berglerfr&uuml;hst&uuml;ck. Das weitere Tagesangebot deckte vieles ab, was das &bdquo;Schwarz&ldquo; zu bieten hat: u.a. Yoga, Aquagymastik, ein Tennisturnier, Slackline im Hotelgarten, Badesee-Abenteuer, ein B&uuml;ffet im Kr&auml;utergarten, Gourmet-Verkostungen an der Bar im neuen Poolhaus der Schwarz-Wasserwelten mit Sauna-Gang und am Abend trafen sich die G&auml;ste zum Galamen&uuml; und Tanz mit der City-Combo.</p>
</td>
