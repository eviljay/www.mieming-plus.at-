    <td colspan="3"><p><b>Die Regierung ist sich einig, grunds&auml;tzlich gilt ein Rauchverbot in der Gastronomie. Je nach Gr&ouml;&szlig;e des Lokals wird es unterschiedliche Regelungen geben.<br></b><br>&quot;Mit der vorliegenden Punktation ist eine gute &ouml;sterreichische L&ouml;sung erzielt worden&quot;, zeigte sich Gesundheitsministerin Andrea Kdolsky am Mittwoch mit der Einigung zu einer Novelle des Tabakgesetzes zufrieden. Grunds&auml;tzlich gilt demnach ein Rauchverbot in der Gastronomie, der Hauptraum einer Gastst&auml;tte muss k&uuml;nftig ein Nichtraucher-Raum sein. Besteht ein Lokal nur aus einem Raum unter 50 Quadratmeter, so kann der Wirt entscheiden, ob er es als Raucher- oder Nichtraucher-Lokal f&uuml;hrt. Bundeskanzler Alfred Gusenbauer sprach von einem &quot;guten Schritt in die richtige Richtung&quot;<br><br>Bis 80 Quadratmeter muss der Gastronom nachweisen, ob eine r&auml;umliche Trennung &quot;aus rechtlichen Gr&uuml;nden im Rahmen der Betriebsanlage&quot; m&ouml;glich ist. Wenn dies nicht m&ouml;glich ist, besteht weiter die Wahlm&ouml;glichkeit. Ist es schon m&ouml;glich, dann muss der gr&ouml;&szlig;ere Raum zum Nichtraucher-Raum werden. Letzteres gilt auch f&uuml;r alle gr&ouml;&szlig;eren Lokale, auch hier muss der gr&ouml;&szlig;ere Raum der Nichtraucher-Raum sein. Diese Bestimmungen sind deutlich weniger strikt als die Regelungen in anderen europ&auml;ischen L&auml;ndern, die zum Teil generelle Rauchverbote eingef&uuml;hrt haben. Gusenbauer zeigte sich aber zufrieden: &quot;Unbefriedigend w&auml;re, wenn die Dinge so bleiben, wie sie sind.&quot; Die Regelung zeuge von &quot;Augenma&szlig; und Hausverstand&quot;, erg&auml;nzte Vizekanzler Wilhelm Molterer. <br><br>&quot;Mir ist wichtig, dass in &Ouml;sterreich die Wahlfreiheit bei gleichzeitig maximalem Nichtraucherschutz gewahrt bleibt&quot;, so Kdolsky. Ein radikales Rauchverbot w&auml;re ein zu gro&szlig;er Eingriff in die Selbstverantwortung der &Ouml;sterreicher.</td>
