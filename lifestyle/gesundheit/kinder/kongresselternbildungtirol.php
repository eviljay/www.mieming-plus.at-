  <td colspan="3"><h2><a name="031f2a99e60f55c18"></a>Sichere Bindung zwischen Eltern und Kindern</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Die&ldquo; Elternbildung Tirol&ldquo; veranstaltete am Freitag, 9. November 2007 einen viel beachteten Kongress zum Thema: Auf dem Weg zu einer sicheren Bindung &ndash; und die Auswirkungen f&uuml;r die psychische, soziale und emotionale Entwicklung des Kindes. Mit 160 teilnehmenden Fachleuten war die Veranstaltung in der Hermann-Gmeiner-Akademie restlos ausgebucht.<br><br>Die Entwicklung einer ausreichenden sicheren Bindung zwischen Eltern und Kindern ist eine wichtige Basis f&uuml;r die psychische, emotionale und soziale Entwicklung des Kindes. &bdquo;In Zeiten von Pisa sollte man bei 80.000 Schulabbrechern pro Jahr in Deutschland &ndash; der Prozentsatz ist in &Ouml;sterreich etwa gleich hoch &ndash; vielleicht doch auch auf die psychosozialen und soziokulturellen Bedingungen f&uuml;r unsere Kinder schauen!&ldquo;, stellte der Hauptreferent Univ.-Prof. Dr. Gerhard J. Suess aus Hamburg in seinem Vortrag &uuml;ber Bindungsforschung in Erziehung und Beratung fest. Er zeigt auch klar auf, wie wichtig das Entstehen einer sicheren Eltern-Kind-Bindung in den ersten drei Lebensjahren ist.<br><br>Dr. Gerald Poscheschnik aus Innsbruck erl&auml;utert die Grundlagen und Ergebnisse der Bindungsforschung, Dr. Monika Wertfein vom Staatsinstitut f&uuml;r Fr&uuml;hp&auml;dagogik in M&uuml;nchen referiert &uuml;ber Bindung und emotionale Regulation, Prof. Gottfried Spangler von der Uni Erlangen-N&uuml;rnberg &uuml;ber die Anpassung im Lebenslauf. Sie stellen gemeinsam fest, dass f&uuml;r eine positive Entwicklung immer wichtig ist, kindliche Signale wahrzunehmen, richtig zu interpretieren, sowie prompt und angemessen zu reagieren.<br><br>Prof. Suess stellt das in Deutschland inzwischen vielfach erprobte Elternbildungsprogramm &quot;STEEP&quot; vor. Es ist ein Beratungs- und Fr&uuml;hinterventionsprogramm, das auf der Bindungstheorie basiert und in den USA&nbsp;entwickelt wurde. STEEP&trade; bedeutet &quot;Steps toward effective and enjoyable parenting&quot; (&quot;Schritte hin zu gelingender und Freude bereitender Elternschaft&quot;). Es ist ein komplexes Fr&uuml;hinterventionsprogramm, das auf unterschiedlichen Ebenen ansetzt und die Eltern-Kind-Beziehung besonders beachtet. Es will unterst&uuml;tzen, damit Elternschaft gelingt und tr&auml;gt so zu verbessertem Kinderschutz bei.<br><br>Die &bdquo;Elternbildung Tirol&ldquo; wird versuchen, dieses Programm ehest m&ouml;glich in Tirol zu initiieren, um Situationen wie aktuell mit dem kleinen Luca durch fr&uuml;heste Interventionen gar nicht entstehen zu lassen. &bdquo;Das k&ouml;nnte das dritte konkrete Kongress-Projekt werden, das erste war die Elternbildungsstelle selbst, das zweite ein V&auml;terzentrum,&ldquo; stellen Univ.-Prof. Dr. Josef C. Aigner und Mag. Franz Preishuber der Obmann des &Ouml;sterr. Kinderschutzbundes / Elternbildung Tirol sehr zufrieden mit dem Kongressverlauf fest.</td>
