            <tr bgcolor="#EFEFEF">
	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Der 9. Franz&ouml;sische Fr&uuml;hjahrsmarkt war wiederum erfolgreicher Ausdruck einer gelebten Partnerschaft der Gemeinden Limas / D&eacute;partement Rh&ocirc;ne im franz&ouml;sischen Beaujolais und Mieming im &ouml;sterreichischen Tirol. Zum Markt der K&ouml;stlichkeiten reisten rund 40 Aktive aus Limas an, unter ihnen B&uuml;rgermeister Michel Thien, Altb&uuml;rgermeister Hubert Boulaud, Pascal Girin (Pr&eacute;sident de Comit&eacute; de Jumelage) und Philippe Bachelet (Vice-Pr&eacute;sident de Comit&eacute; de Jumelage). Der Markt am Pfingstsamstag, dem 22. Mai 2010 war eine Premiere f&uuml;r Dr. Franz Dengg als B&uuml;rgermeister der Gemeinde Mieming und Wolfgang Schatz, seinem neuen Partnerschaftsobmann. &bdquo;Wir stehen hinter der Partnerschaft mit Limas&ldquo;, sagte B&uuml;rgermeister Franz Dengg beim Empfang der franz&ouml;sischen Delegation am Pfingstsonntag im Sch&uuml;tzenhaus in Untermieming, &bdquo;m&ouml;chten die Partnerschaft aber in Mieming auf eine gesellschaftliche Organisationsform bringen, &auml;hnlich wie in Limas&ldquo;. 	  	  </td>
