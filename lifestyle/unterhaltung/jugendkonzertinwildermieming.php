	                              <td class="cell" align="left" valign="top">
		      	        Weit &uuml;ber 100 Zuh&ouml;rerinnen und Zuh&ouml;rer besuchten am Samstag, dem 31. Mai 2008 das Jugendkonzert der Musikkapelle Wildermieming im Gemeindehaus. Das erste Konzert in diesem Jahr wurde von Christoph Wohlmuth geleitet. Der Kindergarten Wildermieming beteiligte sich mit St&uuml;cken wie &quot;Die flei&szlig;igen Handwerker&quot; und &quot;Die Waschfrauen&quot; am abwechslungsreichen Programm. Das Jugendkonzert wurde von der Querfl&ouml;tistin Eva Volgger moderiert. Unter den G&auml;sten waren u.a. B&uuml;rgermeister Josef Stoll, Vize-Bgm. Karl Soraperra und der Obmann der Musikkapelle Wildermieming, Herbert Oberdanner.&nbsp;</font>
