	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Der Kehraus am Faschingsdienstag in Mieming auf dem Gemeindeplatz bei den Wagen von Liechtmess- und Tuiflverein Mieming. verabschiedet die Fasnacht. Seit 2003 f&auml;hrt der Liechtmesswagen durch die Fasnacht auf dem Mieminger Plateau. Station wird an vier bis f&uuml;nf Standorten der Gemeinde gemacht, aber auch in Obsteig und Wildermieming. Um 14 Uhr am Faschingsdienstag trafen sich heuer wieder zahlreiche Fasnachter bei den Wagen und die meisten hielten durch bis 0 Uhr, bis zum Aschermittwoch. Wer Spa&szlig; an der Freud hatte war dabei. Eine gute Gelegenheit, das letzte Mal die Maske anzulegen. Der Mieminger Liechtmessverein m&ouml;chte im n&auml;chsten Jahr die alte Fasnachts-Tradition wieder aufleben lassen, so wie es in den 70er und 80er Jahren war. Ein Hauch von Politik wehte durch die Kehraus-Wagen: B&uuml;rgermeisterkandidat Franz Dengg war mit Mitgliedern seiner Liste 2 &quot;Gemeinsam f&uuml;r Mieming&quot; bei den standhaften Fasnachtern. 	  	  </td>
