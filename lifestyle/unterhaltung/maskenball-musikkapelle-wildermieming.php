            <tr bgcolor="#EFEFEF">
	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Ausgefallene Masken und kost&uuml;mierte Themengruppen pr&auml;gten den Maskenball im Gemeindezentrum in Wildermieming. Der stimmungsvolle und farbenfrohe Ball wurde wieder von den Mitgliedern der Musikkapelle Wildermieming unter der organisatorischen Leitung von Obmann Herbert Oberdanner veranstaltet. Weit &uuml;ber 300 G&auml;ste kamen zum 2. Maskenball nach Wildermieming. F&uuml;r stimmungsvolle Tanzmusik sorgte das Joggl-Trio aus Oberhofen. Musikalische Einlagen gab es auch von der maskierten &bdquo;Gugga&ldquo;-Musikkapelle Wildermieming. Nach Mitternacht wurden die besten Masken pr&auml;miert. Die Jury war mit den politischen Listenf&uuml;hrern besetzt - die bevorstehende Gemeindewahl l&auml;sst auch in Wildermieming gr&uuml;&szlig;en. Originell war auch das Sch&auml;tzspiel. Gefragt war die Summe der Kabelbindermenge in einer Kugel und dem Gewicht des MK-Obmannes. 	  	  </td>
