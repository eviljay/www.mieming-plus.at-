    <td></td>
    <td valign="top"><p>Wie beim Mieminger Maskenball spielte auch in Wildermieming die bevorstehende Gemeindewahl eine un&uuml;bersehbare Rolle. In der Gruppenwertung kam die Freiwillige Feuerwehr Wildermieming mit ihrer &bdquo;Urgestein-Liste 5&ldquo; auf den ersten Platz. Die Feuerwehrleute meinten, zu den kandidierenden vier Listen ihres Dorfes, k&ouml;nnte eine weitere zus&auml;tzliche Akzente setzen. In der Einzelwertung belegte Marie-Luise Haider als &bdquo;Wahlzuckerl&ldquo; den ersten Rang. Ihren Kopf zierten die Fotos der vier Listenf&uuml;hrer im Gemeinde-Wahlkampf.</p>
