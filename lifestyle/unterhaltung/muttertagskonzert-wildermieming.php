  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Mit der &bdquo;Intrada&ldquo; wurde das Konzert der Jugendmusik Wildermieming am Muttertag eingeleitet. Es folgten &bdquo;Final Countdown&ldquo; der schwedischen Rockgruppe Europe im Arrangement f&uuml;r Blasmusik, &bdquo;Irish Dream&ldquo;, der &bdquo;Festliche Marsch f&uuml;r 4 Fl&ouml;ten&ldquo; und die Wildermieminger Tanzlmusik. Sehr stimmungsvoll war auch &bdquo;Schweigls Hausmusik&ldquo; mit den Geschwister-Solisten Anna Schweigl (Harfe) und Johannes Schweigl (Steirische). Mit der &bdquo;Bl&auml;serweise&ldquo;, &bdquo;The Four Seasons&ldquo;, dem &bdquo;Spider Rag&ldquo; und Military Escort&ldquo; war der offizielle Teil des Konzertes beendet.</p>
