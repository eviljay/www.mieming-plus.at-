  <td></td>
  </tr>
      <tr>
    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Die Jugendmusik Wildermieming begeisterte ihr Publikum beim Muttertagskonzert am Sonntag, dem 9. Mai 2010. Um die 150 G&auml;ste erlebten ein facettenreiches Konzert der 30k&ouml;pfigen Jugendmusik in der Musikkapelle Wildermieming. Das Konzert wurde abwechselnd geleitet von Kapellmeister Alois Kranebitter und Jugendkapellmeister Jakob Koch. Es moderierte die Fl&ouml;tistin Ines J&auml;ger. Zum Auftakt stellte Stefan Steinhauser (Jugendreferent der MK Wildermieming) das Programm vor. Das Muttertagskonzert markiert inzwischen eine kleine Tradition im Wildermieminger Musikleben. 	  	  </td>
    </tr>
