  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Niemand wei&szlig; mehr so genau, wann der erste Feuerwehrball gefeiert wurde. Jedenfalls hat die Freiwillige Feuerwehr Mieming in ihrer 132j&auml;hrigen Geschichte immer erfolgreiche Feste organisiert. Der Feuerwehrball wird traditionell Ende J&auml;nner veranstaltet und hat eine jahrzehntelange Tradition. Einer fehlte: Feuerwehrkommandant Thomas Perkhofer lie&szlig; sich von seinem Stellvertreter Klaus Scharmer entschuldigen, er hatte Nachtdienst.</td>
