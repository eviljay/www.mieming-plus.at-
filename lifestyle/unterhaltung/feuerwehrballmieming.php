    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td class="cell" align="left" valign="top">
		      	        Weit &uuml;ber 350 Ballbesucher kamen am Samstag, dem 24. J&auml;nner 2009 zum traditionellen Feuerwehrball der Freiwilligen Feuerwehr Mieming. Der stellvertretende Feuerwehrkommandant Nikolaus Scharmer begr&uuml;&szlig;te zur Er&ouml;ffnung unter den G&auml;sten u.a. B&uuml;rgermeister Dr. Siegfried Gapp mit Gattin Margit sowie die Feuerwehren aus Silz, Haiming, M&ouml;tz, Stams, Wildermieming, Rietz und Telfs. Tanzmusik spielten die &bdquo;Tiroler Alpenkavaliere&ldquo;. Getanzt und gefeiert wurde im Gemeindesaal und in zwei Bars.  Vielfach in Anspruch genommen wurde der &quot;Heimbringerdienst&quot; der Freiwilligen Feuerwehr.&nbsp;</font>
	  	  </td>
