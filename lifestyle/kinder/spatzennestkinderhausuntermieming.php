		      	        52 Kinder aus Mieming, Wildermieming, M&ouml;tz und Silz werden zur Zeit im Montessori-Kinderhaus &bdquo;Spatzennest&ldquo; in Untermieming betreut. Darunter Max, Maxi und Moritz. &bdquo;80 Prozent der Kindergartenkinder kommen aus Mieming&ldquo;, sagt die Kinderhaus-Leiterin Bernadette Kohl. Das &bdquo;Spatzennest&ldquo; wurde am 3. J&auml;nner 2004 gegr&uuml;ndet und feiert heuer sein f&uuml;nfj&auml;hriges Bestehen. Im &bdquo;Spatzennest&ldquo; werden Kinder zwischen eineinhalb und zehn Jahren als Pers&ouml;nlichkeiten mit ihren Bed&uuml;rfnissen ernst genommen. Das &quot;Nest&quot; ist ein alter Bauernhof in Untermieming-See. 	  	  </td>
