	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Das Kinderbuch zum Osterfest kl&auml;rt die alte Frage, warum ausgerechnet der Hase die Eier bemalt. Warum nicht die Henne und auch kein anderes Tier die Eier bunt malt. Das Buch &quot;Pepi&quot; von Angela Annecke gibt auch Antwort auf die wichtige Frage, warum der Hase dann auch noch die Eier versteckt, jedes Jahr zur Osterzeit. Wichtige Fragen der Kindheit mit einer lustigen Geschichte erkl&auml;rt und 18 grafischen Illustrationen veranschaulicht.Die Geschichte wurde vorab in zwei Kindertagesst&auml;tten getestet und f&uuml;r gut, lustig und in sich stimmig befunden. Das Buch ist f&uuml;r Kinder ab vier Jahren geeignet. Zum Osterhasen-Buch &quot;Pepi&quot; gibt es noch eine CD, die neben dem H&ouml;rbuch &quot;Pepi&quot; auch 3 Kinderlieder enth&auml;lt. Die Texte der Songs sind im Buch enthalten. Durch die eing&auml;ngigen und leichten Melodien singen Kinder sehr schnell mit, aber auch Erwachsene haben Freude daran. Die Texte sind lustig, leicht zu lernen und wecken die Vorfreude auf die warme Jahreszeit. Unser Geschenktipp zum Osterfest. 	  	  </td>
