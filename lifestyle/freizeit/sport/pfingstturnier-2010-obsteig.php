	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Am Pfingstwochenende veranstaltete die SPG Mieminger Plateau auf dem Sportplatz in Obsteig das 34. Internationale Pfingstturnier. 12 Mannschaften nahmen am 22. und 23. Mai 2010 am Kleinfeldturnier teil. Turniersieger war der FC Urdorf I. Urdorf liegt im schweizerischen Kanton Z&uuml;rich. Am Abend des 1. Turniertages, Samstag, 22. Mai, wurde im Festzelt beim Sportplatz das Champions League-Finalspiel zwischen Inter Mailan und dem FC Bayern M&uuml;nchen &uuml;bertragen (Endstand: 2:0). Danach spielten die &bdquo;Trenkwalder&ldquo;. Vor dem Fr&uuml;hschoppenkonzert am Finalsonntag, 23. Mai mit der Musikkapelle Obsteig (Ltg. Kapellmeister Stefan Laube) schossen die Sch&uuml;tzen Obsteig einen Ehrensalut f&uuml;r die Turniermannschaften. Nach der Siegerehrung, am sp&auml;ten Nachmittag, spielten abends die &bdquo;Zillertaler Mander&ldquo; im Festzelt. 	  	  </td>
