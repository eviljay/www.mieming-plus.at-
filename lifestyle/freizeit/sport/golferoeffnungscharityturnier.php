	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Mit einem gro&szlig;en Er&ouml;ffnungs&#8208;Charity&#8208;Turnier zu Gunsten der Hilfsorganisation AMREF wurde am Samstag, dem 16. August 2008 in Mieming der neue 18&#8208;Loch&#8208;Championcourse feierlich eingeweiht. Mit dabei waren &uuml;ber 150 Golferinnen und Golfer, darunter zahlreiche Prominente wie Hansi Hinterseer oder Harald Krassnitzer. Ein H&ouml;hepunkt jagte den anderen: Neun internationale Spitzenk&ouml;che und sieben renommierte &ouml;sterreichische Winzer verw&ouml;hnten die G&auml;ste auf der neuen St&ouml;ttlalm und am Abend im Hotel Schwarz mit einem Gala Dinner. Alle Blicke richteten sich gen Himmel, als am Nachmittag der Mieminger Golf-Club-Pr&auml;sident und fr&uuml;here Weltrekordler im Skispringen, Armin Kogler und dem Weltrekordler im H&ouml;henflug, Mike K&uuml;ng, mit einem Gleitschirm von der Wankspitze kommend am Abschlag 10, bei der St&ouml;ttlalm punktgenau landeten. Die Golfer spendeten der Hilfsorganisation AMREF aus den Erl&ouml;sen des Turniers 4000 Euro. 	  	  </td>
