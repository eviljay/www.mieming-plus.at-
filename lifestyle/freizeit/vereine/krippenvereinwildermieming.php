  <td colspan="3"><h2><a name="031f2a9b910a0b525"></a>Krippendorf Wildermieming</h2></td>
  <td></td>
  </tr>
      <tr>
    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Wildermieming scheint auf einem guten Weg zu sein, ein Krippendorf zu werden. Die Krippenbauer aus Wildermieming finden mehr und mehr Akzeptanz in den Medien und der interessierten &Ouml;ffentlichkeit. Seit 1965 gibt es einen Krippenverein in Wildermieming. Initiator war der fr&uuml;here Schulleiter Josef Weber. Er baute mit seinen Sch&uuml;lerinnen und Sch&uuml;lern die ersten Krippenberge. 1967 wurde erstmals eine Freikrippe beim Kriegerdenkmal bei der Kirche aufgestellt. Das war die erste Dorfkrippe Wildermiemings. Seit 1989 leitet Erwin Auer den Krippenverein. 	  	  </td>
    </tr>
</table><br><br>
</td>
    <td></td>
    </tr>
    
