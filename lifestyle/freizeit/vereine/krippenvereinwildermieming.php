  <td></td>
  <td colspan="3"><h2><a name="031f2a9b910ade32f"></a>Krippenlokal in der Volksschule</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Der Krippenverein plant f&uuml;r die Dorfkrippe im neuen Domizil beim &bdquo;Schuster&ldquo; eine Erweiterung, damit auch die K&ouml;nige stilvoll pr&auml;sentiert werden k&ouml;nnten. Landtagspr&auml;sident DDr. Herwig van Staa kaufte das Haus und will auch k&uuml;nftig vor dem Haus den Platz f&uuml;r die Dorfkrippe zur Verf&uuml;gung stellen. Das Krippenlokal des Vereins befindet sich seit November 2000 in den Kellerr&auml;umen der Volksschule, zuvor war es 13 Jahre lang im Hause Auer untergebracht.</p>
