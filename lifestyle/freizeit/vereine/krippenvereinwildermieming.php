  <td></td>
  <td colspan="3"><h2><a name="031f2a9b910a8d62d"></a>Die Dorfkrippe von Wildermieming</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Die erste Dorfkrippe stand beim &bdquo;Hummel&ldquo;, d.h. bei Max und Albina Hafele. Sie wurde zum 77. Landeskrippentag 1992 in Wildermieming gebaut. Die Einweihung der neuen Dorfkrippe war neben einer gro&szlig;en Krippenausstellung, der Landestagung und einem Adventsingen damals der H&ouml;hepunkt des Landeskrippentages. Die Dorfkrippe stand sp&auml;ter u.a. vor der Gemeindeverwaltung, beim &bdquo;Ragg&ldquo; (heute Atelier Tiefengraber) und aktuell beim Schuster, Heinrich.</p>
</td>
    <td><img src="../../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
