  <td colspan="3"><h2><a name="031f2a9b910aba82e"></a>Die drei Bilder der Dorfkrippe</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Die Dorfkrippe wird in drei Bildern dargestellt: Das erste Bild &bdquo;Verk&uuml;ndigung an Maria&ldquo;, wurde geschnitzt vom akademischen Bildhauer Georg Eigentler aus G&ouml;tzens und gefasst von Franz Niederhauser aus Thauer. Das zweite Bild ist &bdquo;Die Geburt Christi&ldquo;, geschnitzt von Peter Volgger aus Uttendorf im Pinzgau (Land Salzburg) und gefasst von Michael Schretthauser aus Innsbruck. Das dritte Bild, &bdquo;Anbetung der K&ouml;nige&ldquo; schnitzte wieder Georg Eigentler. Gefasst wurde es von Franz Niederhauser.</p>
</td>
    <td><img src="../../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
