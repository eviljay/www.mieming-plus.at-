    <td></td>
    <td valign="top"><p>Peter Kraler ist auf dem Mieminger Plateau bekannt wie kaum ein anderer. Kaum vorstellbar, dass es jemanden gibt, der den &quot;Wander-Peter&quot; nicht kennt. Das Urgestein der Bergwelt geh&ouml;rt unverr&uuml;ckbar zu den Originalen in unserer Region. Der naturverbunde Bergwanderf&uuml;hrer ist immer gut aufgelegt und mag die Menschen, die sich ihm anvertrauen. &quot;Ich m&ouml;chte meine Erfahrungen und Erlebnisse weiter geben&quot;, sagt er im Gespr&auml;ch mit Mieming Plus und &quot;es gibt so viel zu erz&auml;hlen&quot;.</td>
