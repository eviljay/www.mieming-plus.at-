		      	        Das 1.554 Meter hochgelegene Lehnberghaus bei Obsteig hat seit Mitte Dezember 2009 Wintersaison. F&uuml;r Skitourengeher, Wanderer und Rodler lohnt sich in diesen Wochen der Weg dorthin besonders. Er f&uuml;hrt ausgehend vom Gasthof Arzkasten (Weiler Arzkasten, Obsteig) etwas &uuml;ber eine Stunde (1 Stunde 15 Minuten) durch zauberhafte Schneelandschaften. Das Lehnberghaus wird seit &uuml;ber 30 Jahren von den H&uuml;ttenwirten Hans und Linde Auer aus Obsteig gef&uuml;hrt. Bei ihnen treffen sich Bergler und Wanderer, viele sind seit Jahrzehnten Stammg&auml;ste bei den Auer&rsquo;s. 	  	  </td>
