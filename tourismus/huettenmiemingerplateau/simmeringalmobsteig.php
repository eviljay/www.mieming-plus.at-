  <td colspan="3"><h2><a name="22426398f60ffed03"></a>Simmering hoch &uuml;ber dem Inntal</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Der Simmering (2.096 m), die &ouml;stlichste Erhebung im Zug Tschirgant-Simmering, ist eine vorgeschobene Kuppe hoch &uuml;ber dem Inntal. Auf der Simmering Alm findet man besonders im Juni ein wahres Blumenparadies. Der Aufstieg ist eher einfach. Beeindruckend ist die ausgezeichnete Aussicht ins Inntal und auf das <a href="../../region/miemingerplateau/miemingerplateau.html">Mieminger Plateau</a> und die Gebirgsz&uuml;ge des mittleren Oberinntals.</p>
