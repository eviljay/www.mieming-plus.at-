    <td></td>
    <td valign="top"><p>Heiteres und Gen&uuml;ssliches von bemerkenswerten Tirolerinnen und Tirolern: Monika Elisabeth Meller wollte wissen, wie das ist mit dem Essen und Trinken in Tirol, ob es die so genannte &bdquo;Tiroler Kost&ldquo;, von der gern behauptet wird, sie sei so schwer, gibt und ob davon noch etwas geblieben ist. Dazu musste die Autorin eine Menge Fragen stellen, die ihr allesamt bereitwillig beantwortet wurden. Ihr Res&uuml;mee: Es gibt die Tiroler Kost, und es wird sie immer geben. In verfeinerter, erweiterter und ges&uuml;nderer Art tr&auml;gt sie nach wie vor einen betr&auml;chtlichen Teil zur Lebensqualit&auml;t der Tirolerinnen und Tiroler bei. Kurzum: Der Kn&ouml;del ist also noch immer rund &ndash; und das ist gut so.<br><br>Das vorliegende Tiroler Kochbuch ist kein &bdquo;gew&ouml;hnliches&ldquo; Kochbuch &ndash; es bietet etwas Besonderes. Nicht nur werden eine F&uuml;lle anregender Rezepte vorgestellt, sondern auch &ndash; ganz pers&ouml;nlich &ndash; deren K&ouml;chinnen und K&ouml;che &ndash; allesamt bekannte Pers&ouml;nlichkeiten des &ouml;ffentlichen Lebens in Tirol, deren Kochgewohnheiten, Vorlieben und Erlebnisse sowie Anekdoten rund ums Thema K&uuml;che alle &bdquo;Neugierigen&ldquo; interessieren werden.<br><br>Monika-Elisabeth Meller ist es gelungen, ein kleines Tiroler Gesellschaftspanorama &ndash; garniert mit zahlreichen Rezepten - vorzulegen, das die Vielfalt des Landes, seiner Bewohnerinnen und Bewohner sowie deren (Ess-) Kultur aufgreift. <br>Viel Vergn&uuml;gen und Freude beim Schm&ouml;kern und gutes Gelingen beim Nachkochen der Speisen.</td>
