  <td colspan="3"><h2><a name="22426398e907fb701"></a>Eine Region stellt sich vor</h2></td>
  <td></td>
  </tr>
      <tr>
    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#B22121">
	                    <td class="rowheader" align="left" valign="top">
      	        Das <b>Mieminger Plateau</b> begleitet das Inntal als n&ouml;rdliche Mittelgebirgsterrasse zwischen Telfs und dem Abfall in das Gurgltal beim Holzleitensattel im Westen, wobei ein stufenweiser Anstieg des Plateaus von rund 850 m &uuml;ber Obsteig (1000 m) bis zum Holzleitensattel (&uuml;ber 1100 m) festzustellen ist. Die teils ebene, teils st&auml;rker geformte und durchfurchte Plateaulandschaft ist ein Ergebnis der letzten Eiszeit. Ihre s&uuml;dliche Begrenzung zum Inntal bildet der niedrige Felskamm des Achberges. Das Landschaftsschutzgebiet Mieminger Plateau im Bereich der Gemeinden von Nassereith und Obsteig besteht seit 1981 und umfasst als Besonderheit vor allem die L&auml;rchenwiesen im Bereich des Holzleitensattels. Siedlungsspuren auf diesem durch seine Sonnenlage klimatisch bevorzugten Gebiet reichen in die Steinzeit zur&uuml;ck. Heute ist das Mieminger Plateau als beliebtes Wohngebiet durch eine starke Siedlungsexpansion gepr&auml;gt. [ <a href="../22426398e9089ca0f/index.php">Bilder rund um das Mieminger Plateau</a> ] &nbsp;</font>
	  	  </td>
    </tr>
</table><br><br>
</td>
    <td></td>
    </tr>
    
