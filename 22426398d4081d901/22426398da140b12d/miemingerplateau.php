  <td></td>
  <td colspan="3"><h2><a name="22426398da1411d30"></a>Das Mieminger Sonnenplateau</h2></td>
  <td></td>
  </tr>
      <tr>
    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td class="cell" align="left" valign="top">
		      	        <b>Landschafltiche Vielfalt:</b> Das Mieminger Plateau (fr&uuml;her: <a href="./miemingerberg.php">Miemingerberg</a>) ist eine zwischen 850 m bis 1000 m hoch gelegene Mittelgebirgsterrasse oberhalb des Tiroler Oberinntals, am S&uuml;dfu&szlig; des <a href="../22426398f00940101/index.php">Mieminger Gebirges</a>. Die Hochfl&auml;che ist etwa 14 km lang und bis zu 4 km breit. Es f&auml;llt bis auf die Neigungen bei <a href="../22426398e20c2db2b/telfs.php">Telfs</a> und <a href="../22426398e20c2db2b/moetz.php">M&ouml;tz</a> 200 m steil ins Inntal ab und endet im Westen beim Holzleitensattel, wo die Stra&szlig;e weiter ins Gurgltal und zur Fernpa&szlig;strecke f&uuml;hrt. Das Gebiet ist gekennzeichnet durch eine kleinr&auml;umige Teilung in Wiesen und &Auml;cker, die sich mit L&auml;rchenwiesen und lichten W&auml;ldern abwechseln. Ein Netz von Spazier- und Radwegen und im Winter von Langlaufloipen durchzieht das Plateau und macht es zu einem beliebten Erholungsgebiet. &nbsp;</font>
	  	  </td>
                          <td class="cell" align="left" valign="top">
		      	                  	
        	
	    	
	              	      <a href="../../images/22426398da1413331.jpg" target="_blank"><img src="../../images/22426398da1413331cols2.jpg" width="250" height="187" alt="" border="0"></a>
	    	    	  	  </td>
    </tr>
      <tr bgcolor="#ffffff">
                              <td class="cell" align="left" valign="top">
		      	                  	
        	
	    	
	              	      <a href="../../images/22426398da142a03d.jpg" target="_blank"><img src="../../images/22426398da142a03dcols2.jpg" width="250" height="187" alt="" border="0"></a>
	    	    	  	  </td>
                          <td class="cell" align="left" valign="top">
		      	        <b>Das </b><a href="../22426398f00940101/index.php">Mieminger Gebirge</a><b>:</b> (auch <a href="../22426398f00940101/index.php">Mieminger Kette</a> genannt) ist eine Gebirgsgruppe der N&ouml;rdlichen Kalkalpen in den Ostalpen. Es befindet sich im Bundesland Tirol/&Ouml;sterreich. Die Untergruppe befindet sich etwas im Schatten des ber&uuml;hmteren, direkt im Norden anschlie&szlig;enden Wetterstein. W&auml;hrend das Gebiet um Coburger H&uuml;tte, Seeben- und Drachensee im Westen (Ehrwalder Sonnenspitze und Vorderer Tajakopf mit neuem Klettersteig &uuml;ber die Tajakante) und die <a href="../22426398f00940101/22426398f009d5c13/index.php">Hohe Munde</a> (s. Foto) ganz im Osten regen Besuch erhalten, ist es im wenig erschlossenen Mittelteil sehr ruhig geblieben. Die Hohe Munde ist auch ein beliebtes anspruchsvolles Skitourenziel. &nbsp;</font>
	  	  </td>
    </tr>
