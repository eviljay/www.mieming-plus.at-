	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Zu einer ersten Jahressitzung traf sich am Donnerstag, dem 22. April 2010 am Abend die Werbegemeinschaft Mieminger Plateau im Gasthof L&ouml;wen in Mieming-Barwies. Obmann Dietmar Falch begr&uuml;&szlig;te die B&uuml;rgermeister der Plateau-Gemeinden Mieming &ndash; Dr. Franz Dengg, Wildermieming &ndash; Klaus Stocker, Obsteig &ndash; Hermann F&ouml;ger, den Wirtschaftsbund-Obmann Martin Larcher und &uuml;ber 30 TeilnehmerInnen aus Handwerk, Kaufmannschaft, Gewerbe und Tourismus. 	  	  </td>
