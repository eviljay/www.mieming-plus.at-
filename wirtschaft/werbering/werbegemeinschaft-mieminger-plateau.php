  <td colspan="3"><h2><a name="8627589d6a0cd0236"></a>Mit den Ortschefs wurden Pl&auml;ne diskutiert</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Zu der April-Sitzung wurden die neuen B&uuml;rgermeister der drei Plateaugemeinden geladen: Dr. Franz Dengg (Mieming), Klaus Stocker (Wildermieming) und Hermann F&ouml;ger (Obsteig). Gemeinsam mit den vor sechs Wochen gew&auml;hlten Ortschefs wurden Fragen diskutiert wie Zeitung/Medien f&uuml;r das Plateau, Ausschreibung und Angebote der Gemeinden, Bauchdichte, Wirtschaftstreiben, Einkaufsgutscheine und Aktivit&auml;ten am Mieminger Plateau. Wirtschaft, Handel und Tourismus sichere die Haupteinnahmen der Gemeinde Mieming, sagte B&uuml;rgermeister Dengg. Die unternehmerischen Anstrengungen seien unverzichtbar zur Sicherung der Arbeitspl&auml;tze.</p>
</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
