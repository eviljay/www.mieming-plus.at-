  <td colspan="3"><h2><a name="8627589d6a0d32940"></a>Priorit&auml;ten der Aufgabenkatalog der Gemeinden</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Zum Auftakt der Werbegemeinschafts-Sitzung erhielten die B&uuml;rgermeister das Wort. Sie wurden um Statements der jeweiligen, anstehenden Gemeindevorhaben gebeten. B&uuml;rgermeister Dr. Franz Dengg setzt die Priorit&auml;ten seiner Gemeinde Mieming zun&auml;chst auf ergebnisorientierte Verhandlungen mit den Agrargemeinschaften mit dem Ziel, neue Baugr&uuml;nde ausschreiben zu k&ouml;nnen. B&uuml;rgermeister Klaus Stocker benennt f&uuml;r Wildermieming den Nordausbau der Siedlung als vorrangiges Ziel und der Obsteiger B&uuml;rgermeister Hermann F&ouml;ger beschreibt die Konsolidierungsbem&uuml;hungen seiner Gemeinde. &quot;Sparen und Fortschritt - zwei scheinbar gegens&auml;tzliche Vorhaben sind unsere Zukunftsaufgaben&quot;, so F&ouml;ger.</p>
</td>
