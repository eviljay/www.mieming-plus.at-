  <td colspan="3"><h2><a name="8627589d6a0db9c4a"></a>Vorgehen bei Gemeindeausschreibungen</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Wiederum an die Adresse der B&uuml;rgermeister richtete sich der, von der Bauwirtschaft vorgetragene Wunsch, bei Ausschreibungen heimischen Betrieben den Vorzug zu geben und die derzeitige Baumassendichte in Mieming (mind. 1 bzw. h&ouml;chstens 1,8) nicht zu ver&auml;ndern. Dadurch werde einerseits eine platzsparende Bebauung der Grundreserven sichergestellt, andererseits ein Ausufern der Gro&szlig;bauten eingeschr&auml;nkt, erkl&auml;rte dazu B&uuml;rgermeister Franz Dengg. &bdquo;Gemeinde-Ausschreibungen&ldquo;, so Dengg weiter, &bdquo;d&uuml;rfen niemanden bevorzugen, aber rund 99 % der Auftr&auml;ge gehen an heimische Betriebe&ldquo;. Ortsn&auml;he sei ein Kostenfaktor f&uuml;r die Unternehmen.</p>
</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
