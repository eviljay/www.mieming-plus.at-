  <td colspan="3"><h2><a name="8627589d6a0e4fa52"></a>Wirtschaftsmesse wird vorgeschlagen</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Die drei B&uuml;rgermeister der Plateaugemeinden sprechen sich f&uuml;r einen kontinuierlichen Gedankenaustausch im Kreise der Wirtschaftsschaffenden und dar&uuml;ber hinaus aus. B&uuml;rgermeister Dr. Franz Dengg weist noch auf die Werbem&ouml;glichkeiten im neu aufgelegten Gemeindef&uuml;hrers Mieming hin, der auch viele Neu-Mieminger erreiche und deshalb zielgruppengerecht werbe. Dietmar Falch regt die Organisation einer turnusgem&auml;&szlig;en Wirtschaftsmesse im Verbund mit dem Wirtschaftsbund an. Wirtschaftsbund-Obmann Martin Larcher will den Vorschlag zur Diskussion stellen. Dietmar Falch k&uuml;ndigte abschlie&szlig;end die Fortsetzung der Werbegemeinschafts-Gespr&auml;che an.</p>
