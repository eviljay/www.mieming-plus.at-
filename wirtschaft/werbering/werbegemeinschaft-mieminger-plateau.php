  <td colspan="3"><h2><a name="8627589d6a0d7e946"></a>Einkaufsgutscheine sollen st&auml;rker beworben werden</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Verschiedene Kaufleute, u.a. Othmar Maurer (Sportmoden Maurer, Mieming) Dietmar Falch (Elektro Falch, Mieming) setzen sich f&uuml;r verst&auml;rkte Werbema&szlig;nahmen zugunsten der Einkaufsgutscheine der Region ein. Raika-Gesch&auml;ftsf&uuml;hrer Andreas Grutsch: &bdquo;Diese Gutscheine, in der H&ouml;he von 5, 10, 20 und 50 Euro Euro k&ouml;nnen in der Raiffeisenbank Mieminger Plateau erworben werden und in allen teilnehmenden Betrieben eingel&ouml;st werden&quot;. Die Gutscheine eigneten sich auch als Gratifikationsleistung f&uuml;r Mitarbeiterleistungen, so Falch und seien in diesem Zusammenhang steuerlich absetzbar. Bis zu 186 Euro pro Mitarbeiter seien steuerfrei. &quot;Das investierte Geld bleibt auf dem Mieminger Plateau&quot;, argumentiert Falch erg&auml;nzend.</p>
</td>
    <td><img src="../../images/pcp.gif" width="20" height="100" alt="" border="0"></td>

    	
		
    	
