  <td colspan="3"><h2><a name="8627589d6a0df244d"></a>Tourismus und Handel auf dem Plateau</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Weitere Themen waren die Werbem&ouml;glichkeit an den Werbetafeln der Gemeinde und eine verbesserte Zusammenarbeit zwischen Tourismusverband und heimischer Wirtschaft. Was die Werbetafeln angehe, sei in Mieming eine rechtzeitige Reservierung von Bedeutung, so Franz Dengg. TVB-Obmann und B&uuml;rgermeister Hermann F&ouml;ger: &bdquo;Die Tourismusverb&auml;nde Mieminger Plateau und &bdquo;tirolmitte&ldquo; teilen sich mit je 200.000 Euro die Marketingma&szlig;nahmen in Kooperation&ldquo;. Deshalb habe der TVB nur noch eingeschr&auml;nkte Entscheidungskompetenz. In Sachen G&auml;stekarten seien allerdings bereits eingestandene Fehler gemacht worden, die man korrigieren werde.</p>
</td>
