  <td colspan="3"><h2><a name="8627589d6a0d65a43"></a>Zeitung f&uuml;r die Plateaugemeinden favorisiert</h2></td>
  <td></td>
  </tr>
          <tr>
    <td></td>
    <td valign="top"><p>Die Werbegemeinschaft spricht sich f&uuml;r ein gemeinsames Medienprojekt aus. Die B&uuml;rgermeister sollten pr&uuml;fen, ob ein Zeitungs- und Internetprojekt in gemeinsamer Verantwortung der Gemeinden Mieming, Wildermieming und Obsteig realisierbar sei. Dietmar Falch: &bdquo;Wir w&uuml;nschen uns eine gemeinsame Zeitung und ein gemeinsames Internetportal f&uuml;r das Mieminger Plateau&ldquo;. In Mieming, so Franz Dengg, sei man grunds&auml;tzlich an einem solchen Projekt interessiert, wolle aber erst einmal die hoch akzeptierte Dorfzeitung stabilisieren. &bdquo;Wir haben aber keine Einw&auml;nde, wenn sich die beiden anderen Gemeinden daran beteiligen w&uuml;rden&ldquo;, sagte Dengg.</p>
</td>
