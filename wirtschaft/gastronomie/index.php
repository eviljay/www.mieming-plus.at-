    <td></td>
    <td colspan="3"><table border="0" cellspacing="0" cellpadding="4" width="100%">

            <tr bgcolor="#EFEFEF">
	                              <td width="100%"  class="cell" align="left" valign="top">
		      	        Der renommierte Tiroler &quot;Gasthof zur Post&quot; in Mieming ist seit Mitte November 2009 wieder ge&ouml;ffnet. Die neuen Postwirte hei&szlig;en Selma und Selahattin Demirkiran. Urspr&uuml;nglich aus Trabzon in der T&uuml;rkei kommend, lebt die Familie Demirkiran seit 36 Jahren in Tirol. &quot;Nennt mich einfach Salati&quot;, sagt der neue Postwirt, &quot;denn meinen richtigen Vornamen, Selahattin, kann sich keiner so leicht merken&quot;. Salati pr&auml;gt sich schnell ein und der sympathische Vollblut-Gastronom macht es einem leicht. &quot;Ich m&ouml;chte ein wenig Schwung in die Mieminger Gastronomie bringen&quot;, sagt er im Gespr&auml;ch mit Mieming-Plus. Unser erster Eindruck: Das wird er und seine Familie m&uuml;hlelos schaffen. Der Gasthof zur Post ist weit &uuml;ber 500 Jahre alt und im Besitz der Familie Thaler. Ein altes Familien-Wappen am Stammtisch macht die Geschichte der fr&uuml;heren Poststation in Obermieming transparent. Die &laquo;Post&raquo; war immer der Gasthof im Ort, wo man sich traf und redete. Nicht selten - so wird &uuml;berliefert - wurde in den Poststuben auch Politik gemacht. [ <a href="./gasthof-zur-post-in-mieming.php">weiter</a> ]	  	  </td>
